                                        <p class="pt-serif">As listed by the American Psychiatric Association and supported by drug rehabilitation centres in India, the following are some of the symptoms that might be seen in the victims of drug addiction:</p>
                                        <ol class="ol">
                                            <li class="pt-serif">Overspending on drugs despite poor financial conditions</li>
                                            <li class="pt-serif">Extreme violent and disturbing symptoms observed just hours after drug withdrawal</li>
                                            <li class="pt-serif">Cutting off from social settings</li>
                                            <li class="pt-serif">Facing repeated failures while attempting to discontinue drugs</li>
                                            <li class="pt-serif">Continuing substance abuse even while facing turbulence in personal relationships</li>
                                            <li class="pt-serif">Spending most of the time in either scoring or taking drugs</li>
                                            <li class="pt-serif">Constantly increasing the dosage of the drugs since, becoming tolerant to the standard dose</li>
                                            <li class="pt-serif">Even after physical harm, unable to stay away from drugs</li>
                                            <li class="pt-serif">Stocking up an increased amount of drugs for a longer period</li>
                                            <li class="pt-serif">Intense cravings that are difficult to control – psychologically and physically</li>
                                        </ol>
                                        <p class="pt-serif">As analyzed by drug treatment centres in Tamilnadu and across India, the following are some of the physical symptoms that the victim’s family can spot:</p>
                                        <ol class="ol">
                                            <li class="pt-serif">Erratic mood shifts and unpredictability</li>
                                            <li class="pt-serif">Speech mis coordination</li>
                                            <li class="pt-serif">Extreme cases can cause internal bleeding</li>
                                            <li class="pt-serif">Lack of motor coordination</li>
                                            <li class="pt-serif">A sudden shift in the weight</li>
                                            <li class="pt-serif">Feeling confused or disoriented</li>
                                            <li class="pt-serif">Facing breathing issues</li>
                                        </ol>
