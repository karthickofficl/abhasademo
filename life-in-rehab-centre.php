                                                <li class="pt-serif"><i class="fa fa-circle"></i>I’m afraid of the guilt I'll feel.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>It’s going to be uninteresting.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>What will I actually do soon after?</li>
                                            </ul>
                                        <p class="pt-serif">There are many reasons why these fears should keep us from going to rehab, but turning fear into power is the key. Concerns can be turned into hope! Rehabilitation is how we build new, sober lives. Feeling anxiety, anxiety, and doubt are all normal emotions. Realise that this place can be used to your advantage.</p>
                                        <div class="pr-subtitle">Rehab is not prison.</div>
                                        <p class="pt-serif">Rehabilitation is a way to get away from the stresses and pressures of daily life so that you can focus exclusively on your health and wellbeing. You’ll be permitted to take the following items with you to rehab: </p>
                                            <ul class="ul">
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Casual, comfortable clothing.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Approved prescriptions, though the staff may take possession and distribute as needed.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Laundry supplies.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Personal hygiene items.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Electric razors.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>An alarm clocks.</li>
                                            </ul>
                                        <p class="pt-serif">A portion of the intake process contains a physical test to find out your own health and a detox test to determine when a person has recently used drugs or alcoholic beverages. 
In case a person hasn’t used this, you’ll go directly into treatment. In the event that you have, detoxification will start. Detoxification is daunting. The worry about withdrawal can be real. No one in particular really wants to be unpleasant. Fortunately, most therapy facilities provide choices that help ease this plus the discomfort of this drawback. Expect things like:
 </p>
                                            <ul class="ul">
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Non-narcotic medications to ease withdrawal symptoms.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Staff that regularly checks your blood pressure, heart rate and other vital signs.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Meetings with counsellors to discuss how you’re feeling.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Introductions to other patients.</li>
                                            </ul>
                                        <p class="pt-serif">The consequences of withdrawal can be mitigated with proper treatment. A lot of us have experienced the bad flu or virus that has knocked all of us out for a couple of days. Detoxing from drugs or alcohol is not any different. It’s the psychological part that’s difficult. You may feel alone, but you won’t be.   

                                        <br>You will be surrounded by compassionate staff and fellow addicts in recovery. People often bond quickly because all they have is each other’s emotions. Once the detoxification period is over, the real work begins. This does not operate in the sense that you’ll be doing manual work; this is actually the work of recovery. </p>
                                        <div class="pr-subtitle">What Do You Do in Rehab?</div>

                                        <ol class="ol">
                                            <li class="pt-serif"><b>Group Counselling</b>
                                                <p class="pt-serif">The major thing that you will notice is the number of group guidance sessions you will have. This will daunt in the beginning. All of us aren’t used to sharing the personal, and sometimes unpleasant, details of living with other people. There is catharsis in this. Becoming open develops, provides, and helps all of us heal. 
Team sessions may slow down on the weekends, but clients will be very involved with 12-step meetings, counselling classes, group meetings, and activities. Recovery is not about sealing ourselves off in a room and reading a book all day long. Although there’s nothing wrong with a good read, we need to learn how to make healthy, sober connections. All of us need to have the ability to live our lives and be available for therapy. </p>
                                            </li>
                                            <li class="pt-serif"><b>Recreational Activities</b>
                                                <p class="pt-serif">Quite a few think it will be boring and that there’s nothing fun to do, which couldn’t be further from the truth. While it isn’t intended to be all fun, people need to occasionally let go and have fun. So, when it comes to enjoying themselves, what do you do in rehab? At Doze Keys, we’re blessed to be situated on 10 beautiful waterfront acres in South Florida.   

                                                <br>This kind of proximity to the ocean allows people to offer one-of-a-kind treatment amenities. These kinds of amenities include skating, kayaking, and traveling. Our staff regularly schedules outdoor activities that allow the splendours of Mommy Nature to play a role in our client's restoration. Rehab is not really a static environment. Every day will be stuffed with activities, whether fun or therapeutic in nature. Completing treatment is one step in the lifelong restoration process. Once the time comes to leave, what will post-rehab life look like? </p>
                                            </li>
                                        </ol>
                                        <div class="pr-subtitle">What Is Life Like After Rehab?</div>
                                        <p class="pt-serif">Restoration doesn’t end once we leave rehab. Which initial feeling of anxiety and stress we had when we first crossed the threshold returned the day we walked out and about? Though treatment offers us the equipment to maintain true sobriety in the outside-the-house world, it still isn’t something we are used to.   

                                        <br>Typically, the safety and comfort of treatment have incubated our thoughts about the sometimes-harsh realities of the real world. Learning how to take training rims off and find equilibrium in restoration is what treatment prepares us for. How we carry on with these lives after treatment gives meaning to everything we accomplished while in treatment. Many aftercare programmes provide preparation for a life without drugs or alcohol.   

                                        <br>They include regular 12-step gatherings and follow-up solutions with counsellors. A living will be bundled with challenges, and aftercare will help you manage them. Beating years of medication is a multistep process. Acknowledging the need for an aligner is the first step. The second is entering treatment. This is creating a healthy new life post-rehab. While in treatment, recovering lovers attend a regular group of meetings, duties, and scheduled activities.   

                                        <br>Various duties and duties provide construction. Addiction aftercare helps recovering addicts deal with normal everyday activities that would have normally left us to experience helplessness. Learning how to manage emotions again is as important as sticking to a healthy routine. Medications forced the emotions down. Rehab instructs us on how to embrace our emotions and turn them into sources of inspiration. Aftercare helps us put those practices into basic principles. </p>

