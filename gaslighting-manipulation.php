                                    <h3>GASLIGHTING MANIPULATION</h3>
                                </div>
                                <div class="fl-wrap text-block">
                                    <div class="clearfix"></div>
                                    <p class="pt-serif">Psychologists use the word "gaslighting" to refer to a specific form of manipulation in which the manipulator attempts to make another individual (or group of individuals) question their own reality, memory, or perceptions. According to experts, it is usually a significant concern. </p>
                                    <p class="pt-serif">Gaslighting occurs in personal relationships (consider an abusive spouse or, on rare occasions, a parent), professional ties (a manipulative boss or co-worker preying on a subordinate), and even by public figures. Stern identifies multiple instances of gaslighting by President Donald Trump and his administration.</p>
                                    <p class="pt-serif">Whether it occurs in a marriage, between a leader and his or her constituents, or elsewhere, it's crucial to recognise the warning signs that you (or someone you know) may be a victimâ€”the first step in escaping an abusive situation.</p>

                                    <div class="pr-subtitle">Gaslighting and addiction</div>
                                    <p class="pt-serif">Gaslighting occurs frequently in addiction. It is used to manipulate loved ones so that the addict can continue engaging in addictive behaviour. When addicts persistently gaslight others, they continue to lie about their addictions, escalating their risk of mental and physical health problems. In order to begin the process of addiction treatment, it is crucial that the victims recognise the indicators of gaslighting (described in Part 1; include link).</p>
                                    <div class="pr-subtitle">How do you manage gaslighting?</div>
                                    <p class="pt-serif">Give one another space from the current conversation. Things might become confused with time, and both parties can become more rigid in their beliefs.</p>
                                    <p class="pt-serif">Engage in talks with trustworthy individuals who can provide you with sound advice and viewpoints you respect. It is preferable if these individuals are familiar with your past and do not simply relay information because you request it.</p>
                                    <p class="pt-serif">Finally, pay attention to your emotions. By engaging in a conversational loop with the abuser, you lose touch with your own emotions.</p>
                                    <p class="pt-serif">It is also advisable to gather evidence in case you need to refer back to what was stated to you or verify your facts. One may do so by:</p>
                                    <p class="pt-serif">Keeping a diary by noting the date, time, and specifics of events.</p>
                                    <p class="pt-serif">Taking photographs can be used as a fact-checking resource.</p>
                                    <p class="pt-serif">Take voice memos. This is a quick way to record events for future reference.</p>
                                    <div class="pr-subtitle">Gaslighting typically occurs in power relationships; however, it is not always deliberate or malicious.</div>
                                    <p class="pt-serif">Stern explains that recognising that you or someone you care about may be involved in a "gaslight tango" is not always as clear as it may seem because it typically begins in very subtle ways and frequently involves two people (or groups) who appear to care deeply for one another.</p>
                                    <p class="pt-serif">If it's someone you love and care about (such as a spouse or parent), you'll want to believe the other person, and the gaslighted may use that against you, explains Darlene Lancer, JD, MFT, a marriage and family practice therapist in private practice and author of "Co-dependency for Dummies and Conquering Shame and Co-dependency: Steps to Freeing the True You."</p>
                                    <div class="pr-subtitle">Be suspicious of gaslighting if you begin to question yourself frequently.</div>
                                    <p class="pt-serif">Stern explains that recognising that you or someone you care about may be involved in a "gaslight tango" is not always as clear as it may seem, because it typically begins in very subtle ways and frequently involves two people (or groups) who appear to care deeply for one another.</p>
                                    <p class="pt-serif">The word "gaslighting" originates from the 1938 play "Gas Light," which was later adapted into the 1944 film "Gaslight," in which a husband manipulates his wife into believing she is losing touch with reality so he may send her to a mental institution and take her inheritance. "Not all real-world examples are so evil," Stern explains.</p>
                                    <p class="pt-serif">A more subtle example would be a mother who consistently disapproves of her daughter's selections to the point where the daughter wonders her mother's approval of her choices. The mother may or may not be deliberately attempting to exert control over her daughter's every decision, but she is doing it by being excessively critical.</p>
                                    <p class="pt-serif">When a popular high school student gets another student to question his or her emotions or judgement in a certain situation, this is an example of gaslighting. When the latter student asks the more popular student why he did not reserve a spot at the lunch table, the more popular student deflects the subject by asking, "Why are you being so sensitive?" It is not that significant. I did not realise you were without a seat."</p>
