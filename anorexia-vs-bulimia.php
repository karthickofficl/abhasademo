                                    <h1>ANOREXIA vs. BULIMIA: WHAT’S THE DIFFERENCE? </h1>
                                </div>
                                <div class="fl-wrap text-block">
                                    <div class="clearfix"></div>
                                    <p class="pt-serif">Eating disorders are serious health conditions where people exhibit abnormal eating patterns and an obsession with food and the body. Such behaviors produce an adverse impact on the physical and mental well-being of the individual. Showing up with negative emotions, anxiety, or depression toward food as a mechanism of healing is a common cause of eating disorders. Abnormal eating, binge eating, or starving and restricting food intake in an obsession to reduce body weight are common causes of eating disorders. Such disorders are predominant in the teen and young adult stages, though they can affect anyone, irrespective of age.

                                        <br>This blog explains the different types of eating disorders and elaborates on the difference between anorexia and bulimia.
                                    </p>
                                    <div class="pr-subtitle">Types of Eating Disorders</div>
                                    <p class="pt-serif">There are six major <a rel="nofollow" href="https://en.wikipedia.org/wiki/Eating_disorder">types of eating disorders</a> including Anorexia Nervosa, Bulimia Nervosa, Binge-Eating Disorder, Muscle Dysmorphia, Compulsive Overeating, and Other Specified Feeding or Eating Disorders (OSFED). Anorexia Nervosa, Bulimia Nervosa, and Binge-Eating Disorder are the most prevalent types of eating disorders. Others include selective eating disorders, drowsiness, diabulimia, and so on. The following section describes Anorexia Nervosa vs. Bulimia Nervosa.</p>

                                    <div class="pr-subtitle">Anorexia Nervosa vs. Bulimia Nervosa</div>
                                    <div class="pr-subtitle">1. Definition: Anorexia vs. Bulimia</div>

                                    <div class="pr-subtitle">What is anorexia nervosa?</div>
                                    <p class="pt-serif">People with anorexia nervosa perceive themselves to be overweight despite being underweight, and they limit their food intake to an extreme that results in a sudden, abnormal loss of weight and malnourishment. Such people have such a constant fear of adding on weight, losing their fitness, or losing their body shape that they involve themselves in excess workouts and exercise. They constantly monitor their weight, restrict calorie intake, and refrain from eating certain types of food items. Their extreme effort to lose body weight drains them physically, resulting in potential health problems.</p>

                                    <div class="pr-subtitle">What is bulimia nervosa?</div>
                                    <p class="pt-serif">Bulimia nervosa is commonly called bulimia and is a case where individuals are involved in episodes of binge eating followed by purging. People affected by Bulimia Nervosa consume abnormally large quantities of food in a shorter span of time. They lose control over their eating habits and eat until they are extremely full. After bingeing, they are obsessed with compensating for the excess calories they consume. They fear gaining weight due to their overeating behaviors and feel guilty after bingeing. In an endeavor to shed the same, such individuals vomit and purge forcefully. Some people do take up severe exercises and workouts, while there are also others who use laxatives, diet pills, etc. to lose weight instantly. Such behaviors produce life-threatening outcomes and have a severe preoccupation with body shape and weight. Such binge eaters are usually shamed for exposing the amount of food they consume in public. What secret eaters they are! The difference between anorexia and bulimia is that a person affected by the former restricts food intake, while the latter consumes in excess and compensates by purging.</p>

                                    <div class="pr-subtitle">2. Symptoms: Anorexia Nervosa vs. Bulimia Nervosa</div>

                                    <p class="pt-serif">The difference between anorexia and bulimia lies in their symptoms. The main symptom of anorexia nervosa is limiting the intake of food with extreme dietary procedures, while the primary symptom of bulimia nervosa is purging the food they consume in excess in order to compensate for the calories they consume.</p>

                                    <div class="pr-subtitle">Symptoms of Anorexia Nervosa</div>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Losing weight in no time</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Eating very little</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Becoming anaemic and weak</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Constipation, bloating and feeling of fullness</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Thinning of bones</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Intolerance to cold climate</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Tiredness, dizziness, and fainting from dehydration</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Nails and hair strands may become brittle</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Women may experience irregular or no menstrual cycles</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Emotional disturbances like anxiety, depression, irritability, and lack of concentration</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Fatigue</li>
                                    </ul>

                                    <div class="pr-subtitle">Symptoms of Bulimia Nervosa</div>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Overeating often, mostly in private</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Vanishing after meals</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Gastroesophageal reflux and heartburn</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Erosion of tooth enamel by stomach acid</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Swelling of the salivary glands</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Unnecessary use of laxatives and dietary pills</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Sore throat and recurring diarrhea</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Dehydration, dizziness, and fainting</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Traces of food consumed in private, like vanishing food and empty wrappers and containers</li>
                                    </ul>
                                    <div class="pr-subtitle">3. Diagnosis: Anorexia Nervosa vs. Bulimia Nervosa</div>

                                    <p class="pt-serif">As far as both Anorexia Nervosa and Bulimia Nervosa and other types of eating disorders are concerned, there is no specific diagnostic procedure. A physician or therapist diagnoses the patient based on the observation of the symptoms he or she exhibits.<br>

                                    Diagnosis of eating disorders is usually challenging because people with such disorders try to hide the symptoms. Even if you have made an observation and you think that your loved one, a friend, or a relative has an eating disorder, it is important that you do not advise them spontaneously. If you initiate a conversation and are interested in letting them understand that they have a disorder, be prepared, for they will be in a mode of constant denial. Even if they truly perceive and understand that they are affected by an eating disorder, they are afraid of seeking help and medical advice. This makes the diagnosis of eating disorders relatively harder. There is some <a rel="nofollow" href="https://www.healthline.com/health/eating-disorders/anorexia-vs-bulimia"> difference between anorexia and bulimia in their modes of diagnosis.</a></p>

                                    <div class="pr-subtitle">How is anorexia nervosa diagnosed?</div>
                                    <p class="pt-serif">The physician or therapist initiates a diagnosis based on the symptoms and observations of their close ones or family members. The therapist may ask a few questions of the following kind:</p>
                                    <ol class="ol">
                                        <li class="pt-serif">History of the diet and food intake</li>
                                        <li class="pt-serif">How often does a person weigh himself?</li>
                                        <li class="pt-serif">Psychological assessment for stress, depression, and healing</li>
                                        <li class="pt-serif">Records of exercise, and fitness practices</li>
                                        <li class="pt-serif">Family history of eating disorders</li>
                                        <li class="pt-serif">Menstrual cycle records (if the affected is female)</li>
                                    </ol>
                                    <p class="pt-serif">Also, remember that the earlier you diagnose anorexia nervosa, the better the treatment is. If you know someone experiencing anorexia nervosa, do not delay taking them to <a href="https://www.abhasa.in/anorexia-nervosa-treatment/">consult a healthcare provider or a professional therapist.</a></p>
                                </div>
