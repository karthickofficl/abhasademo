                                    <div class="clearfix"></div>
                                    <p class="pt-serif">A panic attack is a sudden and acute sense of worry. Physical symptoms of panic attacks include trembling, disorientation, nausea, fast, irregular heartbeats, dry mouth, perspiration, and dizziness.</p>
                                    <p class="pt-serif">Though terrifying, panic attack symptoms are not hazardous. They can make you feel like you're experiencing a heart attack, collapse, or death. The majority of panic episodes last between five and thirty minutes. You may develop a fear of going out alone or in public areas because you're afraid of having another panic attack. Agoraphobia is a term used to describe when fear gets really severe.</p>
                                    <p class="pt-serif">Everyone has panic attacks at different times. Some people have one panic attack and never have another, while others have them on a daily basis or several times in a short period of time. You may realise that certain locations, events, or activities appear to set off panic episodes in you. They could occur, for example, before a tense appointment. </p>
                                    <p class="pt-serif">The majority of panic episodes last 5 to 20 minutes. They can strike at any time. Within 10 minutes, your symptoms will likely be at their worst. You can also have signs of a panic attack that last for a long time. This might indicate that you're having a second panic attack or that you're experiencing additional anxious symptoms.</p>
                                    <p class="pt-serif">Physical symptoms that resemble a heart attack, such as shaking, tingling, or a high heart rate, are common in <a href="https://www.abhasa.in/panic-disorder-treatment-center-in-mumbai/"> panic attacks.</a> An incident of panic might strike at any time. Many people with panic disorder are concerned about having another episode and may make considerable changes in their lives to avoid it. Panic attacks can strike as frequently as many times each day or as infrequently as once or twice a year.
                                    </p>
                                    <div class="pr-subtitle">What aids in the management of panic attacks? </div>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Keep your attention on your breathing. Counting to five while gently inhaling and exhaling might be beneficial.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Place a stamp on the place. Some people feel that this helps with breathing control.
                                        </li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Concentrate on your senses. Take a bite of mint-flavoured candy or chewing gum, or touch or snuggle something soft.
                                        </li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Try some grounding exercises. Techniques for grounding might help you feel more in control. They're especially helpful if you get panic episodes and feel dissociated.
                                        </li>
                                    </ul>
                                    <div class="pr-subtitle">After a panic attack: </div>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Consider self-care. Following a panic attack, it's critical to pay attention to what your body requires. You could, for example, need to find a quiet place to rest, eat, or drink something. </li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Tell someone you can rely on. If you're able, telling someone you've experienced a panic attack may be beneficial. Mentioning how they could notice if you have another one and how you'd like them to assist you could be very useful. </li>
                                    </ul>
                                    <div class="pr-subtitle">What is panic disorder? </div>
                                    <p class="pt-serif">If you have a number of panic episodes at random times with no obvious trigger or explanation, you might be diagnosed with panic disorder. It's not uncommon to have panic disorder and phobias at the same time.
                                    </p>
                                    <p class="pt-serif">People with panic disorder may go through phases where they have few or no panic attacks but then have a lot of them at other times. Panic episodes occur frequently and unexpectedly in people with panic disorder. Even when there is no obvious risk or cause, these episodes are characterised by a rapid surge of panic or discomfort, as well as a sensation of losing control. </p>
                                    <p class="pt-serif">Panic disorder does not affect everyone who has a manic episode. Panic disorder can run in families, but no one understands why some members of the family suffer from it while others do not. Researchers have discovered that fear and anxiety are influenced by numerous regions of the brain as well as biochemical processes.</p>
                                    <p class="pt-serif">According to some studies, panic episodes are similar to "false alarms," in which our bodies' natural survival instincts are activated too frequently, too powerfully, or a mix of the two. Someone suffering from panic disorder, for example, can feel their heart thumping and believe they're having a heart attack. This can set off a vicious cycle, causing panic episodes to appear out of nowhere, which is a key aspect of panic disorder. </p>
                                    <div class="pr-subtitle">Panic Disorder Treatment: </div>
                                    <p class="pt-serif">If you think you could be suffering from panic disorder, see a doctor. A health care practitioner may do a physical exam after reviewing your medical history to check that your symptoms are not caused by an unrelated physical condition.</p>
                                    <p class="pt-serif">A mental health specialist, such as a psychiatrist, psychologist, or clinical social worker, may be referred to you by your health care practitioner. A diagnosis, generally from a mental health expert, is the first step towards effective therapy.</p>
                                    <p class="pt-serif"><a href="https://www.abhasa.in/panic-disorder-treatment-center/">Panic disorder</a> is usually treated with cognitive-behavioural therapy (CBT), a research-based kind of psychotherapy. CBT teaches you how to think, act, and behave differently to the sensations that arise during or before a panic attack.</p>
                                    <p class="pt-serif">Once you learn to react differently to the bodily sensations of worry and dread that occur during a panic attack, the episodes will become less common. Exposure therapy is a typical CBT technique that focuses on facing panic disorder-related anxieties and beliefs in order to enable you to engage in previously avoided activities. Relaxation activities are occasionally used in conjunction with exposure treatment. </p>
