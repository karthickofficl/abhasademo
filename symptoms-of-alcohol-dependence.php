                                    <div class="pr-subtitle">Causes of alcohol dependence </div>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Isolation</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Depression</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Anxiety </li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Loneliness to some extent </li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>To show off</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>as a societal trend</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Peer pressure </li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>To ward off frustration levels</li>
                                    </ul>
                                    <p class="pt-serif">In all such cases, alcohol comes to the rescue. It is one of the most demanded non-essential commodities. </p>
                                    <div class="pr-subtitle">How alcohol consumption does impacts immunity? </div>
                                    <p class="pt-serif">Your friend might have told you that alcohol does not impact immunity. Rather, it boosts it or can help battle certain diseases. Let's find out if it's true or false. </p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Overconsumption of alcohol weakens your first line of defense, which are white blood cells that help you fight in case any virus or bacteria enter your body. </li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>It slows down your immune response to foreign viruses or bacteria, making you vulnerable to diseases. </li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>It kills good bacteria inside your body and damages the gastrointestinal tract, causing bacteria to leak into the bloodstream. </li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Over-alcohol dependence can make you prone to certain respiratory disorders like pneumonia, pulmonary diseases, TB, and other respiratory disorders. Many people think that smoking makes them susceptible to respiratory disorders; however, this is not true. Alcohol is lethal, too. </li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Regular drinking habits can lower down your immunity cell numbers. </li>
                                    </ul>
                                    <div class="pr-subtitle">Signs and symptoms of alcohol dependence</div>
                                    <p class="pt-serif">The symptoms of alcohol abuse are many, and they will negatively impact different facets of life. The symptoms include physical, psychological, social, etc. Let’s start discussing the same. </p>
                                    <div class="pr-subtitle"> Signs of alcohol dependence</div>
                                    <p class="pt-serif">Many symptoms point towards your addiction</p>
                                    <ul class="ul">
                                        <li class="pt-serif">Drinking a large amount of alcohol</li>
                                        <li class="pt-serif">Spending time, energy, and money on buying drinks</li>
                                        <li class="pt-serif">Most of the time, you appear hungover and are recovering from alcohol effects </li>
                                        <li class="pt-serif">Signs of intoxication include poor hand movement coordination, speech, and walking trouble.</li>
                                        <li class="pt-serif">Decreased self-care and falling.</li>
                                        <li class="pt-serif">Poor oral hygiene.</li>
                                        <li class="pt-serif">Falling prey to risky behavior.</li>
                                        <li class="pt-serif">The Smell of alcohol around the home.</li>
                                        <li class="pt-serif">Significant gain in the weight.</li>
                                    </ul>
                                    <div class="pr-subtitle">Other physical signs of the abuse include </div>
                                    <ul class="ul">
                                        <li class="pt-serif">Cardiac problems, high blood pressure, stroke, irregular heartbeat </li>
                                        <li class="pt-serif">Liver concerns </li>
                                        <li class="pt-serif">Damage to the pancreas </li>
                                        <li class="pt-serif">Cancers.</li>
                                    </ul>
                                    <div class="pr-subtitle">Psychological symptoms of alcohol consumption </div>
                                    <p class="pt-serif">The psychological signs arise when alcohol interferes with brain functioning. The visible signs that you will notice are </p>
                                    <ul class="ul">
                                        <li class="pt-serif">Frequent mood changes</li>
                                        <li class="pt-serif">Increased irritability, anger, depression</li>
                                        <li class="pt-serif">Not able to carry out daily tasks and responsibilities </li>
                                        <li class="pt-serif">Problem with concentration.</li>
                                        <li class="pt-serif">Anxiety disorders.</li>
                                        <li class="pt-serif">Hallucination or delusional thinking.</li>
                                        <li class="pt-serif">Withdrawal symptoms.</li>
                                    </ul>
                                    <div class="pr-subtitle">Social symptoms of alcohol addiction </div>
                                    <p class="pt-serif"><a href="https://www.abhasa.in/alcohol-rehabilitation-centre-in-mumbai/">Alcohol addiction </a>extends beyond physical, immune, and psychological signs and includes social signs. The most visible signs are </p>
                                    <ul class="ul">
                                        <li class="pt-serif">Social withdrawal</li>
                                        <li class="pt-serif">Isolating yourself from friends</li>
                                        <li class="pt-serif">Spending time on the internet</li>
                                        <li class="pt-serif">Change in behavior or pattern </li>
                                        <li class="pt-serif">Failing to do the required tasks.</li>
                                        <li class="pt-serif">Conflicts between family members and co-workers.</li>
                                        <li class="pt-serif">Decreased output at work.</li>
                                        <li class="pt-serif">No longer able to carry out nicely in sports activities.</li>
                                    </ul>
                                    <p class="pt-serif"><b>Cognitive behavior therapy:</b> In this therapy, the counsellor will identify the feelings or cues that are stopping you from overcoming the habit and create a roadmap on how to end it. The goal is to change your thought process to positively respond to situations that trigger alcohol consumption. </p>
                                    <p class="pt-serif"><b>Motivation alcoholism therapies:</b> These therapies are conducted to strengthen your behavior. The therapy focuses on creating a plan, developing confidence, and creating an urge to stick to the plan. </p>
                                    <p class="pt-serif">During the treatment, brief interventions are made to analyse the individual's drinking patterns and risk-taking behaviour and take steps to overcome them. Even personal feedback is given to family members to make a change. </p>
