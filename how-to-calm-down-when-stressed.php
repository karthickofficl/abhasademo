                                        <p class="pt-serif">Do you often struggle with restlessness, sweating, trembling, tiredness, feeling weak, rapid breathing, and preoccupation with your worries? Well, we have some good news for you. You can manage all these by following these easy steps:</p>
                                        


                                        <h3 class="pr-subtitle pt-serif-h3">Quick Ways to Calm Down When You Feel Overwhelmed</h3>
                                       

                                      <ul class="ul">
                            <li class="pt-serif"><i class="fa fa-circle"></i>Sit wherever you are. Take a deep breath. slowly… Breath in… Breath out…</li>
                            <li class="pt-serif"><i class="fa fa-circle"></i>Start doing these and notice amazing results.</li>
                            <li class="pt-serif"><i class="fa fa-circle"></i>Meditate…… Sit in a calm room. Hold a comfortable posture. Close your eyes and concentrate in between your eyebrows. Regular meditation has proven to be useful in controlling anxiety.</li>
                            <li class="pt-serif"><i class="fa fa-circle"></i>Practicing relaxation techniques like  <a href="https://www.betterhealth.vic.gov.au/health/healthyliving/breathing-to-reduce-stress" style="text-decoration: underline;">abdominal breathing</a>, progressive muscle relaxation, and isometric relaxation exercises will be a huge relief for you.</li>
                            <li class="pt-serif"><i class="fa fa-circle"></i>Practicing regular Mindfulness is a sure-shot way of becoming self-aware and disconnect from anxiety-provoking thoughts.</li>
                            <li class="pt-serif"><i class="fa fa-circle"></i>Be sure to do some regular physical exercise, as it acts as a wonder in controlling your anxiety</li>
                            <li class="pt-serif"><i class="fa fa-circle"></i>Work on your self-esteem by taking assertiveness training</li>
                            <li class="pt-serif"><i class="fa fa-circle"></i>Approach your problems step-by-step rather than seeing them as a whole and worrying about them.</li>
                            <li class="pt-serif"><i class="fa fa-circle"></i>Follow the proper diet, consuming healthy food in comparison to unhealthy, oily, or junk food. Make sure to drink a minimum of 3 litres of water a day.
</li>
                            <li class="pt-serif"><i class="fa fa-circle"></i>Have approximately 8 hours of sleep.</li>
                            <li class="pt-serif"><i class="fa fa-circle"></i>Consulting your therapist. Cognitive-behaviour therapy is proven to be effective for anxiety.</li>
                            <li class="pt-serif"><i class="fa fa-circle"></i>Consult a psychiatrist and take regular and timely medication.</li>
                            <p class="pt-serif">So yeah…. following these steps would definitely work for you provided you are consistent at it… Good luck!</p>
