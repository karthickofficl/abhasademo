                                        <p class="pt-serif">People take drugs and use alcohol for a variety of reasons. Using drugs or alcohol for whatever purpose can have a long-term hurtful effect on your health. The following are some of the probable long-term consequences.</p>
                                            <ul class="ul">
                                                <li class="pt-serif"><i class="fa fa-circle"></i>To obtain the same impact, you'll need to take more.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Strokes and high blood pressure.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>You're having issues with your liver and pancreas.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Certain malignancies, such as liver cancer, bowel cancer, and mouth cancer, develop.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Having trouble getting or keeping an erection.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Orgasms are a problem.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Having trouble getting pregnant.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Feeling compelled to consume the drug or drink. This is referred to as being reliant.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>When you don't take them, you may have withdrawal symptoms like as nausea, cold, sweating, or shakiness.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i> Mood swings can occur unexpectedly.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i> Having a pessimistic attitude about life.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i> Motivation is waning.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i> Depression.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i> Anxiety.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i> Relationship difficulties.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i> Being evasive.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i> Experiencing drug-induced psychosis.</li>
                                            </ul>

                                        <p class="pt-serif">Long-term usage of alcohol or drugs can have main consequences for your mental health. Drugs can make you feel worse and make you more likely to hurt yourself or commit suicide. There's also some evidence that consuming certain substances for the first time might lead to mental disease. Cannabis, for example, has been demonstrated in studies to raise the risk of developing psychosis or a psychotic condition.</p>
                                        <div class="pr-subtitle">What exactly is psychosis?</div>
                                        <p class="pt-serif">Psychosis is a medical word for mental illness. Psychosis causes you to process the world around you in a different way than other individuals. This can include how you feel, think, and see things. You may be able to see or hear things that others are unable to. Or you may believe things that others do not. It's been described as a "break from reality" by others. Psychosis is described by a variety of terminologies. "Psychotic symptoms," "psychotic episode," or "psychotic experience" are examples. It might be a sign of mental illness or a side effect of some medications. </p>
                                        <div class="pr-subtitle">Which chemicals have the potential to harm my mental health?</div>
                                        <p class="pt-serif">We've included some of the numerous sorts of drugs that might affect your mental health in this area. Please keep in mind that this is not an exhaustive list of all drugs. Taking any drug has the potential to be hazardous. They may also interact negatively with any drugs or other substances you may be taking.</p>
                                        <div class="pr-subtitle">Cannabis:</div>
                                        <p class="pt-serif">Some individuals use cannabis because it relaxes or makes them joyful, but it may also make them uncomfortable or paranoid. Some people may have paranormal experiences. This is a symptom of psychosis brought on by drugs. According to certain research, your chances of developing psychosis are increased if you: Use cannabis for a long time, on a regular basis, and with 'high-strength' cannabis such as skunk.</p>
                                        <div class="pr-subtitle">Alcohol:</div>
                                        <p class="pt-serif"><a href="https://www.abhasa.in/alcohol-rehabilitation-centre-in-mumbai/"><b>Alcohol abuse</b> </a> is a concern for some persons with mental illnesses. Because alcohol is legal, it is easy to obtain. It can exacerbate the symptoms of several mental health conditions. The long-term consequences of alcohol are also influenced by how much and how often you consume it. If you consume too much alcohol on a regular basis, you risk significant bodily and emotional harm. Drinking can cause you to do things you would never do otherwise. Self-harm and suicide are examples of this. Alcohol use at extremely high levels can lead to psychosis.</p>
                                        <div class="pr-subtitle">New Psychoactive Substances (NPS):</div>
                                        <p class="pt-serif">These are pharmaceuticals that contain one or more chemical ingredients. They have comparable effects to cocaine, marijuana, and ecstasy. Some new psychoactive substances have the potential to produce disorientation and fear. Hallucinations are another possibility. When you see, smell, hear or feel something that other people don't, you're experiencing clairvoyance. Hallucinations might have an impact on your behavior.</p>
                                        <p class="pt-serif">Your behaviour may become unpredictable, putting your personal safety in jeopardy. These substances can also impair your judgment, putting you in danger. Some NPS are quite harmful. They have the ability to kill or severely injure you. When combined with alcohol or other psychotropic substances, this risk increases. </p>

                                        <div class="pr-subtitle">Amphetamine and methamphetamine</div>
                                        <p class="pt-serif">These medicines might make you feel fully awake and alert in the short term. It may be difficult for you to relax or sleep as a result of this. You might get a drug-induced psychosis as a result of them. Amphetamines might make you worried and unhappy in the long run. They have the potential to be addicting. You may feel gloomy and have trouble sleeping if you stop taking the medicine. </p>
                                        <div class="pr-subtitle">Benzodiazepines </div>
                                        <p class="pt-serif">Tranquilizers include benzodiazepines. Anxiety is treated with them. They're also utilized to relax muscles. Benzodiazepines are sometimes prescribed by doctors to aid alleviate anxiety. However, due of its calming properties, some people purchase them illegally. Because they can be addicting, physicians only prescribe them for a limited period. These medicines might make you feel calmer in the short term. They may make you feel disoriented or extremely tired, depending on the sort you take. It's risky to combine benzodiazepines with other medicines or alcohol. It has the potential to impact your respiration. It can also put you at danger of overdosing and dying. Some people get hooked in the long run. This might have a significant impact on their daily lives.</p>
                                        <div class="pr-subtitle">Cocaine</div>
                                        <p class="pt-serif">Cocaine might make you feel alert, chatty, and confident in the short term. After the effects of this wear off, you may feel fatigued and melancholy. Cocaine usage can have a long-term effect on how you feel. It might have an impact on your friendships and family ties.</p>
                                        <p class="pt-serif"><a href="https://www.abhasa.in/cocaine-drug-addiction-treatment/"><b>Cocaine</b> </a> is also addictive, and you're more likely to develop sadness, paranoia, or anxiety over time if you use it. Cocaine has been linked to convulsions, heart attacks, and strokes. You are more likely to overdose or die if you combine it with other medicines. </p>
                                        <div class="pr-subtitle">Ecstasy</div>
                                        <p class="pt-serif">Ecstasy can make you feel energized, joyful, conversational, and confident in the short term. It can also make you feel uneasy, perplexed, or even cause drug-induced psychosis. Ecstasy usage might cause memory issues in the long run. Depression and anxiety are also possible side effects</p>
                                        <div class="pr-subtitle">Heroin</div>
                                        <p class="pt-serif">Heroin might make you feel calm and cheerful in the short term. It relieves pain and may make you tired. However, compared to other narcotics, there is a greater danger of taking too much or overdosing with heroin.</p>
                                        <p class="pt-serif"><a href="https://www.abhasa.in/cocaine-drug-addiction-treatment/"><b>Heroin</b> </a> may be consumed in a variety of ways, including through injection. If you inject heroin, however, you run a greater chance of contracting an infection, especially if you share needles. Heroin is a highly addictive substance. It has the potential to have catastrophic long-term consequences. You may begin to believe that heroin is more essential than other aspects of your life. This might make it more difficult to hold a job and have an impact on your relationships. </p>
                                        <div class="pr-subtitle">LSD</div>
                                        <p class="pt-serif">LSD might make you experience things that aren't really in the short term. The experience will be delightful at times and terrifying at other times. This is referred to as a terrible trip. If you have a history of mental health issues, LSD can exacerbate them. It might be frightening if you panic when on LSD. LSD may also cause mental health issues that you have never had before.</p>
                                        <div class="pr-subtitle">Importance of rehabilitation centre:</div>
                                        <p class="pt-serif">Treatment for substance misuse and drug addiction can help people recover from addictions, injuries, and even physical or mental diseases. Substance addiction treatment facilities, on the other hand, are frequently what comes to mind when the word "rehab" is used. Addiction treatment clinics give additional attention and help to those who are addicted to drugs or alcohol. <a href="https://www.abhasa.in/about-maharastra/"><b>Drug and alcohol rehab treatment centres</b> </a> assist individuals in making good life changes by correcting maladaptive habits. Patients are taught healthy coping skills, impulse control, emotional regulation abilities, and drug-refusal tactics, all of which can help patients prevent relapse in the long run.</p>
                                        <p class="pt-serif"><a href="https://www.abhasa.in/cocaine-drug-addiction-treatment/"><b>Drug addiction treatment</b> </a> programs assist people in overcoming their addictions. Addiction treatment programs arise in a variety of shapes and sizes. Some focus on assisting patients with single substance addiction, while others provide a more comprehensive spectrum of drug and alcohol addiction treatment options. Some rehab facilities, such as an adolescent or women-only treatment facility, are gender- or age-specific, which can help patients feel more at ease in the therapeutic setting. There are both inpatient and outpatient therapy programs accessible. </p>
                                        <p class="pt-serif"> Individual counseling will assist you in learning to recognize and cope with triggers. Therapists may also be able to assist you in improving your emotional regulation skills in order to help you prevent a recurrence. Group counseling allows you to put your sober social skills and the coping methods you gained in individual treatment into practice. Sessions with a family therapist may aid in the mending of damaged relationships, the improvement of communication skills, and the development of conflict resolution skills.</p>
