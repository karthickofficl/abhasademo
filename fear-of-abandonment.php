                                        <p class="pt-serif">One day, when you are going through and living your routine life, without warning, your world turns dark and upside-down. Then there comes a day when you suddenly feel a need for protection from those you trusted yesterday. You feel a sense of anger, hurt, and rejection in relationships that made you happy previously. Its aftershocks also remain inside us if not dealt with. It works like quicksand, leaving us with  <a href="../panic-disorder-treatment-center"><b>feelings of worthlessness and despair.</b></a> No matter how hurtful or demoralising the circumstances may have been, you are not undeserving of love. You matter, your heart matters, and the love you bestow upon others matters. You definitely have a lot to give. Always remember that you will receive a lot of love. This is why we need to deal with this fear, because we should be able to open our arms to someone who genuinely wants to take care of us. The fear of abandonment restricts us and moves us away from even genuine people.  </p>
                                        <h3 class="pr-subtitle pt-serif-h3">‘The Wounded Child’</h3>
                                        <p class="pt-serif">A wounded child is someone who has been left behind by a loved one, especially in childhood when a parent abandons the child or dies. </p>
                                        <h3 class="pr-subtitle pt-serif-h3">‘The Wounded Adult’</h3>
                                        <p class="pt-serif">This is the person who feels wounded when his or her spouse or partner unexpectedly walks away. </p>
                                        <p class="pt-serif">We acknowledge your fear of abandonment and the wound left by it as important. It is something that needs to be addressed because it is paralysing, and we can address it for you because we don’t want it to trigger you later in your life</p>
                                        <p class="pt-serif">The fear of abandonment is a serious issue, as it leaves the person fearing that a loved one will leave them or abandon them. It is paralyzing. </p>
                                        <p class="pt-serif">It can be caused by the following, which has a greater impact in the long term: </p>
                                        <ul class="ul">
                                            <li class="pt-serif"><i class="fa fa-circle"></i>Traumatic events</li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>Death of a loved one, a friend moving away, a relationship ending, a transition from high school to college to marriage and parenthood. </li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>Losing someone to violence or tragedy</li>
                                        </ul>
                                        <p class="pt-serif">The people who are or may be vulnerable to the fear of abandonment have the following symptoms: </p>
                                        <ul class="ul">
                                            <li class="pt-serif"><i class="fa fa-circle"></i>They Unnecessarily Attach quickly </li>
