                        <div class="clearfix"></div>
                        <p class="pt-serif">Neurofeedback is a non-drug option for treatment of neuropsychiatric conditions. It is a type of brain training which is done to improve brain’s functioning by increasing or decreasing specific brainwaves. This can be compared to a Batsman doing net practice before going for the big match.</p>
                        <h3 class="pr-subtitle pt-serif-h3">Neurofeedback: History and Applications</h3>
                        <p class="pt-serif">The technology behind Neurofeedback is not new- it has been in use for more than 30 years, but was mostly confined to developed countries such as the US and Europe. Its earliest use was in treatment of Attention Deficit Hyperactivity Disorder (ADHDH), a condition which affects nearly 5-7 % school age children and nearly 2-5 percent adults. ADHD has major impact towards individuals and societies resulting in majority of academic problems, school dropouts, teenage drug use and criminal activities. Medications are effective in ADHD, but people stop taking them due to a range of side effects (such as sleep issues, loss of appetite, heart problems and emergence of tics). Many studies have shown that Neurofeedback has similar effectiveness (to medications) without accompanying side effects and longer duration of response. This has made neurofeedback a very attractive non-drug option in ADHD. 

                        <br>In the last decade Neurofeedback has seen a worldwide presence and has also seen expanded use in other conditions such as Treatment resistant Depression, Anxiety, Psychosis and Post traumatic disorders etc., where there was lack of response with standard treatments or where the individuals were experiencing too much medication side effects.  

                        <br>Another major application is Addiction disorders where Neurofeedback has been shown to improve sleep and reduce agitation, craving, and relapse rates in alcohol and other substance use disorders. Peniston et al in a series of landmark studies showed significantly lower rates of relapse in individuals with Alcohol Dependence compared with usual treatments when they received Neurofeedback in addition to usual de-addition treatment. These individuals, who generally relapsed within 3 months of discharge, remained sober for months to years after receiving Neurofeedback. Thus, addition of Neurofeedback to standard treatment resulted in improved outcome in this population. </p>

