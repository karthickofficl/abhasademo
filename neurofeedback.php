                        <p class="pt-serif mt-5" >The overall the goal of neurofeedback is to improve brain functioning by training the brain using technology. The system that we use in Abhasa is simple, painless, non-invasive and state-of-the art.  It is a new generation system which does not require the cumbersome equipment of the past and utilizes the latest technology available anywhere in the world.  

                        <br>Neurofeedback training is quite individualized and not the same for everyone. Specific Neurofeedback protocols are selected based on the Clinical Interview and results from Brain Mapping. Brian Mapping is conducted prior to starting a course of Neurofeedback; it is different from standard neuroimaging techniques (such as CT or MRI) is done using the equipment as Neurofeedback and does not require any extra expenditure. 

                        <br>A course of Neurofeedback may require 20-40 sessions depending on the severity of condition and comorbid factors. For example, someone who is high-functioning and comes in with only alcohol dependence might require less whereas another individual who is otherwise poor functioning with multiple substance use disorder and/or comorbid psychiatric conditions will require more sessions. Sessions are typically done 3 times/week but can be done more frequently with a maximum of once daily. In deaddiction setting, ideally Neurofeedback can be started within a week once the acute risk of withdrawal seizures has been managed. Similarly, for psychiatric conditions, neurofeedback should be considered early as delaying would mean denying these individuals will require more sessions during their admission.</p>

                        <div class="pr-subtitle">Neurofeedback with Medications and/or Psychotherapy</div>
                        <p class="pt-serif">It is important to understand that for complex psychiatric conditions, Neurofeedback is not a stand-alone treatment and requires standard medications; however, it has been consistently showed that addition of Neurofeedback leads to better response and on lower doses. This happens because Neurofeedback improve self-regulation of brain leading to lower requirement of psychotropic medication. In rare cases individual may be able to stop all their medications, but in general clients will be required to continue medications in lower doses- especially if they have a relapsing mental health condition.  

                        <br>Similarly, Neurofeedback has been shown to improve the response rates of Psychological treatments such as Cognitive Behavioural Therapy (CBT). This is best exemplified in Post-Traumatic Stress disorder (PTSD) where many individuals fail to respond to CBT as they are afraid of being “re-traumatized”. But many such clients after starting Neurofeedback become more organized, their “fight or flight” is blunted, and they spontaneously start disclosing traumatic material. At this point they can safely and successfully engage in CBT leading to better long-term outcomes.</p>
