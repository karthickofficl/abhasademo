                                <div class="fl-wrap text-block">
                                    <div class="clearfix"></div>
                                    <p class="pt-serif">COVID-19 has hit humanity badly, especially in terms of jobs. This has led to a lot of depression and <a href="../panic-disorder-treatment-center"><b>generalized anxiety among people</a></b> and the mental health of those already suffering has also gotten worse. </p>

                                    <p class="pt-serif">Job gives a sense of security, but we are surrounded by insecurities. What should we do about it? We can’t let it escalate. We can’t suppress it; we can’t sit idle. Then what can we do about it? We need to detach ourselves from the idea of counting our self-worth through a job. Just imagine a place where you have no luxuries or amenities and are given two options: to die or to survive. What will you choose? We have the survival instinct because we are human beings; we don’t want to die, even though we may have suicidal ideations or hopelessness. </p>




                                    <h3 class="pr-subtitle pt-serif-h3">Read More</h3>
                                    <p class="pt-serif">The following measures are suggested to deal with this kind of anxiety because even upgrading your skills or developing new skills during this period can be stressful: </p>
                                    <ol class="ol">
                                        <li class="pt-serif">Sit down and relax—try to ground yourself in this very moment. You haven’t lost anything. It is just a matter of time. It is not your fault. Treat yourself with warmth and kindness at this time. You are not going to get benefits from feeling anxious and troubled. </li>
                                        <li class="pt-serif">Meditate on the good things in life. Think about the previous achievements you have made in the past, wherein you were so patient and calm. Past victories are a reminder of how far we have come. </li>
                                        <li class="pt-serif">Normalise the problem. Understand that unemployment is part and parcel of your life. Don’t keep holding on to the problem; otherwise, it will keep staying with you and do more harm than good. </li>
                                        <li class="pt-serif">Bring the focus to yourself. Try to rejuvenate your hobbies. </li>
                                        <li class="pt-serif">Develop a daily plan of action. </li>
                                        <li class="pt-serif">Volunteer: Work to make yourself happy in this phase. E.g., volunteer for children, the underprivileged, etc. Spend time with these people, and you will find so much peace. </li>
                                        <li class="pt-serif">Don’t stress too much about looking for new jobs. </li>
                                        <li class="pt-serif">In case you don’t get responses from new recruiters, do not feel bad. Self-criticism is going to make the problem worse. </li>
                                        <li class="pt-serif">Join a community. Don't let yourself stay isolated. Join online or offline communities like professional organisations, political interest groups, environmental and spiritual groups, etc. Such communities broaden our horizons and give us a greater meaning of life. </li>
                                        <li class="pt-serif">When you do the above activities, you will realise that you are much more than just a job. You are alive. You are a survivor. You are a person who can give hope to others by coming out of this problem.</li>
                                    </ol>





