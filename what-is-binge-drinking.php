                                        <p class="pt-serif">Studies characterize binge drinkers as individuals who drink too much alcohol at once. Breaking thresholds of <10 and <15 drinks is considered.</p>
                                        <p class="pt-serif">If we have to understand this in detail then, for girls 9 to 17 years of age, the cut-off suggested is <3 drinks. For reference check with the <a href="../alcohol-rehabilitation-centre-in-tamilnadu"><b>best psychiatric & De-addiction specialist in South India.</b> </a> For boys 9 to 13 years of age, the cut-off suggested is <3 drinks; for boys 14 to 15 years of age, the cut-off suggested is <4 drinks; and for boys 16 to 17 years of age, the cut-off suggested is <5 drinks.</p>
                                    



                                <ol class="ol">
                                           

                                            <li class="pt-serif">Stage 1: Experimentation
                                            The first stage of alcoholism is a general experimentation with alcohol. Some drinkers, especially, see drinking as a status symbol, or they do it out of peer pressure. As on continues done by a de-addiction specialist on the recovered addict. The people coming under this stage may justify their actions by saying that they drink occasionally. While this may be the automatic defence mechanism they use and they may not be drinking regularly, they consume exceptionally large amounts of alcohol at one time.</li>

                                            <li class="pt-serif">Stage 2: Finding Excuses to Drink Alcohol
                                                Here, drinking becomes a regular phenomenon. The following excuses are quite common in this. For instance, character seen during treatment:
                                             <ul class="ul">
                                               <li class="pt-serif"><i class="fa fa-circle"></i>as an excuse to get together with friends,</li>
                                             <li class="pt-serif"><i class="fa fa-circle"></i>to alleviate stress,</li>
                                             <li class="pt-serif"><i class="fa fa-circle"></i>out of boredom,</li>
                                             <li class="pt-serif"><i class="fa fa-circle"></i>to combat sadness or loneliness.</li>
                                         </ul>
                                             </li>

                                            <li class="pt-serif">Step 3: When Drinking overtakes your Life
                                            When people start seeing alcohol as their only companion or as an escapist route from the actual problems in their daily life, this is when your life gets hijacked by alcohol. A common behaviour pattern and made note during rehab treatment. The drinker avoids the following stressful situations by drinking alcohol:
                                            <ul class="ul">
                                               <li class="pt-serif"><i class="fa fa-circle"></i>Relationship issues– be it the relation with parents, wives, husbands, kids, etc. Drinking has already reduced your ability to argue rationally and you become more impulsive. And there is more need for comfort and luxury for individuals to listen.</li>
                                             <li class="pt-serif"><i class="fa fa-circle"></i>Decreased social activity because of erratic behaviour- similar to the above point, the mood fluctuates so much that you’re either too needy of people or you avoid people altogether. An understanding of what they are going through as professionals.  This is a sign of the illnesses that are coming along with alcoholism. In the next stage, we will see how binge drinking has now become alcohol abuse.</li>
                                             <li class="pt-serif"><i class="fa fa-circle"></i>Sudden change in friends– you tend to interact with those who are in the same shoes as you. In search of people who could care and give comfort or better care. You will love their company and detest the company of those who want to try to bring you out of this situation.</li>
                                             <li class="pt-serif"><i class="fa fa-circle"></i>Difficulty conversing with strangers– your social skills and professional skills go for a toss when you become so dependent on alcohol because it affecting your response system of the brain. The stage in need of better service for your comeback.</li>
                                         </ul>

                                        </li>

                                             <li class="pt-serif">Stage 4: Withdrawal Symptoms
                                             If you are not able to live without alcohol, you will experience withdrawals. In words of best therapists, when you don’t get access to alcohol or you are trying to quit drinking, you will experience the following:

                                          <ul class="ul">
                                               <li class="pt-serif"><i class="fa fa-circle"></i>nausea that is unrelated to a hangover</li>
                                               <li class="pt-serif"><i class="fa fa-circle"></i>body tremors</li>
                                               <li class="pt-serif"><i class="fa fa-circle"></i>sweating</li>
                                               <li class="pt-serif"><i class="fa fa-circle"></i>severe irritability</li>
                                               <li class="pt-serif"><i class="fa fa-circle"></i>a racing heart</li>
                                               <li class="pt-serif"><i class="fa fa-circle"></i>trouble sleeping</li>
                                         </ul></li>

                                          
                                        </ol>
