                                        <div class="clearfix"></div>
                                        <p class="pt-serif">Addiction is described as a loss of control over doing, taking, or using anything to the point that it may be damaging to one's health. Addiction is most typically connected with gambling, alcohol, drugs, and smoking, although anybody may get hooked on almost anything, including:</p>

                                        <p class="pt-serif"><b>Work:</b> Some people are so consumed by their profession that they become physically fatigued. If your relationships, family, and social life suffer as a result of your work and you never take vacations, you may be addicted to work.<br>

                                        <b>Internet:</b> People may spend hours each day and night surfing the internet or gaming while disregarding other elements of their lives as computer and mobile phone use have expanded. <br>

                                        <b>Solvents:</b> Volatile substance misuse occurs when you inhale chemicals such as glue, aerosols, gasoline, or lighter fuel to achieve a state of intoxication.  <br>

                                        <b>Shopping:</b> When you buy goods you don't need or want to get a rush on, you develop an addiction; this is immediately followed by emotions of guilt, humiliation, or despair. 
                                        </p>
                                        <div class="pr-subtitle">What Causes Addictions?</div>
                                        <p class="pt-serif">When you buy goods you don't need or want to get a rush on, you develop an addiction; this is immediately followed by emotions of guilt, humiliation, or despair.<br>
                                        This might become a habit that is difficult to break. When you are addicted to anything, not having it creates withdrawal symptoms, sometimes known as "coming down." Because this can be uncomfortable, it is simpler to continue having or doing what you desire, and thus the cycle continues. Addictions can spiral out of control because you require more and more to fulfil a craving and obtain the "high." </p>
                                        <div class="pr-subtitle">How Mobile Phone Addictions Can Affect You</div>
                                        <p class="pt-serif">The stress of dealing with an addiction can harm your job, life, and relationships. Addiction can have major psychological and physical consequences in the case of substance abuse (for example, drugs and alcohol). Some studies show that a person's chance of getting addicted is partially hereditary, but environmental variables, such as being around other dependent individuals, are likely to raise the risk as well. Substance abuse, for example, might be used to avoid confronting tough topics. Addiction can be triggered by unemployment and poverty, as well as stress and emotional or professional strain. <br>

                                        Mobile phones have become an indispensable item for the majority of individuals. Your smartphone serves as a navigator, personal assistant, and source of entertainment in the modern world. Tablet computers and other linked gadgets have also become commonplace in many households. Many smartphone users can control how much time they spend gazing at their screens. <br>

                                        They may still participate in social events and pay attention to their environment without feeling compelled to check their phone. Some users, however, have developed a dependence on their mobile phones. They find it difficult to communicate with the world around them and spend more time on their mobile phones than conversing with friends at social occasions.  <br>

                                        With mobile phones and other gadgets at the centre of our universe, it's critical to understand how to prevent and diagnose addiction so you can be healthy and happy in both your real and digital lives. Every mobile phone user is in danger of acquiring a phone addiction. There are several ways that smartphone applications grab users and make it difficult to look away, whether it's through online gaming, social networking, text messaging, or emails. Teens are more likely than any other age group to get addicted to mobile phones.  <br>

                                        Adolescents under the age of 20 are the most vulnerable to mobile phone addiction, according to research published in Frontiers in Psychiatry, since they are more likely to develop behavioural difficulties. Teens can't always manage screen time properly, and they're also the group recognised for spending the most time on their phones, owing to a lack of self-control. Teen media management may be nonexistent, and the same survey discovered that almost 27 percent of smartphone owners between the ages of 11 and 14 never turn their phones off, even to sleep. <br>

                                        Other risk factors for mobile phone addiction, in addition to teens, include smartphone users who: </p>
                                        <div class="pr-subtitle">Suffer from anxiety.</div>
                                            <ul class="ul">
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Live with <a href="https://www.abhasa.in/depression-rehabilitation-center/">depression.</a></li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>low-self-esteem.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>introverted in social situations.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Don’t have disciplined self-control.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Haven’t yet mastered impulse control.</li>
                                            </ul>
                                        <p class="pt-serif">You miss out on meaningful time with friends and family, as well as thrilling activities, while you are hooked on your phone. Other detrimental consequences of excessive mobile phone use include the following: </p>
                                        <div class="pr-subtitle">Mental health issues:</div>
                                        <p class="pt-serif">Excessive mobile phone use has been linked to anxiety and sadness in studies. Adolescents who were addicted to their mobile phones were more likely to suffer from chronic stress and a lack of mental stability.</p>
                                        <div class="pr-subtitle">Poor sleep:</div>
                                        <p class="pt-serif">Scrolling on your phone immediately before bed might lead to disturbed sleep. Researchers at Ohio State University analysed college students who used their phones for an average of 46.6 minutes each night and discovered a link between mobile phone usage and sleep quality. This use resulted in insomnia and may have been related to shorter sleeping hours, an inability to fall asleep, and poor sleep quality.</p>
                                        <div class="pr-subtitle">Increased risk of car accidents:  </div>
                                        <p class="pt-serif">Using a smartphone while driving is risky because it takes your attention away from the road. Mobile phone addicts are more likely to use their gadgets while driving, increasing their chances of getting involved in a vehicle accident.</p>
                                        <div class="pr-subtitle">Poor school performance:</div>
                                        <p class="pt-serif">If you're unsure if your child is mature enough for a smartphone, examine whether they can obey their school's mobile phone policy. If your children use their iPhones in class, they will struggle to concentrate. This lack of class participation may result in issues at school, such as bad grades or disciplinary action.</p>
                                        <div class="pr-subtitle">Poor work performance:</div>
                                        <p class="pt-serif">While at work, the majority of phone addicts maintain eye contact with their smartphones. Being distracted by your phone at work may have serious implications, including poor performance or even termination.</p>
                                        <div class="pr-subtitle">Suggestions For Avoiding Mobile Phone Addiction</div>
                                        <p class="pt-serif">Setting appropriate boundaries between yourself and your mobile phone increases your chances of avoiding smartphone addiction. Set mobile phone usage limitations for young children to encourage your family to follow these boundaries as well. Make guidelines for your family, such as "no phones at the dinner table."  <br>

                                        Unplugging from your phone for a few hours each day, such as while you're exercising or eating with your family, allows you to reconnect with the world around you. To avoid mobile phone addiction, you also need: Keep track of your data consumption and set limitations for yourself and your family.  <br>

                                        Remove any apps that you spend the most time on, such as gaming or social networking. Modify your phone's settings so that you don't receive as many alerts. Take up a hobby or activity that does not require your mobile phone, such as playing an instrument or painting. Your smartphone is a lifeline that gives you real-time information and keeps you in touch with loved ones. However, it is critical to monitor your mobile phone usage to avoid becoming addicted to your gadget. Maintain a healthy and pleasant mental state by balancing your real-world involvement with your screen time.</p>
