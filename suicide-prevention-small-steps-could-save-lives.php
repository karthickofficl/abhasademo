                                    <div class="clearfix"></div>
                                    <p class="pt-serif">It is always heartbreaking to watch people harm themselves to death. Globally, more than 8 lakh people die due to suicide every year. Suicide remains the 12th leading cause of death. Research suggests that in India, suicides are becoming a very common phenomenon and are emerging as a leading cause of death. A whopping 1.5 lakh people die due to suicide in India every year. These statistics are utterly disheartening. Looking at the age group, 35% of suicides in India happen in the 18–30 age group and about 32% in the 30-45 age group. There is a huge scope for suicide prevention, and this blog is aimed at offering suicide prevention steps and understanding why people commit suicide. 
                                        


                                        <br>If you are looking for ways to prevent suicide, you are at the right place for this blog, which has proven suicide prevention steps.

                                    </p>
                                    <div class="pr-subtitle">Why do people commit suicide?</div>

                                    <p class="pt-serif">Suicidal emotions and thoughts can impact anyone, irrespective of their age, gender, background, etc. Such thoughts emerge when one is struggling to cope with a loss and repeatedly concludes that there is no means to solve a problem. Perhaps they think that death is a solution to their problems
                                        <br>Suicides can be prevented by understanding and perceiving the signs and symptoms. If you or your loved ones are considering suicide or going through suicidal thoughts, look up the following signs: Approach a therapist or a counsellor when you recognise these signs and symptoms. If you are looking for reasons why people commit suicide, the common causes of suicide are listed below.
                                    </p>

                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Strife or conflicts in family</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Being in an abusive relationship</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Grief after the loss of a loved one</p>
                                        </li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Inability to handle life problems due to depression.
</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Poor performance at school or in the workplace.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Facing mental or physical distress.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Use or misuse of drugs and alcohol.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Feeling guilty or angry for a reason.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Tormented due to bullying.</li>
                                    </ul>
                                    <p class="pt-serif">Teenagers today endure stress and depression in their struggle to achieve a work-life balance during the COVID and post-COVID periods. The pandemic posed challenges as it limited people to their houses and kept them away from social circles and outdoor activities. This causes a lot of distress. In addition, the learning platform moved from offline to online mode, which is new for young learners. This resulted in exam fear and anxiety for the majority of the students who were to face the board examinations that year. Temporary unemployment, working from home, and minimal physical contact were the factors that triggered anxiety in adults in recent days.<br>

                                    Depression is the principal cause of suicide among teens. They consider themselves feeble enough to handle such critical phases of life. Now that you have developed an idea of why people commit suicide, the next section describes the most common suicidal behaviours.</p>
                                    <div class="pr-subtitle">What is suicidal behavior?</div>
                                    <p class="pt-serif">The more attempts a person makes, the greater the chances of him committing suicide. Teenage suicides have now become more common compared to the past. More than half of the people who died by suicide showed warning signs that are described below. Observing these behaviours is important if you are working out suicide prevention steps. 
                                        <br>These signs were identified directly by close friends or family members.
                                    </p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Heedless and impulsive behavior</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Facing trouble concentrating</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Undergoing disturbances in sleep.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Constantly lacking energy</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Talking more about death in despair</li>
                                    </ul>

                                    <p class="pt-serif">If you notice something suspicious other than these, talk to them kindly and get to know what they are going through. Assure them that every problem can be sorted out. Embrace them and help them overcome the agony. Or else take them to therapy sessions, as proper communication is proven to solve the problematic situation. Abhasa, the leading rehabilitation centre in India, is one such place that offers counselling and therapy sessions to assist people in recovering from suicidal intentions and thoughts.
</p>

                                    <div class="pr-subtitle">How to prevent suicide?</div>
                                    <p class="pt-serif">Developing an understanding of how people with suicidal thoughts would feel is important. There are greater chances that they are in a wrecked state of mind that needs mending. Love, affection, and care from a loved one can heal the wounds and minimise the scars. The power of kindness is illimitable. Show empathy and compassion towards them. People with suicidal thoughts yearn for hope to shower upon them. They wait in anticipation of a miracle to toss their lives upside down. They crave a tide to sweep their distress away. Even a small act of concern you show can put them on the road to recovery.

                                        <br>Constantly reassure them that they are not alone, and encourage and motivate them to ask out. Seeking help can help them in a great way. All they need is a mechanism to heal their throbbing mind and disturbed heart. And who knows? They may even totally forget what troubled them.

                                    </p>

                                    <div class="pr-subtitle">Small suicide prevention steps</div>
                                    <p class="pt-serif">It is important that you learn suicide prevention steps, as there are chances that suicide can be prevented. Get familiar with the wrong signs, offer support, and keenly listen to them with love. Talk more about their mental health and take steps to eradicate the stigma around it. Families ought to be very supportive. The more the person opens up, the more rapidly they heal. They should be given chances to vent. Engage them in activities that are of interest to them. You may gift them a bicycle so they enjoy driving. You may take them on a vacation. You may encourage them to spend time in nature and let them relax. Help them deviate from reality to some other hypothetical world until they feel safe. 
                                        <br>You may motivate them to pen down their thoughts, as writing can direct a person to think the right way. It gives better clarity to perceive problems and arrive at solutions. This way, people learn what would work for them. Help them understand that living is inevitable and bigger than all their problems summed up.

                                    </p>

                                    <div class="pr-subtitle">Seek professional help</div>
                                    <p class="pt-serif">If family members get to know that their loved one is feeling distressed or depressed, they must understand that the person needs professional help at the earliest. Learning how to prevent suicide and why people commit suicide is very important. Watch out for the signs of suicide and educate yourself to differentiate behaviours and activities with suicidal intention from the usual thread.
                                        <br>So, empower yourself with suicide prevention steps. Most importantly, check in on them often and remind them that their family is with them to support them throughout. Save a life and help revive their hope and rejuvenate their mind. 

