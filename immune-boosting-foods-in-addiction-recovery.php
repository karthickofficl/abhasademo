                                    <p class="pt-serif">High-fibre foods can help ease constipation. Lighting and easy-on-the-stomach foods can help relieve feelings of sickness and vomiting. Protein shakes and smoothies can help provide nutrients when detoxification decreases your hunger. Complicated carbohydrates can help balance these levels, helping your own brain relax.  </p>
