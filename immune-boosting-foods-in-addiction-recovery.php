                                        <li class="pt-serif"><i class="fa fa-circle"></i><b>Strengthen the Immune System and Reduce Risk of Infection and Disease: </b> As soon as you start feeling much better, you’ll want to stay better, specifically when you prepare to leave the therapy centre and changeover back into the “real world.” Vitamin C, E, A, D, folic acid, iron, selenium, and zinc can function together to reinforce your immune system. A tougher immune system may help you combat infections, situations, and even several diseases. Some great-tasting immune-boosting foods are usually citrus fruits, shellfish, yoghurt, red bell peppers, almonds, sunflower seeds, green herbal tea extract, papaya, and poultry. </li>
