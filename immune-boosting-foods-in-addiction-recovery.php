                                        <li class="pt-serif"><i class="fa fa-circle"></i><b>Overeating: </b> Even though some substances can suppress your urge for food, others can make you consume more food than the body needs. </li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i><b>Organ damage: </b> Mistreating drugs and alcoholic beverages can damage your liver, pancreas, tummy lining, and other organs that help your digestive system break down food and soak up minerals and nutritional vitamins. </li>
