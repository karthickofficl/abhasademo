                                    <h1>Wanting to help someone with an eating disorder? Here are some tips. </h1>
                                </div>
                                <div class="fl-wrap text-block">
                                    <div class="clearfix"></div>
                                    <p class="pt-serif">Whether it is your friend, relative, or someone you know, to see them grapple with an eating disorder is heart-breaking. An eating disorder is one of the most complicated challenges that an individual can endure. Beyond being a physical challenge, it impacts their mental and emotional well-being. It is equally traumatic for the family and the people around them.
                                        If you are looking for information on how to help someone with an eating disorder, you are in the right spot. We are glad that you have come forward to understand the disorder in detail and to sort out ways to normalise your friend or family member. A plethora of questions leaf through your mind at the very thought of helping a dear one reverse their eating disorder. A lack of awareness and information makes you feel helpless all the way. We share your helplessness, and we have come up with a detailed article that may answer all your questions on how to help someone with an eating disorder. </p>
                                    <div class="pr-subtitle">How to understand if someone has an eating disorder?</div>
                                    <p class="pt-serif">Unlike the common perception that eating disorders are solely physical, they are a form of mental illness caused by a blend of biological, social, and psychological attributes. Eating disorders arise when people try to heal their stress, depression, or anxiety with food. They release their negative emotions on the food that they end up binge eating or undereating, leading to anorexia and other conditions. Some tend to develop an obsession with their weight and the calories they consume to such a degree that the subject of their weight and calories dominates other walks of life. While some of them engage in binge eating, purging, or consuming large quantities of food as they start to believe that eating is essentially out of one’s own control, if you are looking for ways to help someone with an eating disorder, understanding the symptoms might help. </p>

                                    <div class="pr-subtitle">Common symptoms of eating disorders</div>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Developing an obsession with food labels, calories, and their physical image</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Restricting food or cutting down food in excessive quantities</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Consuming dietary pills, stimulants, and drugs to lose weight</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Eating normally in front of people while bingeing when alone. Secret eating!</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Consuming junk foods and calorie-rich, not-so-healthy items in private</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Throwing up after almost every meal intake</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Consuming laxatives or taking up enemas</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Holding a negative self-perception, particularly with one’s own weight and appearance</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Endeavoring to hide their weight and exhibiting a continuous fear of weight gain</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Giving up on social life just because he or she wants to exercise </li>
                                    </ul>
                                    <p class="pt-serif">Given the above symptoms, it is pretty clear that treating eating disorders and restoring people to normalcy require therapies and consultations. You can read more about them at <a href="https://www.nimh.nih.gov/health/topics/eating-disorders">symptoms of eating disorders.</a> In case you notice any of the aforementioned symptoms do hot hesitate to initiate a deeper conversation with the concerned individual. The earlier, the easier it is. A lot of times, the impacted person does not open up because they have such low self-esteem that they put themselves down, imagining that they are not worthy of being helped or treated. If you are looking for ways to help someone with anorexia, read through. </p>


                                    <div class="pr-subtitle">How to help someone with an eating disorder?</div>
                                    <p class="pt-serif">In case you observe any of the above-mentioned symptoms in your friend, relative, or someone who is very close to you, the following may answer your question on how to help treat someone with an eating disorder. </p>

                                    <div class="pr-subtitle">Initiate a conversation! Speak up.</div>
                                    <p class="pt-serif">As far as we have observed, the near and dear ones of the affected display inhibition in initiating the conversation. You may be worried if you are mistaken. You may be afraid that the situation might aggravate, and the person may feel isolated if they learn that they have an eating disorder. This is a very normal concern, and you should always remember that people with such disorders are reluctant to come forward and ask for help. Because they are already feeling negative and depressed, there are greater chances that they will try to resist and refrain from such conversations. </p>

                                    <div class="pr-subtitle">Some tips for you to initiate a conversation</div>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Do not feel bad if they do not show any interest in discussing the issue</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Let them know that they are heard and that you would be there if he or she wants to talk to you about it</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Keep asking them if they require any assistance or support</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Do not feel dejected if they jump into denial or defensive mode</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Do not push them into fear by starting your conversation with all the negative or serious consequences of the disorder</li>
                                    </ul>
                                    <div class="pr-subtitle">Make mealtimes fun</div>

                                    <p class="pt-serif">Try to take meals together as a family or in a group. Keep the conversations going, and do not discuss problems or negative happenings. Discuss happy moments and funny incidents. This may help your loved ones shed their fear of food and push them to enjoy it. Do not force them to try more. Do not talk to them about the serious consequences of their eating disorders, as it might aggravate the issue. Do not keep monitoring them directly. This might motivate them to get into a private mode, and it may become harder to get them back on track. </p>

                                    <div class="pr-subtitle">Listen without reacting</div>
                                    <p class="pt-serif">The problem with most people who are suffering from eating disorders is that they feel unheard. They are afraid of being judged. All you can do is let them know that you are there for them, no matter what. Do not offer advice or criticise them for what they are going through. Listen without judging them or showing adverse reactions.</p>

                                    <div class="pr-subtitle">Do not talk about food, weight, etc.</div>
                                    <p class="pt-serif">Whether it is just the two of you, or there are people with you, avoid discussions on food, weight, etc. Do not talk negatively about weight, body image, or food. This might not solve eating disorders but aggravate the complications. Do not hesitate to eat well when you are with them. This might motivate them to get back to their healthy eating routine.</p>

                                    <div class="pr-subtitle">Eating disorder treatment.</div>
                                    <p class="pt-serif">Eating disorders require professional therapy, and we recommend that you get your friend or relative treated right away when you observe the symptoms. The right eating disorder treatment varies depending on various factors, including symptoms and the stage of the disorder. Perhaps the right treatment requires medical treatment, nutritional counselling, and therapeutic ounseling. Read through to understand how to help someone with eating disorders. </p>

                                    <div class="pr-subtitle">Medical consultation</div>
                                    <p class="pt-serif">Treating health issues is the utmost priority when it comes to eating disorder treatment. Take your loved one to medical treatment and check if he or she is malnourished or is struggling with anorexia. Whether or not they require residential treatment depends on the stage of the disorder. </p>

                                    <div class="pr-subtitle">Nutritional counseling</div>
                                    <p class="pt-serif">Educating your loved ones on the importance of healthy eating while also helping them with tailor-made diet plans, trackers, and milestones might help them get rid of their obsession with food. This is an effective eating disorder treatment. </p>

                                    <div class="pr-subtitle">Therapeutic counseling</div>
                                    <p class="pt-serif">Because eating disorders are mostly psychological, reversing the disorder may require therapies. Personalised therapeutic counselling would help them shed their stress and negative emotions. Educating your loved one on how to deal with not-so-favourable circumstances and stressful situations also helps. </p>

                                    <div class="pr-subtitle">Looking for an eating disorder treatment center?</div>
                                    <p class="pt-serif">We at <a href="https://www.abhasa.in/about/">Abhasa Rehabilitation Center</a> offer personal counselling to identify and treat the root cause of the eating disorder. We invest our efforts in trying to identify what has caused their obsession with food and weight. We subject them to trauma therapy and other holistic therapies to relieve their stress and anxiety. We also offer residential eating disorder treatment if your friend or family member requires one. The therapeutic ethos and the serene environment accelerate their healing and relieve themselves of their obsession with food. Educating them on the importance of eating the right food in the right quantity enables their quick return to normalcy. The residential treatment is more effective if the individual has a prolonged obsession with food. Spending some time away from their stressors accelerates their recovery. If you are looking for ways to help someone with anorexia, our eating disorder treatment centre has all the facilities to accelerate healing. </p>

                                    <div class="pr-subtitle">Support your loved one post their treatment.</div>
                                    <p class="pt-serif">Whether it is a therapeutic or physical treatment, your friend or family member still needs your support, even after their treatment period. Help them feel happy. Let them know that they are heard. They will endure good and bad days. There might be an urge inside their minds to get back to their dietary routines. So, do not be disheartened if the eating disorder treatment is taking some time. It usually takes longer periods. Make up your mind and keep supporting them throughout the recovery period. </p>

