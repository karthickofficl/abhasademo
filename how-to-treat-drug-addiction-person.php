                                        <br>Addiction is an effective and intricate disease. People who have a dependency to drugs cannot simply quit, even if they want to. The drugs replace the brain in a way that makes stopping physically and emotionally difficult. Treating dependency often requires long term care and treatment. Treating drug addiction can be done in any treatment centre, but the treatment provides in <a href="https://www.abhasa.in/about/">Abhasa Rehabilitation centre</a> with much more facility will help the client to recover faster. </p> 

                                        <div class="pr-subtitle">Exactly what drugs lead to addiction?</div>
                                        <p class="pt-serif">Drugs that are commonly misused include:</p>

                                            <ul class="ul">
                                                <li class="pt-serif"><a href="https://www.abhasa.in/alcohol-rehabilitation-centre-in-tamilnadu/">Alcohol</a></li>
                                                <li class="pt-serif">Club drugs, like GHB, ketamine, MDMA (ecstasy/molly), flunitrazepam (Rohypnol®).</li>
                                                <li class="pt-serif">Stimulants, such as <a href="https://www.abhasa.in/cocaine-drug-addiction-treatment-in-mumbai/">cocaine</a> (including crack) and methamphetamine (meth).</li>
                                                <li class="pt-serif">Hallucinogens, including ayahuasca, D-lysergic acid diethylamide (LSD), peyote (mescaline), phencyclidine (PCP) and DMT. </li>
                                                <li class="pt-serif">Inhalants, including solvents, aerosol sprays, gases and nitrites (poppers).</li>
                                                <li class="pt-serif">Marijuana.</li>
                                                <li class="pt-serif">Opioid pain killers such as <a href="https://www.abhasa.in/heroin-drug-addiction-treatment-in-mumbai/">heroin,</a> fentanyl, oxycodone, hydrocodone, codeine and morphine.</li>
                                                <li class="pt-serif">Prescription drugs and cold medicines.</li>
                                                <li class="pt-serif">Sedatives, hypnotics and anxiolytics (anti-anxiety medications).</li>
                                                <li class="pt-serif">Steroids (anabolic).</li>
                                                <li class="pt-serif">Synthetic cannabinoids (K2 or Spice).</li>
                                                <li class="pt-serif">Synthetic cathinone’s (bath salts).</li>
                                                <li class="pt-serif">Tobacco/nicotine and electronic cigarettes.</li>
                                            </ul>
                                        <p class="pt-serif">While these drugs are extremely different from each other, all of them strongly trigger dependency in the middle of the brain. That is why some of these ingredients are habit-forming, while others are not. Individuals feel intoxicated after using drugs. With time, the brain is changed by drugs. The mind becomes desensitised to the drug, so more of it must be used to produce the same impact.   

                                        <br>Since the person uses more, drugs take over his or her life. You can't stop enjoying other aspects of life. For many individuals, social, family, and work obligations drop aside. The person with SUD begins to feel like something’s wrong if he or she isn’t under the influence of the substance. They may become consumed with the need to recapture that original feeling. 

                                        <br>Anyone can develop a substance use condition. Nobody can predict whether a person will develop an addiction. A person may be more prone to medication use due to:</p>
                                            <ul class="ul">
                                                <li class="pt-serif"><i class="fa fa-circle"></i><b>Biology:</b> The person’s hereditary makeup, gender, racial, and mental health concerns may raise their risk of developing an addiction. About two-thirds of people in habit treatment are men. Particular nationalities are at higher risk for chemical use disorder.
•	Environment: The environment can affect the likelihood of establishing substance.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i><b>Environment:</b> The environment can affect the likelihood of establishing substance use conditions. For example, stress, peer pressure, physical or sexual mistreatment, and early direct exposure to drugs can raise the danger.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i><b>Age:</b> Teens who start taking drugs are especially at risk. The particular parts of the brain that control judgement, selection, and self-control aren't completely developed. Teens are more likely to engage in dangerous behaviours. In a developing brain, drugs can cause changes that make dependency more likely.</li>
                                            </ul>
                                        <div class="pr-subtitle">How might substance use disorder affect myself?</div>
                                        <p class="pt-serif">Medicines affect the brain, especially the “reward centre” of the brain. Humans are biologically motivated to seek rewards. Regularly, these rewards begin from healthy actions. Whenever you spend time with a adored one or eat a meal, the body releases a chemical called dopamine, that makes you feel pleasure. It is a cycle: A person seek out these because they incentive you with good feelings.  

                                        <br>Medicines send massive spikes of dopamine through the brain, too. But feeling inspired to do the things you need to survive (eat, work, spend time with loved ones), such massive dopamine levels can guide to damaging changes that change ideas, feelings and conduct. That can create an unhealthy drive to find pleasure from the medication and less from more healthy enjoyable experiences.  

                                        <br>The period revolves around seeking and consuming drugs to get that pleasurable feeling. Reliance on drugs changes the mind over time. It influences how the brain works and even the brain’s framework. That’s why health care providers consider material use disorder a brain disease. The particular first use of the drug is an option. But dependency can produce, creating a very dangerous condition. Drugs impact your decision-making capability, including the decision to prevent medication use. You might be aware there is an issue but unable to stop. With addiction, preventing drug use can be physically unpleasant. It can make you sick and even become life-threatening.</p>
                                        <div class="pr-subtitle">What are symptoms of substance use disorder?</div>
                                            <ul class="ul">
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Bloodshot eyes and looking tired.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Changes in appetite, usually eating less.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Changes in physical appearance, such as having a poor complexion or looking ungroomed.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Desire drugs.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Difficulty finishing tasks at the job, college or home.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Appealing in risky behaviours, despite knowing negative consequences (such as driving while reduced or having vulnerable, unguarded, isolated, exposed, unshielded, at risk sex).</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Inability to lessen or control drug use.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Problems with money.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Bodyweight loss.</li>
                                            </ul>
                                        <div class="pr-subtitle">How is substance use disorder diagnosed?</div>
                                        <p class="pt-serif">The particular initial step to diagnosing a medication habit is spotting the condition and seeking help. This preliminary step may start with involvement from friends or loved ones. As soon as someone decides to seek help for addiction, the subsequent steps include:</p>
                                            <ul class="ul">
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Finish the exam with a doctor.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>•	Individualised treatment, either inpatient or outpatient,</li>
                                            </ul>
                                        <div class="pr-subtitle">Precisely, what are the treatments for drug dependency?</div>
                                        <p class="pt-serif">A number of therapies exist for treating substance use disorders. Even in a severe situation, treatment can help. Often, you’ll get a blend of these therapies:</p>
                                            <ul class="ul">
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Cleansing: You stop taking drugs, allowing the drugs to leave the body. A person may need health care supervision to detoxify safely.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Medication-assisted treatments: During detox, medication can help control cravings and reduce withdrawal symptoms.</li>
                                                <li class="pt-serif"><i class="fa fa-circle"></i>Behaviour therapies: cognitive behaviour remedies or other psychotherapy (talk therapy) can help with addiction’s cause. Therapy also helps build self-esteem and teaches healthy handling mechanisms</li>
