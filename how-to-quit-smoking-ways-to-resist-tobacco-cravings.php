                                        <h3>How to Quit Smoking Ways to Resist Tobacco Crowding.</h3>
                                    </div>                                  
                                    <div class="fl-wrap text-block">
                                        <div class="clearfix"></div>
                                        <p class="pt-serif">Nicotine addiction is the most detrimental form of addiction. Smoking distorts the functioning of the entire body, its organs, and its cells in the long run. In spite of knowing this, people start smoking, which turns into an uncontrollable urge later. People start smoking out of improper influence. They consider smoking to be a trendy act that can even help them cope with stress.   
                                        <br>Smoking is physically addictive, and giving up tobacco is too hard. People become dependent on nicotine to a greater extent, and they endure withdrawal symptoms when trying to discontinue smoking. There are also cases where people entitle themselves as smokers so that when it comes to terminating the smoking habit, they feel like losing their identity. Smoking becomes an integral part of their behaviour in the long run.  

                                        <br>In this blog, we put forth easy and effective ways to quit smoking, manage withdrawal symptoms, handle tobacco cravings, and improve the quality of their mental and physical well-being.


                                        </p>
                                        <div class="pr-subtitle">Why should a person quit smoking?</div>

                                        <p class="pt-serif"><a href="https://www.health.gov.au/health-topics/smoking-and-tobacco/about-smoking-and-tobacco/what-are-the-effects-of-smoking-and-tobacco" target="_blank">Studies</a> suggest that within 10 seconds of taking a puff, tobacco and other chemicals in the smoke reach the heart, brain, and other organs of the body. Thus, smoking poses serious threats to the body of the smoker. There are 15 types of cancer that are caused by smoking, and with people’s will, they are preventable.</p>
                                        
                                                    <ul class="ul">
                                                        <li class="pt-serif"><i class="fa fa-circle"></i>Smoking is deleterious to the brain as it decreases the receptor count in the brain. In the long run, it turns into a compulsive behaviour in that one has to smoke cigarettes over and over to feel good.</li>
                                                        <li class="pt-serif"><i class="fa fa-circle"></i>Heat effused by smoking damages the buccal cavity, larynx, and trachea, along with the lips. Furthermore, it burns those parts too, which may initiate cancer. Cigarettes are proven to be carcinogenic or cancer-causing agents.</li>
                                                        <li class="pt-serif"><i class="fa fa-circle"></i>Lungs are the worst-affected organs by smoking. In the process of cleaning the irritants out of the lungs, the mucus and cilia are overexploited, causing chronic coughs. Smoking is indeed the leading cause of cancer in the lungs.</p>
                                                        </li>
                                                        <li class="pt-serif"><i class="fa fa-circle"></i>A smoker is vulnerable to heart ailments as smoking increases blood pressure. Also, in response to the irritants, the body releases cholesterol, which blocks blood vessels. Once the plaque blocks the coronary artery, it leads to a heart attack.</li>
                                                        <li class="pt-serif"><i class="fa fa-circle"></i>Smoking suppresses mood and causes depression. </li>
                                                        <li class="pt-serif"><i class="fa fa-circle"></i>Chances of catching infections like the flu are high for smokers as the chemicals in cigarettes invade the natural barriers present in the respiratory tract. It deteriorates the immune system and the respiratory system by damaging the mucous lining and the natural bacteria on them.</li>
                                                    </ul>
                                                <p class="pt-serif">It is indispensable for a person to protect and foster their body and ensure mental and physical well-being. So it’s for one’s own good that the person must quit smoking. The first and foremost step towards making a person drop smoking is to make them truly realise the harmful effects of smoking. The smoker must be fully aware of the mental and physical destruction smoking causes. The following section presents tips to quit smoking.</p>
                                                <div class="pr-subtitle">The best ways to quit smoking</div>
                                                <p class="pt-serif">In the new-fangled world, there are endless ways in which one can find help, support, and tips to stop smoking. It is a fact that, at no level, smoking is safe. Even occasional smoking can also cause cancer. So quitting completely is the best deed one can do for their body. Here are some tips to quit smoking:</p>
                                                    
                                        
                                                    <ol class="ul">
                                                        <li class="pt-serif"><i class="fa fa-circle"></i><b> A turkey is quitting.</b>
                                                            <br><span>When a person goes <a href="https://www.ncbi.nlm.nih.gov/pmc/articles/PMC5360817/" target="_blank">‘cold turkey’</a>, it means that he is quitting smoking abruptly. This sudden stop may cause intense cravings and withdrawal symptoms. The person may face difficulties in coping with those. However, the problems can be tackled by staying motivated. The person may also be motivated to save money, spend time with children or in nature, exercise, take up art or craftwork, work on their health and the way they look, or involve themselves in hobbies or other engaging activities. These steps can free them from nicotine dependence.</span>
                                                        </li>
                                                        <li class="pt-serif"><i class="fa fa-circle"></i><b> Taking varenicline</b>
                                                            <br><span>Varenicline is a commonly prescribed medicine for those who are willing to quit smoking. Varenicline, when consumed, triggers dopamine release in the brain. Dopamine is a feel-good hormone. The medicine also reduces nicotine withdrawal effects and cravings by blocking the nicotine receptors in the brain.  
                                                            <br>Doctors recommend nicotine patches along with the medicine to stabilise the mood and reverse the lows and loneliness that one experiences while getting rid of nicotine addiction.</span>
                                                        </li>
                                                        <li class="pt-serif"><i class="fa fa-circle"></i><b> Nicotine replacement therapy</b>
                                                            <br><span>The NRT offers less nicotine, eliminating the harmful chemicals from tobacco smoke. This can be taken in the form of patches, gums, inhalers, sprays, and lozenges. NRT can assist a person in managing withdrawal symptoms like hand and foot tingling, insomnia, mood swings, and nausea. A person can free themselves from smoking by gradually decreasing the number of patches.</span>
                                                        </li>
                                                        <li class="pt-serif"><i class="fa fa-circle"></i><b> Taking Bupropion</b>
                                                            <br><span>Taking bupropion is a cost-effective way to quit smoking antidepressants that eases irritation and depression resulting from nicotine withdrawal. It improves a person's concentration.</span>
                                                        </li>
                                                        <li class="pt-serif"><i class="fa fa-circle"></i><b> Cognitive Behavioral Therapy (CBT)</b>
                                                            <br><span>Cognitive-behavioural therapy (CBT) involves talking therapy and counselling sessions with assistance from trained doctors or professionals. During the therapy, they motivate the person and give the smoker proper guidance to quit smoking for good. The experts support the person by helping them manage mood changes, reducing the risk of relapse, and boosting abstinence. This has been proven to be a very effective way to quit smoking.</span>
                                                        </li>
                                                        <li class="pt-serif"><i class="fa fa-circle"></i><b> Taking Vitamins B and C</b>
                                                            <br><span>These vitamins are very useful in combating stress and depression. One experiences negative emotions while attempting to quit smoking. Vitamin B and C are powerful antioxidants, and taking more of them is an easy way to quit smoking. Smokers usually face a shortage of these vitamins, and consuming such vitamins can shield them from the oxidative stress caused by smoking. Intake of these supplements can accelerate the process of quitting.</span>
                                                        </li>
                                                        <li class="pt-serif"><i class="fa fa-circle"></i><b> Practice yoga, meditation, and tai chi to help your mind. </b>
                                                            <br><span>Yoga, physical workouts, and other meditative practices can train your mind and are essential when one is looking for tips to stop smoking. Exercising can make you feel good, relaxed, and calm. These are indeed excellent distractions from smoking.</span>
                                                        </li>
                                                        <li class="pt-serif"><i class="fa fa-circle"></i><b> Other inconclusive methods of quitting</b>
                                                            <br><span>The below-mentioned tips to stop smoking are believed to be useful but are yet to be proven and determined by doctors.

                                                                <ul class="ul">
                                                                    <li class="pt-serif"><i class="fa fa-circle"></i><b>E-cigarettes</b>
                                                                        <br><span>These devices allow a person to inhale only the nicotine vapours without other harmful components.</span>
                                                                    </li>
                                                                    <li class="pt-serif"><i class="fa fa-circle"></i><b>Lobelia</b>
                                                                        <br><span>Lobelia binds to the receptors in the brain that release dopamine and helps with mood swings. It also helps in cleaning the respiratory tract.</span>
                                                                    </li>
                                                                    <li class="pt-serif"><i class="fa fa-circle"></i><b>Hypnotherapy and acupuncture</b>
                                                                        <br><span>These processes help a person make positive changes in life. These are the favourite techniques for those who are willing to quit nicotine addiction. The method is otherwise called the hypnopuncture method.</span>
                                                                    </li>
                                                                </ul>


                                                            </span>
                                                        </li>
                                                    </ol>

                                                <div class="pr-subtitle">Effective ways to quit smoking</div>
                                                <p class="pt-serif">We tend to get grief-stricken when we see our loved ones or ourselves suffer mood swings and hassles when we try to quit smoking. Smoking not only impacts the smoker's life but also negatively impacts the people in close affinity with the smoker. It is therefore recommended to quit smoking as soon as possible.  

