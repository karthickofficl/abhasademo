                                <div class="fl-wrap text-block">
                                    <div class="clearfix"></div>
                                    <p class="pt-serif">One of the hardest and most important parts of getting sober is admitting that you have no control over alcohol or drugs. This step can be hard to understand because it asks you to stop trying to beat your addiction on your own and instead admit that you have no control over alcohol or drugs and that your life is completely out of your hands. This can be a very hard thing to do, especially if you still don't believe you have a problem. Your addiction may have started out as something you did for fun, as a way to hang out with friends, as a way to get away, or even as part of a medical treatment. As your addiction got worse, you may have kept finding reasons to use it and ignored how it hurt your personal and professional lives.</p>


                                    <p class="pt-serif">It's not easy to stop drinking. It's hard to admit you have a problem because you have to be honest with yourself and others. We live in a society that tells us we should be able to solve our problems and deal with challenges on our own, and if we can't, we are weak. But it takes a lot of courage to be honest, open, and willing to try something new because it means you know you need help and don't have all the answers.
                                    </p>


                                    <p class="pt-serif">Accepting what is and what isn't is the first step. It is a sign of progress and a way to get to freedom. As you go through the steps of Step One, you move from not knowing about this disease to knowing that it is real and that you can change. You're starting to think that you can change the way you live. This is an adventure. You must get to grips with the idea of powerlessness. This means recognising the many reasons why it's hard to admit, putting in the work to see how powerlessness seeps into every crack and crevice, taking the time to figure out what this really means for the future, and coming back to this step over and over again as you work on your recovery.
                                    </p>

                                    <p class="pt-serif"> Explore Step 1 of the 12 steps on the path to personal growth. Discover insightful articles on self-discovery and transformation at Abhasa. Start your journey. Even though you may not be able to change the fact that you have an addiction ("Give me the serenity to accept the things I can't change"), you are in control of what you do about it (the courage to change the things I can). Accepting that you can't control your addiction is freeing because it gives you time and energy to figure out what you can do to make the life you want and how to live a life of sobriety and recovery.</p>

                                    <p class="pt-serif"> Recovery isn't always a straight line, and some people have to go back to using again. It's hard to tell your 12-Step group, sponsor, or therapist that you've relapsed because it makes you feel like a failure. But this is the furthest thing from the truth. A relapse is a chance to change your treatment, your living situation, your behaviour, and other things that affect your ability to stay sober for a long time. Keeping yourself and others honest is a key part of getting better, and it will help you keep living the kind of life you want to live. </p>

                                    </p>


                                    <div class="pr-subtitle">As part of completing or returning to this step, it is important to reflect on the following questions:</div>

                                    <ul class="ul">
                                        <li class="pt-serif">• What does the disease of addiction mean to me?</li>
                                        <li class="pt-serif"> • How has my disease affected me physically? Mentally? Spiritually? Emotionally? Financially?</li>
                                        <li class="pt-serif">• How does the self-centred part of my disease affect my life and the lives of those around me?</li>
                                        <li class="pt-serif">• Have I blamed other people for my behaviour?</li>
                                        <li class="pt-serif">• • What does unmanageability mean to me?</li>
                                        <li class="pt-serif">• What troubles have been caused because of my addiction?</li>
                                        <li class="pt-serif">• Have I used alcohol or drugs to change or suppress my feelings?</li>
                                        <li class="pt-serif">• What reservations am I still holding onto?</li>
                                        <li class="pt-serif">• Do I accept that I will never regain “control” over drinking or drugs, even after a long period without use?</li>
                                        <li class="pt-serif">• What could my life be like if I surrendered completely?</li>
                                        <li class="pt-serif">• Am I willing to follow a sponsor’s direction, go to meetings regularly, and give recovery my best effort?
                                        </li>
                                        <li class="pt-serif">• Have I made peace with the fact that I’m an addict and that I’ll have to do things to stay clean?</li>
                                    </ul>

