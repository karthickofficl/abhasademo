                                    <div class="clearfix"></div>
                                    <p class="pt-serif">


                                        This short piece was written by a former patient at Abhasa Rehabilitation and Wellness Home. It may speak to people who have lost loved ones or who love animals.
                                    </p>

                                    <p class="pt-serif">"I have loved animals since I was a child, but during my addiction, I was lucky to be able to take care of myself and my kids, let alone a pet."
                                    </p>

                                    <p class="pt-serif">When I started getting better, my mom gave me a pug named Bentley. I couldn't change his name because he was given to my mom by a couple going through a divorce. However, I grew to like both the name and the dog. Bentley was a very shy person. He was like me and my family in that he was lost. As time went on, he and Dougal, the other little dog my mom gave me, became a big part of our family's life in recovery. Both dogs gave us a lot of love and joy in return. All they wanted was to be fed and cuddled, which we did plenty of.
                                    </p>

                                    <p class="pt-serif">"My beautiful, kind, and loving mother died three years ago, and my family and I had to deal with that. The same year my mother died, Dougal, our first small dog, also died. He was seventeen. I had a hard time letting go of my mother because the pain was sometimes too much for me to handle. Then Dougal came along. During this sad time, our family was comforted by Bentley. I talked to my sponsor and got more meetings than I ever thought possible by being honest about how I felt.
                                    </p>

                                    <p class="pt-serif">"I had to accept that I had lost these things. Then anger and resentment hit me like a hurricane, along with every other feeling. I went to a counsellor to get help from someone else. After about a year, it hit me one morning as I looked out over the fields that my mother loved so much. I could feel her all around me, and I felt this sense of acceptance. I felt at peace. After 15 years, I put our little dog Bentley to sleep in my arms on March 13, 2021. Our hearts were all broken. My mother gave us these two little dogs to keep us safe and look after us, but now they are gone. I felt like it was the last thing my mother gave me before she died. Then I thought back to that morning when I stood in the garden and looked out over the fields. I felt my mother's presence again, and then I accepted that my loved ones will always be with us, even though they don't have bodies on this earth. They will work hard with the higher power to help us face each new day with the knowledge that their memories live on in our hearts. I had to get back to the basics and learn to accept myself.
                                    </p>

                                    <p class="pt-serif">"I had to think about what I liked for a while. I need to slow down and enjoy everything I have. Even in the worst situations, there can be something to be grateful for. Some water. A sunset. How does my favourite food aste? It was a good talk. Cherish the bond with family and the shared pet, embracing life's highs and lows. Navigate pain with resilience, guided by an abhasa rehabilitation centre.
                                    </p>
