                                            but this only worsens the underlying problem.</li>
                                    </ul>
                                    <div class="pr-subtitle">Anxiety and Alcohol Abuse</div>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i> Alcohol abuse can increase feelings of anxiety, intensifying the sense of
                                            unease and fear.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i> Individuals with anxiety disorders may use alcohol as a way to momentarily
                                            alleviate symptoms, but it ultimately leads to a cycle of dependence.</li>
                                    </ul>
                                    <div class="pr-subtitle">Bipolar Disorder and Alcohol Abuse</div>
                                    <ul class="ul">
