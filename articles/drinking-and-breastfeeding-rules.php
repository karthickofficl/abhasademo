                                    <h3>CAN YOU DRINK ALCOHOL WHILE BREASTFEEDING?</h3>
                                </div>
                                <div class="fl-wrap text-block">
                                    <div class="clearfix"></div>
                                    <p class="pt-serif">Unfortunately, there hasn't been a lot of research done on the topic of drinking and breastfeeding, so many common myths persist. It can be confusing for new mothers to know if they should drink at all or if they should stick to water. You can find out the latest HSE health guidance and research on this page.</p>

                                    <div class="pr-subtitle">Can You Drink Alcohol While Breastfeeding?</div>
                                    <p class="pt-serif">Alcohol's negative effects on a developing baby have been extensively studied, leading to safe drinking guidelines for expectant mothers. The effects of <a href="https://www.abhasa.in/articles/risky-drinking-alcohol/">drinking alcohol</a> while breastfeeding have received much less attention. One surprising answer to the question "Can you drink while breastfeeding?" is that many women were advised to drink alcohol while breastfeeding in order to boost lactate, relax, and help their baby sleep.</p>
                                    <p class="pt-serif">Now that we know alcohol may be passed on to a nursing infant in significant amounts, many medical professionals are taking extra precautions. Authorities advise that breastfeeding moms either fully abstain from alcohol use until they stop breastfeeding or at least refrain from breastfeeding in the hours following alcohol consumption.</p>
                                    <p class="pt-serif">Since you and your baby are still getting used to breastfeeding, it's best to wait a month before drinking alcohol again while you're breastfeeding (if you choose to breastfeed). Avoid giving your infant alcohol through breast milk if you absolutely must drink while breastfeeding. These measures consist of:</p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>feeding your baby before drinking alcohol</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>expressing breast milk before drinking more than two standard drinks (one standard drink is half a pint of beer, a single measure of spirits, or a small glass of wine).</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Wait two hours after each standard drink before breastfeeding your baby.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>drinking no more than 11 standard drinks in a week.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>spreading drinks over the week</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>having at least two alcohol-free days per week</li>
                                    </ul>

                                    <p class="pt-serif">You may not be able to take care of your baby properly if you are under the influence of alcohol. Safety guidelines to follow if planning to drink include:</p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Never share a bed or sofa with your baby if alcohol has been consumed, as this can increase the risk of cot death (sudden infant death syndrome).</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>not drinking in the first month of your baby’s life</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>waiting two hours per standard drink before breastfeeding</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>if you think you will be drinking more than two standard drinks, plan to organise a babysitter</li>
                                    </ul>
                                    <div class="pr-subtitle">Alcohol’s Effect on Breastfeeding</div>
                                    <p class="pt-serif">Women who consume more than two alcoholic beverages per day are nearly twice as likely to cease breastfeeding after six months compared to women who reported consuming lower amounts, according to a review of the scientific literature. Researchers have found that breastfeeding mothers who drink alcohol express less milk, raising concerns that their infants may not get enough nutrition from breast milk alone.</p>
                                    <div class="pr-subtitle">The Effects of Alcohol on Babies</div>
                                    <p class="pt-serif">Most alcoholic beverages are found in breast milk. Breast milk normally contains traces of alcohol for roughly two to three hours per drink, beginning 30 minutes after ingestion. Yet, the more alcohol a mother consumes, the longer the alcohol will remain detectable in her breast milk.</p>
                                    <p class="pt-serif">Some of the possible outcomes, based on the available research, have been reported to be:</p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>that infants breastfed by women who had consumed alcohol prior to feeding ingested approximately 20% less milk in the first four hours after maternal alcohol consumption due to a reduction in the amount of milk produced.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>changes in an infant’s sleep patterns</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>decreased psychomotor development and reductions in cognitive abilities</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>reduced abstract reasoning ability and academic scores in children at later ages.</li>

                                    </ul>

                                    <p class="pt-serif">Yet, there are still relatively few rigorous scientific investigations on the subject.</p>
                                    <p class="pt-serif">While the effects of long-term exposure to alcohol during breastfeeding are largely unknown, it is advised to adhere to standard recommendations from healthcare authorities regarding alcohol consumption.</p>

                                    <div class="pr-subtitle">Can pumping breast milk after consuming alcohol reduce its alcohol content?</div>
                                    <p class="pt-serif">No, it has been demonstrated that the practice of "pump and dump" is false. The alcohol concentration in a mother's milk is virtually the same as the alcohol concentration in the mother's bloodstream and does not diminish when milk is expressed. Alcohol remains in breast milk as long as it is present in the mother's bloodstream. A mother could instead express milk before she has a drink, and this could be used to bottle-feed the baby when she has a drink.</p>

                                    <div class="pr-subtitle">Why might a woman who is breastfeeding consume alcohol?</div>
                                    <p class="pt-serif">This can be a beautiful but highly stressful time, and you may feel the urge to drink for various reasons. If you are a new mother and suspect you may have a drinking problem, you are not alone.</p>
                                    <p class="pt-serif">Despite the fact that many existing standards urge women to abstain from alcohol during pregnancy and breastfeeding, it is nonetheless common for new mothers to consume more alcohol than recommended.</p>
                                    <p class="pt-serif">Among the causes of alcohol consumption during breastfeeding were:</p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>post-natal depression</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>suffering from depression, anxiety, stress, sleep exhaustion, and the pressures of being a new mother.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>difficulties in the home</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>unclear advice from medical practitioners and family members</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>a lack of awareness of the risks of infant exposure.</li>
                                    </ul>
                                    <p class="pt-serif">If you are drinking excessively or cannot stop thinking about alcohol; if you are hiding your drinking from your partner and other family members; or if your drinking is interfering with your ability to care for your child, you may have a drinking problem. Consult the resources listed at the bottom of this page for guidance.</p>
                                    <div class="pr-subtitle">Risks of Addiction During Early Motherhood</div>
                                    <p class="pt-serif">Few studies address breastfeeding mothers who consume alcohol, which poses risks to you and your child. This transitional period can be exceedingly stressful, with 10–15% of new moms suffering from postpartum depression, a possible trigger for problem drinking and alcoholism.</p>
                                    <p class="pt-serif">If you are a new mother who is breastfeeding and you are experiencing stress, worry, sleep deprivation, boredom, postpartum depression, or you have a history of mental illness, you may consider using medications and alcohol to manage your symptoms. If you have a history of alcohol or drug abuse, if you are a younger mother (perhaps a teenager), if you have experienced trauma, if you lack the support of your partner, family, or friends, if you have a low income, and/or if you live in a deprived area, you are at a greater risk of exceeding the recommended amount of alcohol consumption during early motherhood and while breastfeeding.</p>
