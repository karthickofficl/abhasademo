                                    <p class="pt-serif">This plays a significant function in the neurofeedback training in the area of quantitative EEG brain mapping therapy. Neurofeedback training can be described as the process of making the brain wave activity of a person self-regulate through being informed of the activity in the brain in real time. Gradually, it enables them to reprogram their brains and thus enhance emotional self-regulation as well as the symptoms of these traumas. 
                                    </p>
                                    <div class="pr-subtitle">This Blog aims at describing what a QEEG looks like for a person with developmental trauma. </div>
                                    <p class="pt-serif">It should also be noted that in the case of developmental trauma, a QEEG brain map is likely to show several alterations. This is the reason why trauma that is received at an early age especially during the critical periods of brain development is likely to have a negative impact on the paths of the brain that may display themselves in different ways.In the QEEG brain map of someone, who was abused during childhood, we may have elevated alpha activity in the amygdale that means that the person’s fear/ anxiety level is up. Also, there might be reduced functioning in the pre-frontal cortex which is said to have the function of the affected person’s emotional regulator and executer. Such imbalances can result in emotional and behavior al disorders which are present in clients with developmental trauma. </p>
                                    
