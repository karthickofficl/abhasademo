                                    <div class="pr-subtitle">Isolation During Addiction</div>
                                    <p class="pt-serif">Is it bad to be alone when you're in recovery? It sometimes is. When we're alone, we tend to think about bad things, so it can be tempting to cut ourselves off from other people in recovery. When you're alone, it can be hard to stay motivated and keep away from the feelings of guilt and shame that are often linked to substance abuse symptoms. Loneliness causes a lot of problems for addicts in early recovery, so think about how bad it could be for someone who is not in recovery. Early on in recovery, people often feel alone. People often try to be alone to fill the void that addiction leaves. Many people turn to drugs or alcohol to deal with mental illness, emotional pain, or traumatic events. Even after getting help for drug or alcohol addiction, it can be hard to get used to a sober life.</p>
                                    <div class="pr-subtitle">Managing addiction recovery in isolation</div>
                                    <p class="pt-serif">Peer support is very important for getting better. According to a 2016 article in Substance Abuse and Rehabilitation about the benefits of peer support groups in treating addiction, peer support helps lessen feelings of isolation and keeps people going with their efforts to deal with their problems. It's something that people with substance use disorders can always count on as they go through treatment and get better. They can talk to each other about what it's like to be addicted, which helps them get better.</p>
                                    <p class="pt-serif">Getting over an addiction is hard at any time, but the recent coronavirus pandemic has made it even more difficult. The current quarantine rules have made it harder for people to get to meetings and appointments in person, and they have also made it harder for people who are still getting better. But that doesn't mean that nothing can be done. If you know what makes you feel stressed, it will be easier to deal with your current situation. And it's important to know how many different ways you can still get the help you need.</p>
