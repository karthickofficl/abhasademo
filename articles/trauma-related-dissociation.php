                                    <h3>HOW TRAUMA CAN BRING ABOUT DISSOCIATION</h3>
                                </div>
                                <div class="fl-wrap text-block">
                                    <div class="clearfix"></div>
                                    <p class="pt-serif">The effects of trauma on a person are nuanced, and people
                                        react to stress in different ways. It&#39;s true that people&#39;s
                                        responses to trauma can vary widely, even when they&#39;ve both
                                        experienced the same thing. Some reactions to trauma,
                                        however, tend to be more typical. Dissociation is one such
                                        reaction, and it occurs most frequently in circumstances of
                                        severe physical, emotional, or sexual abuse or severe neglect.
                                        One must realize that dissociation is a defence mechanism, a
                                        method by which the mind deals with the trauma it has
                                        experienced.</p>
                                    <p class="pt-serif">A person&#39;s duration of dissociation might range from hours or
                                        days to weeks or months. People who disassociate for
                                        extended periods of time are at risk for developing the
                                        <a href="https://www.abhasa.in/dissociative-disorder-treatment-in-mumbai/"> dissociative disorder</a>. This article describes dissociation,
                                        discusses its relationship to traumatic experiences, and offers
                                        advice on how to address it. With dissociation, a person loses
                                        touch with their own identity, along with their ideas, emotions,
                                        and memories. When under tremendous pressure, the brain&#39;s
                                        information-processing system breaks down.
                                    </p>
                                    <p class="pt-serif">Taking this approach requires you to disconnect from the world
                                        around you. It is a normal reaction to traumatic experiences,
                                        and it is more common in people who have had multiple
                                        traumatic experiences or who experienced trauma at a young
                                        age. Dissociation serves as a defence mechanism here. It&#39;s a
                                        coping mechanism for when things seem too overwhelming to
                                        deal with, such as during a tragic occurrence (after a traumaticevent).</p>
                                    <p class="pt-serif">It&#39;s a form of aiding the person in enduring it, I suppose. Up to
                                        10% of the population will experience dissociation at some
                                        point in their lives, according to studies. We also know that
                                        dissociation occurs in over 75% of people who encounter
                                        trauma, either immediately after the event or in the days,
                                        weeks, or months that follow. As a symptom of schizophrenia,
                                        bipolar disorder, or borderline personality disorder, dissociation
                                        is another possible outcome of these conditions (BPD).
                                        Dissociative states can also be induced by other medications
                                        like ketamine.</p>

                                    <div class="pr-subtitle">When does dissociation become problematic?</div>
                                    <p class="pt-serif">Mild forms of dissociation present as daydreaming or ‘zoning
                                        out, a bit like getting lost in a book, movie, or meditation, or
                                        simply losing a sense of awareness of what is going on around
                                        you. This is a normal process that almost everyone
                                        experiences from time to time. In its chronic and more serious
                                        form, it involves a deep disconnection from feelings and
                                        memories and is highly disruptive.</p>
                                    <p class="pt-serif">When a person dissociates, they may forget completely about
                                        certain time periods, events, or personal information and can
                                        feel strangely detached from their own body. A person with a
                                        serious dissociative disorder has no sense of identity and feels
                                        completely disconnected from the world. People have
                                        described the experience as feeling like being “outside the
                                        pilot’s chair, looking at, but not controlling, the gauges.</p>

                                    <div class="pr-subtitle">How many distinct dissociative illnesses are there?</div>
                                    <p class="pt-serif"><a href="https://www.abhasa.in/dissociative-disorder-treatment/">Dissociative disorders</a> fall into one of three broad categories.
                                        Specifically, these are:</p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>In dissociative amnesia, one&#39;s identity and past
                                            experiences become inaccessible to them.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Dissociation from self and environment (the world doesn&#39;t
                                            seem real, like in a dream) and depersonalization or
                                            derealization disorder (body parts can feel smaller or not
                                            real)</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Dis characterized by the presence of two or more distinct
                                            identities that do not merge or disappear</li>
                                    </ul>

                                    <div class="pr-subtitle">Dissociation: What Kind of Trauma Is Responsible?</div>
                                    <p class="pt-serif">Dissociation can be brought on by any traumatic experience. It
                                        occurs in the majority of cases after an individual experiences a
                                        distressing event over which they had no control. This may be
                                        the result of repeated or chronic stress (as that caused by war)
                                        or abuse. To dissociate is to remove oneself from the present
                                        moment, from the agony and terror that one is experiencing.
                                        Dissociation is a common reaction to the trauma of any kind.</p>
                                    <p class="pt-serif">Anything from being attacked or abused (either physically or
                                        emotionally or sexually), to being in the line of fire in a battle, to
                                        being kidnapped, to undergoing invasive medical treatments, to
                                        being neglected, all fall into this category. The American
                                        Psychiatric Association reports that over 90% of patients with
                                        dissociative identity disorder in the United States, Canada, and
                                        Europe had a traumatic upbringing.</p>

                                    <div class="pr-subtitle">Can you explain how traumatic experiences lead to dissociation?</div>
                                    <p class="pt-serif">The neuroscience of dissociation after trauma is crucial to
                                        comprehend. You have surely heard of the &quot;fight or flight&quot;
                                        response that the body has while under stress. This is a
                                        defence system that has evolved within the body to keep us
                                        safe. Hormones are released when we feel threatened or in
                                        danger, preparing us to either stay and fight or run.</p>
                                    <p class="pt-serif">It&#39;s a natural process that gives you a jolt of energy and makes
                                        you more aware of your surroundings. The body responds by
                                        increasing its metabolic rate, respiratory rate, and peripheral
                                        eyesight, among other things, to meet the increased demand
                                        for oxygen. However, there are occasions when neither fighting
                                        nor fleeing is an option (perhaps because they are too young).
                                        When this happens, bodily functions freeze.</p>
                                    <p class="pt-serif">The brain sends out substances to dull your senses so you can
                                        cope. Our ability to think ceased. Dissociation is a state of
                                        feeling detached and disoriented. This is a basic, hardwired
                                        response to danger. It is possible for the body to develop an
                                        excessive or overactive response following a traumatic incident.
                                        Dissociation&#39;s neural-circuit dynamics and chemical basis have
                                        been uncovered through studies conducted at Stanford
                                        University. This research points to a specific protein in a
                                        specific population of cells as being essential to the experience
                                        of alienation.</p>
                                    <p class="pt-serif">Recent studies of abused children and adolescents have
                                        revealed neurochemical, functional, and structural
                                        abnormalities in brain regions important for cognition and
                                        memory. Dissociative symptoms are associated with worse
                                        cognitive performance in borderline personality disorder. A
                                        decrease in activity in limbic(-related) temporal areas
                                        (amygdala, superior temporal gyrus, fusiform gyrus), an
                                        increase in activity in frontal areas (inferior frontal gyrus,
                                        dorsolateral prefrontal cortex), and a change in the way these
                                        areas interact with one another have been seen.</p>
