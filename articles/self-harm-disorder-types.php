                                    <h3>THE CONCEALED EPIDEMIC OF SELF-HARM</h3>
                                </div>
                                <div class="fl-wrap text-block">
                                    <div class="clearfix"></div>
                                    <p class="pt-serif">Self-harm has become one of the most shocking mental health problems of our time. Self-harm is much more common than we think, and the number of teens who do it is going up. Studies show that about one-fourth of 14-year-olds have hurt themselves. These numbers don't show how bad the problem is likely to be. A lot of statistical studies are based on how often people go to the emergency room.</p>


                                    <p class="pt-serif">But most people who hurt themselves don't go to the doctor. Instead, it is a secretive act that is never told to anyone. Non-suicidal self-harming, or NSSH, as it is called in the scientific world, is a complex and multifaceted behaviour that has a wide range of causes and motivations. It needs to be better understood and dealt with right away. This blog explains how complicated self-harm is, talks about some of the reasons why people do it, and looks at ways to treat behaviours that are often misunderstood.</p>

                                    <div class="pr-subtitle">What is self-harming?</div>
                                    <p class="pt-serif">Self-harm is the intentional infliction of bodily harm on one's own body. People self-injure as a coping mechanism for challenging emotions, traumatic memories, and/or overwhelming situations and/or experiences. It may be preceded by a succession of thoughts or be entirely spontaneous. It most commonly takes the shape of slashing, burning, or non-fatal overdosing, but it can take any form that results in injury. Eating disorders, for instance, are severe psychosomatic diseases that constitute a form of self-harm.</p>

                                    <p class="pt-serif">Self-harming behaviours include:</p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Scratching</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Cutting</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Banging or hitting</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Pulling out hair</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Overdosing on medication </li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Biting</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Scalding with hot water</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Burning</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Eating or drinking poisonous substances</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Not allowing wounds to heal</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Starving oneself, binging or purging</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Participating in dangerous behaviour</li>

                                    </ul>
                                    <div class="pr-subtitle">Why do people self-harm?</div>
                                    <p class="pt-serif">According to research, those at risk for self-harm frequently describe experiencing chronic emptiness, alienation, and loneliness, in addition to severe, overpowering negative emotions. Kim L. Gratz. The Department Chair and Professor of Psychology at the University of Toledo has published extensively on 
                                        <a href="https://www.abhasa.in/borderline-personality-disorder/">borderline personality disorder</a> (BPD), non-suicidal self-injury, and emotion regulation. In her work on self-harm, she states, "Theoretical research on the aetiology of self-harm implies that family-based early experiences are more likely to be connected with self-harm.</p>

                                    <p class="pt-serif"> According to Gratz, a dysfunctional family background, father-daughter incest, abuse in the context of pathological family interactions, or some other childhood trauma, neglect, or insecure attachment are connected with self-harm. The argument is that, although self-harm may be sparked by a current event or scenario, there is typically a more complex life story underlying it.</p>

                                    <p class="pt-serif"> People self-harm as a coping mechanism for the emotional trauma they cannot bear. This may have accumulated over the years. The link between early childhood trauma and the fact that not everyone responds to stress in this manner explains why. Not everyone harms himself. When a person is emotionally challenged and feels unable to seek assistance, some will turn to drinking, video games, food (or restricting their food intake), or narcotics. They may even engage in a combination of these destructive actions. Self-injury is a coping mechanism for those who are unable to verbalise their feelings. This is a means of asserting control.</p>
                                    <p class="pt-serif">According to the World Health Organisation, the COVID-19 pandemic has produced a 25% increase in the prevalence of anxiety and sadness worldwide.</p>
                                    <p class="pt-serif">4 Lockdowns have increased family abuse and domestic violence and cut people off from their typical support networks, such as jobs, college, and school.</p>

                                    <p class="pt-serif">The global coronavirus pandemic has had a disproportionately negative impact on the mental health of adolescents, who are disproportionately susceptible to suicidal and self-harming behaviours. There has been an upsurge in the incidence and severity of self-inflicted injuries presented to emergency departments, according to research.</p>
                                    <p class="pt-serif">The function of social media is also becoming increasingly significant. Other research indicates that the use of social media by individuals with mental health issues, particularly those who are prone to self-harm, has both potential benefits and drawbacks. Different individuals are likely to be affected differently by their online experiences, and the same individual may be affected differently at different times. It creates a dilemma on how and to what extent social media sites' material should be governed.</p>
                                    <div class="pr-subtitle"> How do you know if someone is self-harming?</div>
                                    <p class="pt-serif">As can be observed (and this blog post just scratches the surface), the causes of self-harm are complex. Most individuals who engage in self-harm conceal their behaviour, making it difficult to detect. However, there are several red signals to watch out for. Depression or anxiety can be precursors to self-harm, so be aware of mood and behaviour changes. This may appear as a lack of desire or interest in anything, as well as an aloof demeanour. Self-harm can also be attractive to perfectionists and individuals who have difficulties setting boundaries.</p>
                                    <p class="pt-serif">Other indicators to watch for include:</p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Undiagnosed cuts, burns, or bruising</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Covering up in warm weather by donning long sleeves and pants or thick tights</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>alterations in eating habits (over- or under-eating)</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Exercising too much</li>

                                    </ul>
                                    <div class="pr-subtitle">How do you support a loved one who is self-harming?</div>
                                    <p class="pt-serif">There is nothing more upsetting than discovering a loved one is engaging in self-harm. It can be difficult to know what to do, especially if you do not comprehend their motivation. It begins with patience and comprehension. Inform the person you are assisting that they do not need to disclose everything at once. It might be difficult to find the right words to convey, especially when the reasons are complex. They might not even completely comprehend why they are doing it. Try not to judge others. Focus on the individual as a whole and not only on the self-harming behaviour. Try to have compassion. Don't make decisions for them; give them autonomy over their own choices. The most significant factor is that they seek expert assistance. You can offer to help them locate the appropriate assistance. This could be via a hotline, a support group, or a therapist with specialised training.</p>

                                    <div class="pr-subtitle">Treatment at Abhasa</div>
                                    <p class="pt-serif">Self-harming is a sort of self-destructive behaviour that will not go away on its own and therefore requires professional treatment. As intentional self-harm is a significant risk factor for eventual suicide, it is crucial to seek help as soon as possible.</p>

