                                    <h3>BREAKING THE SILENCE: HOW DO SURVIVORS OF SEXUAL ABUSE SEEK HELP?</h3>
                                </div>
                                <div class="fl-wrap text-block">
                                    <div class="clearfix"></div>
                                    <p class="pt-serif">The prevalence rate of sexual abuse is shockingly high. One-third of women and one-sixth of men have reportedly experienced sexually motivated violence or abuse in their lifetime.</p>
                                    <p class="pt-serif">High rates of sexual abuse of children paint a disturbing picture. Every nine minutes, child protection services in the United States uncover evidence supporting an accusation of child sexual abuse. According to the National Sexual Violence Resource Centre, one in four girls and one in six boys will be raped before they reach the age of eighteen.</p>
                                    <p class="pt-serif">In certain Asian, African, and Middle Eastern nations, this number is significantly greater. Statistics are similarly problematic in Europe. In Germany, for instance, 10 to 15% of girls and nearly 6% of males experience sexual assault as children. How can I help a survivor of sexual violence in our society? Each and every individual has to take care of it.</p>
                                    <p class="pt-serif">Sexual abuse and acts of sexual violence occur everywhere, regardless of socioeconomic status. Sexual abuse is a global epidemic that affects all sectors of life. Not forgetting that the statistics only reflect known or recorded infractions How many more instances of sexual abuse are concealed?</p>
                                    <p class="pt-serif">All traumas, but especially sexual and violent traumas, have catastrophic and long-lasting effects on individuals. Seeking assistance is difficult, and many victims of sexual assault are frightened to come up.</p>
                                    <p class="pt-serif">This landscape is shifting because of campaigns such as #MeToo and #ItsNotOkay. The establishment of numerous trauma treatment programmes assists sexual assault victims in breaking their silence and seeking assistance. This blog contextualises sexual abuse and analyses some of the resources available to sexual abuse survivors.</p>

                                    <div class="pr-subtitle">What is sexual abuse or sexual violence?</div>
                                    <p class="pt-serif">A definition of sexual abuse may appear needless, but the low conviction rate of rapists demonstrates that the sense of consent is a highly ambiguous concept. Consent for sexual activity is a vast topic that will not be explored in this blog, but it must be mentioned to provide background for why sexual abuse must be precisely defined. Supporting survivors of sexual assault is very important.</p>

                                    <div class="pr-subtitle">The health consequences of sexual abuse</div>
                                    <p class="pt-serif">Sexual abuse involves psychological and emotional as well as physical consequences. There are numerous possible consequences. For instance, research indicates that child neglect and physical and sexual abuse are related to an increased risk of midlife mortality.</p>
                                    <p class="pt-serif">Such an assault leaves indelible marks on the psychology and personality of the victim. While there may be common responses to sexual abuse, it is essential to recognise that not all victims may act or feel identically. Importantly, there is no incorrect or correct way to feel or behave. Trauma, including one-time, repeated, or long-lasting experiences, affects each individual differently. How will survivors of sexual abuse behave in society? Are we really thinking about it? Well, society has to be educated about sexual abuse and the mental health of its members.
                                    </p>
                                    <p class="pt-serif">This type of trauma is intricate, and the health repercussions can be severe. Forced sexual activity can result in bodily harm, such as genital injuries, and may have gynaecological consequences, such as pelvic inflammatory disease (PID), recurrent urinary tract infections, or sexually transmitted illnesses.</p>
                                    <p class="pt-serif">The trauma exposes victims to a variety of psychological and emotional disorders, including shock, anxiety, sadness, panic attacks, <a href="https://www.abhasa.in/post-traumatic-stress-disorder-treatment-in-tamilnadu/">post-traumatic stress disorder</a> (PTSD), and rape trauma syndrome. Everyone reacts differently to trauma; thus, its effects might be subtle, pervasive, or blatant.</p>
                                    <p class="pt-serif">Recent study indicates that sexual abuse in childhood is a
                                        significant risk factor for <a href="https://www.abhasa.in/borderline-personality-disorder/">borderline personality disorder</a> (BPD).
                                            And adult sexual abuse is substantially more prevalent among BPD patients than among those with other personality disorders (PDs). BPD influences how an individual thinks, feels, and interacts with others. Individuals with BPD may experience intense and fluctuating emotions, persistent sensations of emptiness, and difficulties controlling their anger.</p>
                                    <p class="pt-serif">It is normal for victims of sexual abuse to blame themselves. Many victims of sexual abuse suffer from dissociation, a defence mechanism employed by the brain to cope with trauma. On one end of the spectrum, disassociation manifests as daydreaming, while on the opposite end, it manifests as extreme trouble functioning with the real world.</p>
                                    <p class="pt-serif">Self-harm, addictive behaviours (substance and alcohol misuse), food disorders, sleeping difficulties, and even suicide are further consequences. Additionally, survivors may have trouble with relationships, particularly sexual intimacy.</p>
                                    <p class="pt-serif">Sexual abuse or sexual assault refers to any unwanted activity of a sexual nature that occurs without consent or comprehension. It refers to any action that forces or coerces someone to engage in sexual activity against their will.</p>
                                    <p class="pt-serif">Sexual assault and abuse examples include:</p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>inappropriate kissing or touching</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>unwanted physical or sexual aggression</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Sexual assault or attempted sexual assault</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Refusing to use condoms or denying access to birth
                                            control is unacceptable.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Preventing someone from taking precautions against
                                            sexually transmitted diseases (STIs) </li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Sexual intercourse with a person who is extremely
                                            intoxicated, drugged, unconscious, or unable to provide a
                                            clear and informed &quot;yes&quot; or &quot;no&quot;</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Threatening or pushing someone to engage in sexual
                                            activity against their will.</li>
                                    </ul>

                                    <div class="pr-subtitle">Seeking help</div>
                                    <p class="pt-serif">Rape and sexual assault are generally carried out by someone familiar to the victim. It is horrible to experience such suffering at the hands of a trusted person. The event leaves the victim feeling confused, afraid, alone, and often ashamed.</p>
                                    <p class="pt-serif">It’s not uncommon for an abuse survivor to blame themselves or to feel dirty and weak. Such abuse can cause one to mistrust his or her own judgement. When a person has lost all sense of safety and trust, it can be exceedingly difficult for him or her to seek assistance. Each and every individual's responsibility is to support survivors of sexual assault.</p>
                                    <p class="pt-serif">But if a victim of sexual abuse does nothing, they will never live a full life. It is likely that they will experience emotional suppression and disconnection. Not only do shutting down and repressing emotions have physiological effects, but they also reduce happiness.</p>
                                    <p class="pt-serif">The first step in healing from sexual abuse is to speak out. It can be extremely difficult for victims of sexual assault to admit what has occurred. However, this is a crucial step. It starts by reaching out to someone who is trustworthy. This could be a relative or a friend.</p>
                                    <p class="pt-serif">If talking to friends or family results in a negative response, there are still alternative routes that can help, such as a counsellor, therapist, or even a rape or sexual assault helpline. Some individuals may benefit from joining a support group. Google's local support services.</p>
                                    <p class="pt-serif">Step two is to seek psychiatric assistance. Extremely tough to manage on one's own are the sentiments that develop from vulnerability. Even if the victim recognises rationally that they are not at fault, guilt and shame are typical. It stems from misconceptions such as not being cautious enough, placing too much faith in someone, or failing to stop the assault from occurring.</p>
                                    <p class="pt-serif">Importantly, rehabilitation from sexual trauma requires time and can be an emotionally hard process. During the therapeutic process, the victim may have flashbacks, nightmares, and distressing recollections. It is crucial to seek support from a therapist who is experienced in dealing with trauma and sexual assault. Immediate access to the appropriate assistance is vital.</p>
                                    <div class="pr-subtitle"> The sexual abuse epidemic</div>

                                    <p class="pt-serif">The World Health Organisation (WHO) estimates that one in three (30%) of women globally have been subjected to either physical and/or sexual intimate partner violence or non-partner sexual violence throughout their lives. 6 This statistic has stayed basically stable over the past decade, which is alarming for survivors of sexual assault.</p>

                                    <p class="pt-serif">One of the many underrecognized facets of sexual abuse is the effect of poor sexual encounters in infancy on the lives of adult men. Evidence suggests that at least one in six men has experienced sexual abuse or assault, whether in childhood or as adults. While a UK study reveals that half of the male individuals who participated in the survey had experienced unwanted sexual experiences, this number may be higher.
                                    </p>
                                    <p class="pt-serif">A meta-analysis of 22 American-based studies revealed that 30–40% of females and 13% of boys experience sexual assault throughout childhood.</p>
                                    <p class="pt-serif">10 In 2016, a comprehensive analysis of data revealed that 1 billion children (ages 2 to 17) worldwide, or more than half of all children, experienced some sort of violence.</p>
                                    <p class="pt-serif">However, statistics and studies only disclose a portion of the truth. What about the victims who remain silent? How many additional men and women are trying to spend their lives in secrecy as survivors of this heinous crime?</p>
                                    <p class="pt-serif">The rising prevalence of mental health illnesses, specifically PTSD and/or addiction, may be more indicative of the truth. Of course, not everyone with a mental health issue, such as PTSD or addiction, will have experienced sexual abuse in the past, but it is likely that many will.</p>
