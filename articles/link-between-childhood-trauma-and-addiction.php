                                    <div class="pr-subtitle">
                                        FAQs
                                    </div>

                                    <p class="pr-subtitle small-title">
                                        Can childhood trauma really lead to addiction?
                                    </p>

                                    <p class="pt-serif">
                                        Yes, childhood trauma significantly increases the risk of developing addiction later in life. Trauma can create emotional distress and dysregulation, leading individuals to seek relief through substance abuse.
                                    </p>

                                    <p class="pr-subtitle small-title">
                                        If someone was abused as a child, what are some signs that they might become addicted?
                                    </p>

                                    <p class="pt-serif">
                                        Signs that childhood trauma may be linked to addiction include the use of substances as a means of coping, persistent emotional distress, difficulty regulating emotions, high-risk behaviours, and a family history of addiction.
                                    </p>

                                    <p class="pr-subtitle small-title">
                                        What treatments are available to address addictions rooted in childhood trauma?
                                    </p>

                                    <p class="pt-serif">
                                        Treatments available to address addiction rooted in childhood trauma include trauma-focused therapies, cognitive-behavioural therapies, support groups, counselling, and holistic approaches such as mindfulness and yoga.
                                    </p>

                                    <p class="pr-subtitle small-title">
                                        How can society help prevent substance abuse in individuals with childhood trauma?
                                    </p>

                                    <p class="pt-serif">
                                        Society can help prevent substance abuse in individuals with childhood trauma by raising awareness, reducing stigma, providing access to support services, implementing trauma-informed education, and promoting early intervention programmes.
                                    </p>

                                    <p class="pr-subtitle small-title">
                                        How long does the recovery process typically take for someone with a trauma-related addiction?
                                    </p>

                                    <p class="pt-serif">
                                        The recovery process for someone with a trauma-related addiction varies from individual to individual. It can depend on factors such as the severity of trauma, the duration of addiction, access to treatment, and the individual's commitment to recovery. Patience, support, and consistent therapeutic interventions are essential for a successful recovery.
