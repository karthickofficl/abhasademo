                                    <h3>Self Esteem – How Does It Relate to Addiction?</h3>
                                </div>
                                <div class="fl-wrap text-block">
                                    <div class="clearfix"></div>
                                    <p class="pt-serif">Does what other people think of us matter? Almost all of us are affected in some way by what other people think, except for those with certain mental illnesses like psychopathy. A low sense of self-worth can lead to addiction, and it can also keep someone addicted. In this article, we will look at how low self-esteem and addiction are linked.
</p>

                                    <div class="pr-subtitle">We Need to Relate to Each Other</div>
                                    <p class="pt-serif">We are all affected by what other people think. Even though we may think we are, most of our happiness comes from how other people react to how we act. To keep a healthy sense of reality, we need to be reminded of things all the time. We can't live alone for long because, whether we like it or not, we need each other to live. One obvious example is reproduction, but there are many others, like sharing emotions like happiness and sadness and working together to meet our basic needs for safety, food, and shelter.
</p>
                                    <p class="pt-serif">"Involved in mankind," as John Donne put it so well. We must talk to each other. Whether they know it or not, it is the job of our fellow humans to constantly tell us what to do. The nod of the head, the smile of recognition, or even a disdainful look is all feedback that we need, maybe unconsciously, to keep us on the right path.
</p>

                                    <div class="pr-subtitle">Contributing Factors to Addiction</div>
                                    <p class="pt-serif">People who are addicted can usually point to a number of things that led to their problem. A family history of drug abuse, trauma in childhood, or stress at work are all common examples. Low self-esteem can also be a factor. This is often seen as a lack of faith in one's ability to deal with life. People who have low self-esteem as kids may find that a certain drug or behaviour makes them feel better about themselves. This can quickly lead to addiction. So, a person who isn't good with people might find that drinking helps them get along with others.
</p>

                                    <div class="pr-subtitle">The Double Impact of Low Self-Esteem</div>
                                    <p class="pt-serif">One part of the problem is that people use addictive drugs and behaviours to boost their confidence. When addiction is involved, it's like a "double whammy."
</p>
                                    <p class="pt-serif">People who don't feel good about themselves will do the same thing over and over until it becomes an obsession. This will make them act in ways that are not acceptable, like stealing to get money for their habit, which will hurt their already low self-esteem.
</p>
                                    <div class="pr-subtitle">What Is Low Self-Esteem?</div>
                                    <p class="pt-serif">LLow self-esteem is the feeling that you aren't good enough, don't deserve love, and are usually the reason why other people are unhappy. It can happen when a child is exposed to bad behaviour as a child. Getting hurt by abuse, neglect, or unfair criticism as a child can make a person feel like they don't matter. Losing a job or another traumatic event can have the same effect on an adult.</p>
                                    <div class="pr-subtitle">Self-worth needs to be built up.</div>
                                    <p class="pt-serif">Good self-worth doesn't just happen; it's earned through hard work. A lot of people who are addicted to drugs or alcohol get into the habit of making concessions, taking shortcuts, and doing things they know are wrong, like lying, cheating, and petty theft.
</p>
                                    <p class="pt-serif">All of this makes a person feel bad about themselves because we are moral people who know when we aren't living up to our own standards. When we get sober, we have to learn how to do things right all over again. Every day is made up of big and small decisions, and we need to get in the habit of making decisions based on what will help us get better and feel better about ourselves.
</p>
                                    <div class="pr-subtitle">Learning to Make Good Choices</div>
                                    <p class="pt-serif">Self-forgiveness means that you acknowledge your past mistakes and try to figure out why they happened. Talk about them with someone who cares if you can. Tell yourself that your mistakes aren't because you're a bad person. Instead, they're because you didn't know how to deal with the problems you faced because of things you couldn't control, like childhood trauma or an addiction.
</p>
                                    <p class="pt-serif">Work on taking care of yourself instead of punishing yourself. Start a self-affirmation routine. When you wake up, look in the mirror and tell yourself, "I am a good person who has made mistakes, but I now value myself and can do better." Hearing yourself say this makes you feel this way even more.
</p>
                                    <p class="pt-serif">Get in the habit of taking compliments. Instead of putting yourself down or saying it was just luck, say, "Thank you; I'm glad I could do that" or something similar.
</p>
                                    <p class="pt-serif">Help someone, and make it a habit to do so. Simple acts of kindness can make us feel good and help others at the same time, and they don't cost us anything.
</p>
                                    <p class="pt-serif">Ask for help. People with low self-esteem are often afraid to ask for help, but doing so and getting it boosts our sense of worth and ability.
</p>
                                    <div class="pr-subtitle">What Can I Do to Improve My Self-Esteem?</div>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Think about your own needs, wants, and limits.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Prioritize self-care (sleep, nutrition, movement, etc)</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Spend time doing things you like.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Spend time with people who will help you and who you can help.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Take care of your living space and the place you live. </li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Help someone by doing something nice (check in on a friend, make a small donation to charity)</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Pay more attention to what you say to yourself (practice kindness and compassion towards yourself).</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Say positive things to yourself.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Write down three things for which you are thankful.</li>
                                    </ul>
