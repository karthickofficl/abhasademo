                                    <h3>INTERNET & SOCIAL MEDIA ADDICTION</h3>
                                </div>
                                <div class="fl-wrap text-block">
                                    <div class="clearfix"></div>
                                    <p class="pt-serif">With the advent of the era of social media, the concept of addiction to online social platforms have become an increasing source of concern. A social media addiction, defined as a behavioural addiction, is characterised by a preoccupation with social media and a compulsion to use it. Nevertheless, there is a thin boundary. The majority of us spend hours per day on social media, but it becomes problematic when it influences our behaviour, thoughts, and actions.</p>
                                    <div class="pr-subtitle">How can we stop social media addiction?</div>
                                    <p class="pt-serif">You can stop your social media addiction in a variety of ways; simple changes such as turning off notifications, putting your phone somewhere else while you sleep, or removing your phone from your morning routine and conducting digital detox may be a good place to start.</p>
                                    <p class="pt-serif">However, if you develop a psychological addiction to the internet or social media, the best course of action, according to the therapy team, is to seek out a specialised addictions clinic, such as Abhasa Rehabilitation and wellness home, which has programmes in place to rewire the brain and assist you in managing your addiction issues.</p>

                                    <div class="pr-subtitle">What causes addiction to social media?</div>
                                    <p class="pt-serif">Frequently, social media can lead to melancholy, feelings of isolation, and illness. Constant comparisons, the lack of attention of views or likes on your post; making you feel unpopular and inadequate, the feeling of missing out and the inability to keep up with others, arguments, disrespectful comments, and the perception that others live a "picture-perfect life" relative to your own are the causes of this.</p>
                                    <p class="pt-serif">According to a new study published in the Journal of Behaviour Addictions, spending time on numerous social media platforms is not only a waste of many hours of one's day, but it can also influence a person's decision-making and encourage them to engage in "risky behaviours."</p>
                                    <div class="pr-subtitle">What are the long-term effects?</div>
                                    <p class="pt-serif">Since social media and the internet are relatively new technologies, there are very few studies outlining and establishing the long-term effects of excessive social media use. However, many new and numerous studies are beginning to find a strong correlation between heavy social media users and anxiety, depression, loneliness, self-harm, and suicidal ideation.</p>
                                    <div class="pr-subtitle">Bad for the brain</div>
                                    <p class="pt-serif">Internet and social media have the ability to capture and disperse your attention. Not only will it eventually result in a decline in cognitive performance, but it will also cause the area of the brain responsible for focusing attention to diminish.</p>
                                    <div class="pr-subtitle">Long-term heavy users experience:</div>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>An inability to multitask</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Poor performance on cognitive tests</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Disturbed sleep</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Exposure to bullying</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Changes in memory processes</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Reward pathways change to give you a ‘happy’ hormone every time you get a notification</li>
                                    </ul>
                                    <div class="pr-subtitle">Are you addicted?</div>
                                    <p class="pt-serif">The brain scans of those who are addicted to social media and the internet resemble those of those who are addicted to gambling and narcotics. In today's society, where 62% of adults say they "detest" the amount of time they spend on their phones, determining an addiction can be particularly challenging. Why does this percentage of the population not switch off their phones, given this statistic? Are they social media addicts?</p>
                                    <div class="pr-subtitle"> Signs and symptoms of addiction</div>

                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>You check social media first thing in the morning when you finish work, or any moment you have free</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Your first instinct is to reach for your phone</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Social media hinders your productivity</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>You become anxious when you cannot check your social media</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>You consistently check how your posts perform </li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>You overthink your posts and spend a considerable amount of time planning out your feed or stories</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Social media or the internet does not just take up your time but your headspace</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>You think your phone goes off when it doesn’t</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>You neglect offline hobbies</li>
                                    </ul>

                                    <div class="pr-subtitle">What to do about internet addiction?</div>
                                    <p class="pt-serif">If any of the above applies to you, or if you feel you have developed an unhealthy relationship with your phone or computer, we recommend undergoing a digital detox in the form of private residential care. We have assisted thousands of others in overcoming addiction and coping with mental illness.</p>
                                    <p class="pt-serif">To help you become the best version of yourself, we will guide you through your addiction and teach you essential coping mechanisms that you can use in your daily life.</p>
                                    <p class="pt-serif">Internet addiction, also referred to as problematic internet use, is increasingly recognised as a mental health issue. Internet addiction is typically characterised by a level of internet use that impairs relationships, causes family, work, or interpersonal problems, and negatively impacts daily functioning.</p>
                                    <p class="pt-serif">The actual cause of Internet addiction is unknown, but there are numerous prospective factors. Some researchers have compared it to other addictive disorders, including compulsive purchasing. Those who are addicted to the Internet may experience a similar "high" while using their devices as those who shop compulsively. A genetic component may also increase the likelihood that problematic Internet users will develop an addiction. Familial and social factors may also play a role, as a person may increasingly rely on virtual reality to escape negative situations in their daily lives. As one uses the Internet more frequently and experiences positive emotions and sensations as a result, he or she may become dependent on the Internet in order to feel normal or even good.</p>
                                    <p class="pt-serif">It has been demonstrated that cognitive behavioural therapy, self-help treatment groups, group therapy, and family therapy are all effective treatments for Internet addiction. Dr. Kimberly Young, who established The Center for Internet Addiction in 1995, developed CBT-IA, a specialised form of cognitive behavioural therapy for the treatment of Internet addiction.</p>
                                    <p class="pt-serif">Some individuals may also benefit from Twelve-Step programmes and social skills training. When a mental health issue such as stress, melancholy, or anxiety has led a person to seek support via the Internet, a therapist may attempt to treat the addiction by first addressing the mental health issue. Similarly, other underlying conditions are frequently uncovered through therapy, and resolving these issues can frequently aid in addiction recovery.</p>

