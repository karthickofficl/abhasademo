                                    <h3>WHEN IT COMES TO QUESTIONS ABOUT MENTAL HEALTH, COULD IT BE THAT WE HAVE THE WRONG ONES IN MIND?</h3>
                                </div>
                                <div class="fl-wrap text-block">
                                    <div class="clearfix"></div>
                                    <p class="pt-serif">Actually, there is no evidence to support that there is a direct link between self-harm and social media, as Tom Chevers pointed out in an article for the New Scientist published in August 2018. Chevers went on to say that alcohol and cannabis use have decreased during that same period, but no one is making the connection that this decrease is due to an increase in social media and screen time. The issue is both more complicated and less complicated than that. "Exactly what is causing this unhappiness is not clear," as the New Scientist puts it, "is not clear. "The root of all this misery is easier to identify than we give it credit for, and it has everything to do with the vast sea of ignorance that surrounds the subject of trauma. </p>


                                    <p class="pt-serif">Most of us picture combat veterans returning home and finding it difficult to readjust to civilian life when we think about the trauma. Another thing that comes to mind is the thought of a terrorist attack, a rape, or the victim of a rape. And this is indeed trauma; it is indeed traumatic. And if they don't get proper care, the effects can linger for quite some time. We refer to this more common form of trauma as "Big T Trauma," and its precise definition is any experience that leaves us feeling as though our bodily safety has been compromised. It's been reported that some of Abhasa's patients have suffered what's called "Big T Trauma." </p>


                                    <p class="pt-serif">However, practically all of our patients have experienced some other form of trauma. Little trauma is another name for developmental trauma. And it takes place in the first 18 years of our lives, when we are children and growing up. </p>


                                    <p class="pt-serif">Therein lies the vast expanse of confusion I mentioned earlier. The belief that "trauma only happens to other people" is widespread. This is a false assumption. As a matter of fact, we all suffer from at least some degree of trauma, particularly in the form of developmental trauma. We all have it; some of us just have more of it than others. Bessel van der Kolk, one of the world's foremost trauma specialists, labels developmental trauma a "hidden epidemic" in his book, "The Body Keeps the Score." He's right, too; it's a covert epidemic. Bessel addresses the issue of child victims of sexual, physical, and emotional abuse. Also, he locates them geographically. Please accept my gratitude, Bessel. </p>


                                    <p class="pt-serif">It doesn't take much of a stretch of the imagination to picture a youngster who has been subjected to physical, sexual, and emotional abuse. One might easily imagine them huddled together tightly, looking emaciated and "traumatized." Possibly both of their parents are addicts or alcoholics. It's possible that they went without food, water, and shelter on a regular basis while being beaten, screamed at, and yelled at. Our hearts may break for these kids because we realize that they've experienced trauma—not just any trauma, but developmental trauma, the "little t trauma." People who self-harm are not invariably the products of broken homes, despite what the reported numbers might suggest. What's going on is that our knowledge of emotional abuse and neglect is severely lacking. </p>


                                    <p class="pt-serif">There is no such thing as a deliberately abusive or neglectful parent. Well, perhaps a minuscule 0.01 percent does. Every parent I know is certain that their own approach to childrearing is the optimal one. It's conceivable that they'll also attribute their own upbringing's emphasis on the welfare of the young to the same altruistic impulse. The fact that no one is flawless is a relief, since if they were, it would just add stress to a child's life. But let’s simply look at this notion of emotional abuse and neglect so we can understand where we ourselves might have lost out and where our own developmental trauma might reside. </p>


                                    <p class="pt-serif">Was it a comfort to know that our parents were watching over us? When I say "seen," what I mean is that we were acknowledged and connected with on a personal level, not simply as a part of the family or a child. Did we grow up with a parent who really cared about us and took the time to get to know us, listen to us, and respect what we had to say? Do we get comforted when we feel sad? Did we have a parent who, if we were feeling down, would take us into their arms and rock us gently until we felt better? Or were we told to just quit moping around and get on with it? It's not cool for boys or "big girls" to show emotion.

                                        Did we feel like we could confide in our parents about things that were bothering us? If we did make such an effort, did they notice, hear, or see what we were saying? Or was it implied that we should each figure things out on our own? We are more likely to experience emotional security when we are acknowledged and comforted. How safe did we feel? Did we have a permanent place to call our own, a safe haven where we could raise our families and entertain our friends? Therefore, how does this play out in our lives as we mature? How does it appear? There are a variety of ways in which developmental trauma shows up in people, depending on factors including their personalities, their character structures, and the nature and frequency of the trauma they experienced. Some examples include <a href="https://www.abhasa.in/post-traumatic-stress-disorder-treatment-in-tamilnadu/">post-traumatic stress disorder</a> (PTSD), substance abuse, food disorders, anxiety, depression, smoking, antisocial behavior, numbness, self-harm, suicidal ideation, and suicide. These are all symptoms of traumatic experiences during childhood. </p>

                                    <p class="pt-serif">Because the absence of nurturing experiences can be just as traumatic as the presence of traumatic ones, we should all admit that we adore our offspring. Naturally, we do. Our parents loved us before we were born, too. Every parent out there loves their kid. However, the missing ingredient is that not all parents know how to be unconditionally loving to their children since not all adults have a complete picture of what children require. </p>
                                    <p class="pt-serif">When we are not treated as the magnificent human beings that we are in our entirety, we do not feel seen, calmed, protected, or secure enough of the time. Tragically, the youngster experiences emotional abuse or neglect as a result of a lack of respect and unconditional affection. </p>
                                    <p class="pt-serif">Breaking the stigma about mental health is really difficult. Education and awareness will help society understand it better. At <a href="https://www.abhasa.in/about/">Abhasa Rehab</a> we help the client and family understand more about mental health and its importance. </p>
