                                    <ul class="ul">
                                        <li class="pt-serif">• Individual and group counselling: Offer both individual and group counselling sessions to accommodate diverse preferences. Some may find solace in one-on-one sessions, while others may benefit from the shared experiences of a group setting.</li>
                                        <li class="pt-serif">• Specialised Grief Counselling: Recognise the unique challenges of grief in young adults and provide specialised counselling services that address their specific needs and concerns.</li>
                                        <li class="pt-serif">• School-Based Counselling Services: Collaborate with educational institutions to establish on-site counselling services, ensuring that young people have easy access to support within familiar environments.</li>
                                    </ul>
                                    <div class="pr-subtitle">Grief for Young Adults: A Personal Journey</div>
                                    <p class="pt-serif">Grief for young adults is a deeply personal journey marked by a complex tapestry of emotions, challenges, and self-discovery. As individuals in this stage of life encounter loss, whether through the demise of a loved one, the end of a relationship, or other significant life changes, they embark on an intricate path of navigating sorrow, introspection, and adaptation. The experience of grief for young adults is unique, often accompanied by a mixture of sadness, confusion, and a search for meaning. It becomes a transformative process that prompts them to confront the fragility of life and reassess their own identity, aspirations, and relationships. Amidst the pain, young adults may find <a href="https://www.nsft.nhs.uk/grief-and-loss/">unexpected resilience</a>, newfound strengths, and a deeper understanding of themselves. Grief, for them, is not only a process of mourning but also a journey towards self-discovery, growth, and the eventual restoration of hope and meaning in their lives.</p>
                                    <div class="pr-subtitle">Supporting Young People Through Loss: Practical Strategies</div>
                                    <ul class="ul">
                                        <li class="pt-serif">1. Open Communication:</br> Encourage open and honest conversations about the loss, creating a safe space for young people to share their feelings.</li>
                                        <li class="pt-serif">2. Provide Resources:</br> Offer information about grief resources, including support groups, articles, and books that may help them understand and cope with their emotions.</li>
                                        <li class="pt-serif">3. Normalise grieving reactions:</br> Assure young adults that there is no 'right' way to grieve, and it's normal to experience a <a href="https://www.ncbi.nlm.nih.gov/pmc/articles/PMC4119479/">range of emotions</a>.</li>
                                        <li class="pt-serif">4. Encourage professional support:</br> Highlight the benefits of seeking professional help through counselling for young people, emphasising the services available at Abhasa Rehab and Wellness.</li>

                                    </ul>
