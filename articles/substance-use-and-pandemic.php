                                    <div class="clearfix"></div>
                                    <p class="pt-serif">The COVID-19 pandemic has disrupted the lives of drug users in ways that have negatively impacted their mental health and altered their drug use behaviours, thereby increasing their risk for overdose, according to surveys and interviews with rural Illinois residents published in a new study in Addiction Science and Clinical Practice. Researchers are beginning to decipher how the COVID-19 pandemic and attempts to prevent the transmission of the virus, such as stay-at-home orders, may have contributed to this spike in mortality, including interruptions to harm reduction programmes, isolation, and deteriorating mental health.
                                    </p>
                                    <p class="pt-serif">People who use drugs and reside in rural regions may be disproportionately affected by pandemic-related changes, given that many rural communities have higher rates of opioid and methamphetamine use and fewer drug treatment and harm reduction programs. People who use drugs in rural locations may also encounter higher degrees of stigma, which may increase their chance of using drugs alone and their reluctance to seek medical attention. In a series of questionnaires and interviews with people who use drugs in rural southern Illinois, the researchers wanted to understand their experiences during the COVID-19 pandemic and how structural and community changes could affect the overdose risk of individuals.</p>
                                    <p class="pt-serif">In the period between August 2020 and May 2021, the researchers surveyed 50 people who use opioids (without a prescription) or inject substances and performed in-depth interviews with 17 participants. Similar to the overall community, drug users reported worsening economic conditions and mental health during the pandemic in a region already plagued by pervasive poverty. Due to layoffs, disruptions in their work in service industries, and fewer available positions, just 38 percent of participants felt confident that they could retain a consistent income throughout the epidemic.c.</p>
                                    <p class="pt-serif">In addition, according to the participants, the epidemic increased housing and food instability. Three-quarters of poll respondents reported feeling more worried or on edge during the epidemic, while more than half reported feeling more depressed and almost half reported feeling lonelier. <a href="https://www.abhasa.in/emotional-numbness-in-anxiety-and-depression/">Anxiety
                                            and sadness</a> are linked to increased substance use, which increases the risk of overdosing.
</p>

                                    <div class="pr-subtitle">The Connection Between the Epidemic and Substance Abuse</div>
                                    <p class="pt-serif">Compton warns against directly associating all increased drug usage with COVID-19. For instance, changes in drug availability may also be to blame for the rise in opioid-related mortality. If heroin is difficult to obtain, users may turn to fentanyl, which is far more potent. On the other hand, experts concur, based on study and clinical observation, that pandemic-related pressures, including economic stress, loneliness, and general concern about the virus, are a substantial contributor to the increase. William Stoops, Ph.D., a professor of behavioural science, psychiatry, and psychology at the University of Kentucky, explains, "There's a perfect storm of characteristics that we know enhance drug use." "As a result of increased stress and isolation, individuals make harmful decisions, such as drinking more and using drugs." (For further information on drinking behaviours during COVID-19, see January Monitor.)
</p>
                                    <p class="pt-serif">As stress grows, individuals may have fewer coping mechanisms, which, according to Owens, likely leads to an increase in substance abuse. For instance, resilience-promoting activities, such as physical activity and social contacts, have not been as accessible or safe, which can drive some individuals to begin using drugs or to use them more frequently or in higher quantities.</p>
                                    <p class="pt-serif">There are also pandemic-related practical reasons for the increase in overdoses. People who use drugs alone are more likely to die, according to Compton, because no one is present to contact 911 or deliver naloxone, an opioid reversal agent. For people living alone during the pandemic, this solitude poses a clear and present danger. And in the early stages of the epidemic, it was more difficult for people to access the medical care they required for recovery from opioid addiction since some clinics and community organisations reduced their services./p>

                                    <div class="pr-subtitle">What Psychologists Can do to Help</div>
                                    <p class="pt-serif">Psychologists are in a prime position to assist people with substance use disorders. However, the manner in which they assist their patients depends on the medicine. For the treatment of opioid use disorder, medicines such as buprenorphine are essential. Owens argues that encouraging individuals to seek medical treatment is the first step in preventing the long-term effects of opiate addiction, such as overdose. Concurrent psychological treatment can assist individuals in adhering to their medication schedule, identifying and responding in more healthful ways to the circumstances that have contributed to their opioid abuse, and treating disorders such as pain, post-traumatic stress disorder, anxiety, and depression. </p>

                                    <p class="pt-serif">Associate Professor of Pain Medicine at the Johns Hopkins University School of Medicine, Paul Christo, MD, says that psychologists should push for wider access to naloxone, a drug that can reverse the effects of a narcotic overdose and is available on prescription in some states. Christo argues that "we need a better understanding that this is something that can prevent someone from dying" all around the country. All patients of a psychologist should be routinely questioned about their usage of illegal or prescription drugs. Owens warns doctors against assuming that patients who don't have a SUD diagnosis aren't misusing substances or won't start.</p>
                                    <p class="pt-serif">She explains that people are more likely to use drugs because "stressors have continued and appropriate coping strategies have been cut off." If a patient discloses substance abuse, Owens advises physicians to show empathy and work with them to understand the role that several stressors play in driving them to use and find alternative coping mechanisms. She suggests that psychologists help their patients conduct a functional examination of the substance's significance in their lives rather than presuming that their patients desire to discontinue using it.</p>
                                    <p class="pt-serif">Owens adds that it's crucial to remember that weekly outpatient consultations might not be sufficient for many patients, especially those who have more spare time on their hands during the pandemic. Therapists in the field of psychology should work in tandem with other medical professionals to ensure their patients receive the best possible care, and they should also do everything they can to incorporate routine and consistency into their patients' lives. Take the case of a patient with a substance use disorder (SUD) whom Owens sees once a week for outpatient care but who also receives intense outpatient care from another local clinician. Owens argues that psychologists should also consider recommending online support groups to their patients. She argues that there is no simple solution for treating patients with addiction. Tailoring care to the individual needs of each patient is paramount.</p>
