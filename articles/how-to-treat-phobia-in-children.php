                                    <p class="pt-serif"><b>Cognitive-Behavioral Therapy (CBT): </b> The strategies described are aimed at assisting the children to become aware of distorted thought patterns and to change them for more positive and realistic ones. </p>
                                    <p class="pt-serif"><b>Relaxation Techniques: </b> Therefore, by using implemented strategies of teaching the child deep breathing, visualization, or progressive muscle relaxation, the child’s anxiety level and fear can be minimized. </p>
