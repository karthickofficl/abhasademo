                                    <p class="pt-serif"><b>Situational Phobias: </b> Phobias, these may include; Chronophobia: these are mostly the worries involving the doctor. Aerophobia: this is mostly a worry of having to fly; Agoraphobia: this is fear of being in closed or busy places. </p>
