                                    <p class="pt-serif">Fear and phobias are believed to originate from how the brain processes threats. When a child is exposed to a stressor that leads to fear, part of the brain known as the amygdala- which is in the shape of an almond, is stimulated. This leads to the secretion of stress hormones like adrenaline and cortisol, to prepare the body for an instant “fight or flight” response. </p>
