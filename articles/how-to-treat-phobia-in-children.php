                                    <p class="pt-serif">For children, play therapy is considered one of the most effective ways of delivering phobias and anxiety treatments. Carried out with peers and using particular and interesting games appropriate to children’s age, this strategy is based on confrontation with objects or situations that caused fear with the use of peers. Some of the most effective play therapy techniques include: The following are the recommendations that have been identified in the use of play therapy: </p>
