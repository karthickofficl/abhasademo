                                    <p class="pt-serif">The phenomenon of separation anxiety is widespread with the age ranging between the half-year and three years and affecting both children and parents or other close relatives. It includes severe stress whenever a child becomes separated from a major caregiver and results in crying overtiredness, clinging manifestations in the children and worry, guilt, and concentration issues in parents. To deal with such anxiety, the children should be gradually exposed to the triggers, their schedule should remain as unchangeable as possible, comfort items should be provided, and positive reinforcement should be used with the children; the parents too can do some self-care, be open to the children, seek professional help, and practice mindfulness. It is necessary to establish a positive context with teachers, caregivers, and relatives, and to inform the co-parents about everything. In this way, the feelings the children and the parents experience during this phase can be managed, and the situation does not have to become a constant source of stress for the family.</p>
