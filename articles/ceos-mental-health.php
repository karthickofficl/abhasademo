                                <div class="section-title fl-wrap">
                                    <h3>THE TRUTH ABOUT CEOS&#39; MENTAL HEALTH</h3>
                                </div>
                                <div class="fl-wrap text-block">
                                    <div class="clearfix"></div>
                                    <p class="pt-serif">
                                        COVID-19 imposed an extra set of pressures on CEOs, who were forced to operate their companies under unprecedented and difficult conditions. Strong leadership was pushed to its limits, and now the rate of burnout among chief executives is increasing. The fatigue of the past two years is beginning to set in. The International Institute for Management Development (IMD) reveals that throughout the epidemic, CEOs failed to manage their personal self-care. "CEOs were unprepared for the increased levels of VUCA (volatility, uncertainty, complexity, and ambiguity) brought on by the epidemic, and well-planned corporate strategies could not be depended upon in such circumstances.
                                    </p>


                                    <p class="pt-serif">
                                        " Mr. Ramly, CEO of Abhasa Luxury Rehab, added, "I liken it to when airlines instruct passengers to put on their own oxygen masks before assisting others during an emergency. And what we've observed with CEOs is that in many situations, they don't wear masks." During bad times, CEOs have been focused on keeping everyone else afloat but have not been prepared to accept assistance for themselves. Many CEOs don't realize they have a problem until they reach the point of burnout. Six out of ten CEOs and board members who suffered mental illness during the pandemic opted for harmful coping techniques, such as <a href="https://www.abhasa.in/drugs-alcohol-and-mental-health/">alcohol consumption</a> and <a href="https://www.abhasa.in/gambling-addiction-treatment/">gambling,</a> rather than seeking professional assistance.
                                    </p>

                                    <div class="pr-subtitle">The Stigma of Mental Health</div>
                                    <p class="pt-serif">
                                        Another important factor stopping CEOs from seeking assistance is the stigmatization of mental health. In corporate circles, admitting a mental illness might feel embarrassing and equivalent to a personal shortcoming. CEOs are under intense pressure to be "superhuman." Many at the top view the admission of any form of vulnerability as a sign of weakness. This pressure is typically felt both in the workplace and at home with the family. The riches, prestige, and success of the family are at stake.
                                    </p>

                                    <div class="pr-subtitle">CEO Pathologies and Personalities</div>
                                    <p class="pt-serif">
                                        Many of the world's wealthy and renowned entrepreneurs have battled mental illness throughout their lives. People who reach the pinnacle of their field typically do so as a result of their psychological challenges, not in spite of them. Aaron Swartz, co-founder of Reddit, committed suicide in 2013; he had a history of struggling with depression. Elon Musk has been candid about his mental health, tweeting about his struggle with melancholy. Musk tweeted, "Reality is characterized by incredible highs, devastating lows, and constant stress.
                                    </p>
                                    <p class="pt-serif">"Steve Jobs, who died of pancreatic cancer in 2011, had (but was never officially diagnosed with) <a href="https://www.abhasa.in/ocd-treatment-center-in-mumbai/">obsessive-compulsive
                                            personality disorder</a> Obsessives: The Compulsive Energy That Built a Nation" contained a biopic on him, and he struggled 'on and off from anorexia, which was believed to derive from his painful adoption experience. Early adversity was a prevalent element among the idols analyzed by Kendall. Observing that compulsive behavior is part of the psychopath continuum, he concluded, "Sometimes a guy climbs to the top precisely because he is slightly insane." Dr. Michael A. Freeman et al. surveyed 242 entrepreneurs and 93 demographically comparable comparison participants in 2015. They uncovered:
                                    </p>


                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>
                                            72% of business owners self-reported having mental health issues (significantly higher than the comparison group).
                                        </li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>
                                            49% of entrepreneurs have one or more lifelong mental health issues.
                                        </li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>
                                            32% of entrepreneurs have had two or more mental health disorders in their lifetime.
                                        </li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>
                                            18% of entrepreneurs had had three or more lifetime mental health issues.
                                        </li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>
                                            23% of entrepreneurs were asymptomatic members of families with several symptoms.
                                        </li>

                                    </ul>

                                    <p class="pt-serif">
                                        Compared to the control group, survey respondents were considerably more likely to report:
                                    </p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>(30% versus 15% in the control group) depression</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>ADHD (29 percent against 5 percent in the control group)</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Substance use disorders (12 % versus 4 % in the control
                                            group).</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Diagnosis of bipolar disorder (11% versus 1% in the
                                            control group)</li>
                                    </ul>


                                    <p class="pt-serif">
                                        Personality is a component of the portrait. CEOs are shown to have a high prevalence of narcissism. Characteristics of narcissistic personalities include an inflated sense of self-importance, an obsessive need for praise, a drive for power, a lack of empathy, and the ability to manipulate others with ease. Narcissism is frequently accompanied by other mental diseases, such as borderline, histrionic, and antisocial personality disorders. Narcissists are especially motivated to advance rapidly into positions of authority. A narcissistic CEO enjoys being in the spotlight, is confident and charismatic, and can "boldly explain the company's goal."
                                    </p>

                                    <div class="pr-subtitle">The Strain to be Superhuman</div>
                                    <p class="pt-serif">
                                        "Keep calm and carry on" is a strain that comes with running a business, particularly when negotiating obstacles such as the pandemic. The rest of the organization relies on the CEO to offer a steady course. Numerous CEOs disregard the warning symptoms of mental health concerns, such as worry, insomnia, and exhaustion. They resort to unhealthy quick fixes, such as increased alcohol intake, substance misuse, gambling, and self-medication. They believe that concealing their personal troubles is crucial. Families are a common source of pressure to be a super achiever.
                                    </p>
                                    <p class="pt-serif">
                                        In an interview on anxiety for the men's magazine Esquire, IMAX CEO Richard Gelfond recalls his upbringing: "I was raised in the 1950s, and my father was a gruff, uncommunicative man. The message sent to us was "be resilient and persevere." Gelfond, having endured difficult times, now disagrees with this strategy.
                                    </p>
                                    <p class="pt-serif">
                                        CEOs are highly successful at managing people and all elements of their businesses; therefore, they believe they should be able to manage and control their mental health.
                                    </p>

                                    <div class="pr-subtitle">Loneliness, it&#39;s Difficult to Speak.</div>
                                    <p class="pt-serif">
                                        It is difficult for CEOs to rely on others when they have an image of being superhuman'. It might be challenging for leaders to find someone they can trust with their weaknesses. Leadership isolation is a well-known phenomenon. Prior to the epidemic, 30 percent of executives felt lonely, according to research. Another survey indicated that as many as 50 percent of CEOs experience feelings of <a href="https://www.abhasa.in/social-isolation-and-mental-health-issues/">isolation</a> in their positions. Examining the interactions between leadership and loneliness, clinical psychologist Ami Rokach determined that leaders (educational, state, business, and organizational) experience stress, alienation, loneliness, and emotional turmoil, which can lead to health issues and negatively impact social and familial relationships.
                                    </p>
