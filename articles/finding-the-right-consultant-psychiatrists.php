                                    <div class="clearfix"></div>
                                    <p class="pt-serif">Psychological and emotional difficulties often cause addiction. As Irish addiction specialists, we understand the link between depression, trauma, and anxiety.</p>
                                    <div class="pr-subtitle">Why See a Psychiatrist for Addiction?</div>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>A psychiatrist can consider the bigger picture of a patient's life and the factors that contribute to their recovery, such as employment, finances, housing, family, and fundamental relationships.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Can evaluate and advise on medication above and beyond that of a GP.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Psychiatrists often play a role during detox medication, length etc.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Can help identify underlying problems (dual diagnosis) which affect 60-70% of addicts</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Psychiatrists can help plan therapy and treatment more effectively</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Psychiatrists can often accurately measure improvements and next steps longer-term post-rehab</li>
                                    </ul>
                                    <div class="pr-subtitle"> How to Find and Choose the Best Psychiatrist?</div>
                                    <p class="pt-serif">If you've been experiencing symptoms of depression, anxiety, and tension, and you believe medication could be beneficial, you may want to consult a psychiatrist. If you do not know where to begin, you are not alone. Here are some methods to assist you on your journey.</p>
                                    <p class="pt-serif">There are a number of resources available online, in your community, and through your primary care physician that can guide you in the correct direction.</p>
                                    <p class="pt-serif">Before you begin, you must emphasise that psychiatrists are distinct from other mental health professionals. In contrast to psychologists and other therapists, psychiatrists are medical physicians; therefore, during your appointment, they will evaluate for any co-occurring medical diagnoses before prescribing and monitoring psychotropic medication.</p>

                                    <div class="pr-subtitle">What is a psychiatrist?</div>
                                    <p class="pt-serif">In addition to psychosocial approaches, psychiatrists typically approach and treat mental health issues from a biological perspective.</p>
                                    <p class="pt-serif">Psychiatrists must possess one of two medical degrees: an MD (doctor of medicine) or a DO (doctor of osteopathic medicine), in addition to advanced qualifications from a four-year residency and a psychiatry specialty.</p>
                                    <p class="pt-serif">These physicians are educated in a variety of fields, including genetics, neurology, biochemistry, and psychology, among others.</p>
                                    <p class="pt-serif">Among the psychiatric subspecialties are:</p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>addiction medicine</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>pain medicine</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>geriatric psychiatry</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>child and adolescent psychiatry</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>forensic psychiatry</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>sleep medicine</li>
                                    </ul>

                                    <div class="pr-subtitle">Psychiatrists vs. psychologists</div>
                                    <p class="pt-serif">Psychiatrists typically approach and treat mental health issues from both a biological and psychosocial perspective.</p>
                                    <p class="pt-serif">Psychiatrists must possess one of two medical degrees: an MD (doctor of medicine) or a DO (doctor of osteopathic medicine), in addition to advanced qualifications from a 4-year residency and a psychiatry specialty.</p>
                                    <p class="pt-serif">Among others, these physicians are trained in genetics, neurology, biochemistry, and psychology, among others.</p>
                                    <p class="pt-serif">How to find a child and family psychiatrist</p>
                                    <p class="pt-serif">In addition to specialising in general psychiatry, child and adolescent psychiatrists also receive specialised training in the mental health requirements of children and adolescents.</p>
                                    <p class="pt-serif">They can also prescribe medication and provide psychotherapy in addition to diagnosing mental health conditions. If it is difficult to find a child and adolescent psychiatrist in your area, you might consider a child psychologist or a marriage and family therapist, as they are also trained to deal with children and families. This initial phase may aid in the identification of additional treatment sources for medication management.</p>
                                    <div class="pr-subtitle">How to find a psychiatrist for depression</div>
                                    <p class="pt-serif">Experts in mental health specialise in the treatment of specific conditions as well as the form of therapy they administer. If you are pursuing treatment for depression, you should inquire about the techniques used. Among the most prevalent varieties of psychotherapy for depression are:</p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Behavioral therapy</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>CBT</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>dialectical behavior therapy</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>interpersonal therapy</li>
                                    </ul>
