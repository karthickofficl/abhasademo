                                    <h3>HERE IS HOW AND WHAT TO DO IF MOM'S DEPRESSION IS IMPACTING HER CHILDREN.</h3>
                                </div>
                                <div class="fl-wrap text-block">
                                    <div class="clearfix"></div>
                                    <p class="pt-serif">Depression in one parent, usually the mother, can change the
                                        dynamics of the whole family because it affects not only the
                                        parent but also the children. That&#39;s why it&#39;s important to notice
                                        the signs and deal with the problem, finding the best ways to
                                        get help and get better. Children are at risk if their mothers are
                                        depressed. For example, children whose parents are
                                        depressed are two to four times more likely to be depressed
                                        themselves before they turn 18. The mother&#39;s condition can
                                        make it harder for them to grow up socially, emotionally, and
                                        mentally. But there is good news: most of the damage that can
                                        be done by maternal depression can be fixed if the mom gets
                                        professional help.</p>

                                    <div class="pr-subtitle">What the signs of a depressed parent are and why they are often ignored </div>
                                    <p class="pt-serif"> When a mother has postpartum depression, she might feel like
                                        she doesn&#39;t love her baby enough or that she can&#39;t connect with
                                        her child. A depressed mom will feel sad and tired all the time,
                                        much more than the tiredness most parents feel. She may not
                                        be able to get up and do even the simplest things or feel happy.</p>
                                    <p class="pt-serif">Some other signs a mom might have been:</p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>She lost interest in normal things, even things she did with her child.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i> A lot of crying, feeling hopeless</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Getting upset and angry</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>lack of energy and trouble sleeping</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Changes in hunger or weight</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>It&#39;s hard for them to make decisions, focus, and remember
                                            things.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Thoughts of suicide or even tries</li>
                                    </ul>
                                    <p class="pt-serif">This kind of depression can go untreated because the family
                                        may be focused on the children instead of the parent. Even if a
                                        mom knows she is depressed, she may put herself last and
                                        focus on taking care of her kids. Depression, on the other hand,
                                        won&#39;t go away by itself. It needs to be treated, and this is
                                        especially true when families are involved.</p>

                                    <div class="pr-subtitle">How does depression in a parent affect their children?</div>
                                    <p class="pt-serif">At different stages of development, children need different
                                        things from their parents. Sometimes they need reassurance
                                        and affection, and other times they need limits and patience.
                                        Here&#39;s what happens by age.</p>
                                    <div class="pr-subtitle">Babies</div>
                                    <p class="pt-serif">Up to 20% of all new mothers are depressed after giving birth
                                        (PPD). When a mother has this kind of depression, she can&#39;t
                                        connect with her baby. That means they are less likely to talk
                                        to, play with, and show affection for their baby, all of which
                                        would strengthen their bond. One study found that moms with
                                        postpartum depression are less likely to talk to their babies or
                                        stick to routines, and they are more likely to stop breastfeeding
                                        their babies sooner.</p>
                                    <p class="pt-serif">Babies can tell, and this makes them nervous, so they cry more
                                        or act distant. Researchers have found that babies whose
                                        moms have postpartum depression gain weight more slowly
                                        than babies whose moms don&#39;t have depression. This means
                                        that the babies&#39; overall health is affected.</p>
                                    <p class="pt-serif">Besides the symptoms of depression, a mother with postpartum
                                        depression may also:</p>

                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Fear that she is not a good mom</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Pull away from her husband, her family, and her friends.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Have a hard time getting close to the baby. Think about hurting herself or the baby.</li>

                                    </ul>
                                    <div class="pr-subtitle"> what impact does depression have on children</div>
                                    <p class="pt-serif">Young children need a lot of energy because they learn by
                                        playing and interacting with their parents in other ways. When a
                                        mom is depressed, she may not have the energy to keep up
                                        with her child and find it easy to get angry with her. When a
                                        mother is depressed, she is less likely to play make-believe
                                        with her child, which helps keep the child from getting into
                                        trouble. Â The child may also have trouble with language, which
                                        could make them less ready for school.</p>

                                    <div class="pr-subtitle">School-aged kids</div>
                                    <p class="pt-serif">Children whose mothers have <a href="https://www.abhasa.in/depression-rehabilitation-center-in-mumbai/">depression</a> may grow up faster
                                        than other kids, but that doesn&#39;t mean they aren&#39;t hurt by their
                                        mother&#39;s illnesses. They might think less of themselves and fall
                                        behind in school and in social life. One long-term study found
                                        that children whose mothers had chronic, severe depression
                                        were most likely to have suicidal thoughts at age 16. In fact,
                                        they were more than twice as likely to think about killing
                                        themselves as children in the study whose mothers had few
                                        signs of depression.</p>

                                    <div class="pr-subtitle">How do I protect my kids?</div>
                                    <p class="pt-serif">Asking the question is the first important step, and the answer
                                        is always to get help from a professional. Sometimes
                                        medication is part of the solution, sometimes therapy is, and
                                        most of the time it&#39;s a mix of both. It&#39;s also helpful to ask your
                                        spouse, other family members, and other adults who see your
                                        kids often to be involved and helpful while the mom gets
                                        professional help. Also, if the kids are old enough, they need to
                                        know that their mom&#39;s depression is not because of anything
                                        they did, but because of a medical condition that will be treated.
                                        In addition to making the child feel better, this opens the door
                                        for an honest conversation in which the child can talk about
                                        their worries and ask questions. <a href="https://www.abhasa.in/">women's depression treatment
                                        centre</a> like Abhasa rehabilitation and wellness retreat can help.</p>
