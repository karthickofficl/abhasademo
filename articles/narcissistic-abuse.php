                                    <h3>HOW THE METOO MOVEMENT REVEALED THE HIDDEN WORLD OF NARCISSISTIC ABUSE</h3>
                                </div>
                                <div class="fl-wrap text-block">
                                    <div class="clearfix"></div>
                                    <p class="pt-serif">In 2017, #MeToo shook the world awake. A single hashtag unleashed Pandora's box of sexual and emotional abuse stories and shed blazing light on a murky environment. The #MeToo movement gave tens of thousands of abuse survivors a voice and brought other abuse-related topics into the public discourse. The #MeToo era is certainly a watershed moment in history.</p>
                                    <p class="pt-serif">This blog examines how #MeToo has revealed a previously inaccessible world of narcissistic behaviour among the wealthy and powerful.</p>

                                    <div class="pr-subtitle">How #MeToo began</div>
                                    <p class="pt-serif">On October 15, 2017, MeToo went viral on social media when actress Alyssa Milano encouraged women and men who had been sexually abused to share their experiences using the hashtag MeToo. Her objective was to illustrate the frequency of sexual harassment.</p>
                                    <p class="pt-serif">It succeeded. #MeToo was retweeted 23 million times in 85 countries in a few weeks!</p>
                                    <p class="pt-serif">The game had ended. Harvey Weinstein, a Hollywood mogul and infamous sexual abuser, was not the first to be revealed, but his case was likely the one that shook the globe, will be remembered, and continues to propel the #MeToo movement forward. Harvey Weinstein was the first prominent man to be legally punished for his behaviour as a result of #MeToo, 35 months after the movement went viral.</p>
                                    <p class="pt-serif">Despite accumulating evidence of Weinstein's misbehaviour, Manhattan's district attorney, Cyrus Vance, had refused to file charges against him for years. However, #MeToo increased the pressure.</p>
                                    <p class="pt-serif">Weinstein was eventually found guilty of two sex crimes, including rape in the third degree, and sentenced to 23 years in jail. This landmark case acted as a cautionary tale regarding the potential repercussions of unlawful, immoral, and abusive behaviour, particularly among the formerly untouchable wealthy and powerful.</p>
                                    <p class="pt-serif">The "me too" campaign began much before the now-famous tweet. "Me too" was coined in 2006 by activist Tarana Burke. As a youth worker, Burke met a girl who recounted her experience with sexual violence and abuse. She quickly heard many more.</p>
                                    <p class="pt-serif">As a survivor of sexual abuse, Burke was able to relate to these experiences. She knew that many girls from marginalised areas were enduring and surviving abuse without access to the necessary resources, safe spaces, and support. Inspiringly, she began developing a community of champions committed to ending sexual abuse and assisting victims.</p>
                                    <p class="pt-serif">This grassroots movement was transformed into a global organisation by #MeToo.</p>

                                    <div class="pr-subtitle">How has #MeToo exposed narcissism?</div>
                                    <p class="pt-serif">The #MeToo movement emboldened women and gave them the confidence to come up. The Weinstein case encouraged others to relate their personal experiences. On social media, accounts of abusive relationships with prominent individuals in positions of power exploded.</p>
                                    <p class="pt-serif">The social acceptance of flagrant, repressive, and controlling behaviour has shrunk as a result of #MeToo. Once inaccessible, the realm of narcissism has been uncovered.</p>
                                    <p class="pt-serif">The prevalence of narcissism was reaffirmed by the avalanche of apologies from abusers as information about assaults entered the public realm. One writer observed, "The problem has always been that these individuals believed they were so entitled that they could behave however they pleased. These apologies are self-indulgent me-apologies; they are all about the offender and not the victims.</p>

                                    <div class="pr-subtitle">So, what is narcissism, and why did it take #MeToo to expose it?</div>
                                    <p class="pt-serif">When you understand narcissism, you will also understand why it is so difficult for people to speak out against it.</p>
                                    <p class="pt-serif">Sam Vaknin, professor of psychology, expert on narcissism, and author of Malignant Self Love: Narcissism Revisited, describes narcissists as “manipulative, exploitative, dangerous, subtle, and pernicious.” Narcissists are domineering, dictatorial, belittling, uncaring, and dismissive. They are abusive emotionally and, in some cases, also sexually.</p>
                                    <p class="pt-serif">This behaviour isn’t apparent at the beginning of a relationship. Vaknin reminds us that narcissists are not blood-stained killers and can appear on the exterior as completely normal, especially in the early days of a relationship.</p>
                                    <p class="pt-serif">However, as Vaknin notes, a persistent claim by the victims of narcissistic abuse is that they lose all sense of themselves. “They lose voluntary control of their actions, choices, and decisions.” The narcissist starts by brainwashing and grooming, or, as Vaknin describes it, "love-bombing." 4 Then “the narcissist engenders in his victim a dissociative state, akin to a hypnotic trance.”</p>
                                    <p class="pt-serif">According to Vaknin, the victims of narcissistic abuse present a clinical picture substantially different from victims of other, more pinpointed, and goal-oriented types of abuse. “They are more depressed and anxious, disoriented, aggressive (defiant reactance), dissociative, and trapped or hopeless.”</p>
                                    <p class="pt-serif">With this understanding, it is easy to see how victims of narcissistic abuse become incapable of reporting abuse or enacting fight or flight.</p>

                                    <div class="pr-subtitle">The barriers to leaving a narcissistic relationship</div>
                                    <p class="pt-serif">There are various obstacles for a victim to leave an abusive relationship, including psychological, emotional, financial, and physical threats. The victim may experience dread, isolation, and fatigue as a result of the abuse. They may feel embarrassed or unstable emotionally. Victims of narcissistic abuse experience cognitive, behavioural, and emotional symptoms over time. They are unable to evaluate their own position and may become confused.</p>
                                    <p class="pt-serif">A narcissist is an expert at destroying self-esteem and confidence. In her book Stalking the Soul, Dr. Marie France Hirigoyen discusses the identity loss that victims of emotional abuse experience. Hirigoyen compares mental torture to physical assault by describing it as a "virtual murder of the soul."</p>
                                    <p class="pt-serif">Those with a high-profile, public-facing portion of their lives find it particularly challenging to escape such a suffocating grip.</p>
                                    <div class="pr-subtitle">Why is narcissism so prevalent in circles of wealth and power?</div>
                                    <p class="pt-serif">Numerous studies indicate that affluence and higher social class are connected with elevated entitlement and narcissism.</p>
                                    <p class="pt-serif">Paul K. Piff, who got his Ph.D. at UC Berkeley and is now an Assistant Professor of Psychology and Socioeconomic Behaviour at UC Irvine, discovered a direct correlation between social status and fundamental characteristics of personality after years of research.</p>
                                    <p class="pt-serif">Piff and his co-workers examined the impact of affluence on social behaviour. Their findings indicate that wealthy people of high social class are more likely to engage in immoral behaviour and give less to charity. Pfiff explains, "The less scary the world is to you, the more money and prestige you have."</p>
                                    <p class="pt-serif">The prevalence of narcissism and entitlement is greater among those with a higher socioeconomic status.</p>
                                    <div class="pr-subtitle">MeToo and narcissistic abuse</div>
                                    <p class="pt-serif">Statistics involving narcissistic abuse victims are grossly underreported. People do not disclose emotional abuse as frequently as physical abuse, and victims of mental abuse frequently blame themselves.</p>
                                    <p class="pt-serif">MeToo has generated new commotion. The movement provides victims of abuse with hope and a sense of community, and it has given fresh credence to the possibility of change.</p>
                                    <p class="pt-serif">Regardless of how long you've been in a relationship with a narcissist, whether he or she is your boss, friend, or spouse, the first step is to recognise that there is a problem and that you are not to blame.</p>
                                    <p class="pt-serif">The next stage is to acquire the proper assistance, beginning with an exit strategy. It is crucial that you make space for healing and distance yourself from your abuser. Enlist the assistance of trusted relatives and friends during this endeavour</p>
                                    <p class="pt-serif">Therapy is recommended because it will assist you in processing what has occurred and recovering from the trauma. It is essential that you select a treatment facility or therapist with experience treating victims of narcissistic abuse due to the severity and complexity of the trauma.</p>
