                                <div class="fl-wrap text-block">
                                    <div class="clearfix"></div>
                                    <p class="pt-serif">In your recovery process and in your battle to begin recovery and self-healing, there comes a time when you are not necessarily sad, but you are numb to the good feelings. This is known as <a href="../depression-rehabilitation-center"><b>emotional numbness.</b></a> While physical numbness makes sense due to certain physiological conditions. However, emotional numbness is a confusing and very distressing state of mind. We also lose the ability to express our emotions. This happens amongst those with depression, severe stress, anxiety, PTSD. This can happen to anyone at any moment of their life. It can be an early signal of upcoming depression. Some symptoms can be losing interest in things you used to enjoy, forcing yourself to stay happy in social gatherings, etc.</p>
                                    <p class="pt-serif">It is now important to make a plan wherein you can come out of this numbness.</p>
                                    <p class="pt-serif">Recovery begins with a person looking better to other people and talking more and being responsive. It is very difficult to start this process but it is not too difficult to sustain it once you start it. For guidance get advices from the best Therapist.</p>



                                    <h3 class="pr-subtitle pt-serif-h3">“Communication with the Self” àSelf-talk àSelf-fulfilment</h3>
                                    <p class="pt-serif">Communication with the self involves many things varying from how you look at yourself, how you introduce yourself to others, how you talk to yourself when you are not feeling okay, how you react within yourself when something goes wrong. Here “Self-Talk” is a major tool to get back to your life again. You need to sit with yourself and ask what it that is bothering you as done in top wellness centres in India. You can be creative in your self-talk:</p>




                                    <ol class="ol">


                                        <li class="pt-serif">Dialogue with your self </li>
                                        <ul class="ul">
                                            <li>Have a Q&A with yourself.</li>
                                            <li>You can ask yourself questions like- is this how I want to be for the rest of my life?</li>
                                            <li>Do I want to define myself as a weak person?</li>
                                            <li>Do I need to be described as a negative influence in others’ lives?</li>
                                        </ul>
                                        <p class="pt-serif">If the answers to these questions are NO, then you will find the determination to start again and feel okay once again.</p>

                                        <li class="pt-serif">Calm the inner child in you
                                            At times we grow numb because we fall deaf to the demands of our inner child which wants to give life another chance. Just like a crawling baby learns to walk by stumbling here and there, your inner child is stumbling to look for happiness and this is where you need to support him/her by saying soothing words.</li>

                                        <li class="pt-serif">Don’t Abandon Yourself
                                            “I am with you; I am not going to abandon you. When you feel weak, I will strengthen you.”
                                            This is what you need to convey to yourself.
                                            Sit in silence and just close your eyes and think of these affirmations.</li>
                                    </ol>
