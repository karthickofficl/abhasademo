                                    <h3>HOW PTSD CAN DESTROY A RELATIONSHIP, AND HOW TREATMENT CAN HELP</h3>
                                </div>
                                <div class="fl-wrap text-block">
                                    <div class="clearfix"></div>
                                    <p class="pt-serif">Some people develop a problem that impacts their social life and interpersonal interactions after enduring a traumatic incident. If untreated,  <a href="https://www.abhasa.in/post-traumatic-stress-disorder-treatment-in-tamilnadu/">post-traumatic stress disorder</a> (PTSD)
                                        can destroy any relationship, regardless of how strong it was before the disease. A condition characterised by the ancient Greeks as "re-entering war in one's sleep" and by World War I soldiers as "shell shock." Post-traumatic stress disorder has existed for centuries, but it has not been fully understood until the last several decades. In a marriage or relationship, it is of the utmost importance for those with PTSD or loved ones with PTSD to understand the symptoms of the disorder and how it impacts the relationship. A therapeutic approach is frequently effective in enhancing the quality of life for individuals with PTSD and their families. If PTSD persists for three months or more, it is typically called chronic. It can have a delayed beginning, with symptoms appearing months or years after a traumatic experience, such as an abusive relationship. There have been cases of PTSD suddenly manifesting in WWII veterans decades after the fight. In addition, it may manifest a number of symptoms. Recognising the condition's symptoms is the first step in treating it.</p>

                                    <div class="pr-subtitle">What is PTSD and how is it diagnosed?</div>
                                    <p class="pt-serif">A person with post-traumatic stress disorder has previously experienced a traumatic event and continues to experience it in a variety of ways, including:</p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>	Persistent and upsetting recollections and reflections of the traumatic experience. </li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i> recurring nightmares of the event</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i> Anxiety occurs when there are internal or external clues that symbolise or resemble the event or a part of it.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>	Such stimuli elicit intense physiological reactions.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i> Experiencing the incident as though it were occurring once more. </li>

                                    </ul>
                                    <p class="pt-serif">Something as trivial as a phrase, an idea, a sound, or a picture that connects to a little or peripheral aspect of the traumatic experience can provoke a physiological response, possibly even flashbacks, in a person with PTSD. This is because the memory of the experience is fractured and stored in the limbic system, the portion of the brain responsible for the "fight-or-flight" response and other first survival-related behavioural responses. If your loved one suffers from PTSD, he or she may also have difficulty sleeping (falling asleep or staying asleep), focusing, and displaying anger or irritation. They may also become hypervigilant and more readily startled than the average person. Symptoms can also differ according to the type of trauma. For instance, women who have been sexually victimised may experience a sense of filthiness.</p>

                                    <div class="pr-subtitle">What types of traumatic events trigger PTSD?</div>

                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>War and conflict</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Torture</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Sexual, physical, or psychological abuse (including childhood abuse)</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Sexual or physical assault</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Serious accidents</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Natural disasters</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Losing a loved one (including losing a child at birth)</li>
                                    </ul>

                                    <p class="pt-serif">PTSD can be triggered by any experience that significantly disturbs the individual, sending them into the "fight or flight" phase of heightened awareness. However, not everyone who has suffered trauma will acquire the disease; your genetic predisposition and life experiences may also play a role. Approximately 30% of those who experience the same traumatic event will not acquire PTSD.</p>

                                    <div class="pr-subtitle">How is PTSD affecting your relationship?</div>
                                    <p class="pt-serif">PTSD can affect all aspects of a person's life, including their employment and interpersonal connections. It is not uncommon for someone with PTSD to engage in major avoidance—of thoughts and feelings—and talk about the trauma with their loved ones, as well as avoid activities, places, and people who may remind them of the experience. A person with PTSD may appear or feel emotionally distant and have a lessened desire to engage in social or sexual activities. It may appear that they are incapable of feeling love. PTSD may cause irritability and agitation in a subset of patients. Some may not be able to regain the intimacy they had with loved ones before the traumatic event. Others may have trust concerns and be upset. In addition to affecting their partner's quality of sleep, PTSD sufferers' sleeping difficulties may also impair their partner's quality of sleep.</p>
                                    <div class="pr-subtitle">How can you help a loved one suffering from PTSD?</div>
                                    <p class="pt-serif">The most important thing you can do to assist a loved one is to locate a professional who can provide assistance. In addition to educating yourself on the subject, you can also:</p>

                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Listening when they wish to speak without forcing them to speak.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Recognising that the individual may need to revisit the painful incident several times</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Encouraging them to participate in enjoyable social activities</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Managing your own stress: The more you can assist others, the calmer you are.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Accepting PTSD might be difficult, but there is assistance available.</li>

                                    </ul>
