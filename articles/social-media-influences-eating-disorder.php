                                    <h3>Eating Disorders And The Influence Of Social Media</h3>
                                </div>
                                <div class="fl-wrap text-block">
                                    <div class="clearfix"></div>
                                    <p class="pt-serif">Numerous studies have examined the correlation between the media's idealized depiction of a slender female and a muscular male body type and an increasing preoccupation with body size, as well as harmful and even deadly weight-loss procedures engaged in by individuals in pursuit of the "perfect body." The number of people seeking help for issues related to food and body image has risen since 1960. Simultaneously, there is clear evidence that the media's portrayal of the "perfect or ideal figure" for men and women has shifted significantly. The societal ideal of a woman's body has changed over time, becoming considerably slimmer (70 per cent of photographs from publications published at the end of that time period depict an underweight model) whereas the ideal of a man's physique has changed, becoming stronger and more muscular.</p>

                                    <p class="pt-serif">The growing trend in the prevalence of eating disorders has unfortunately persisted in the previous 20 years, with at least 9% of the global population being affected. During this time span, the popularity of social media has skyrocketed. Social media apps and sites like Facebook, Instagram, Snapchat, TikTok, and Pinterest flood your mobile device with content at all hours of the day and night. The device that most teens and young people rely on is to spend up to seven hours each day on social networking. While everyone at any age is susceptible to developing an eating disorder, the typical onset is between the ages of 12 and 25.</p>

                                    <p class="pt-serif">Although women and girls have traditionally been at greater risk for developing them, men and boys are experiencing a steady increase in their incidence. Using a social networking platform is a wonderful way to stay in touch with loved ones, learn about current events, and commemorate the milestones of everyday life. One of the greatest drawbacks, however, is the devastating effect it can have on a person's psychological and emotional well-being. The material presented on social media platforms can have a considerable impact on an individual's thoughts, feelings, and behaviours, just as it does in other areas of the media (newspapers, magazines, television).</p>

                                    <p class="pt-serif">A person's attitude toward themselves, diet, and physical activity can all be affected by how they feel about themselves and how they view their bodies. Daily, young people face the temptation to conform to unrealistic standards of beauty and behaviour on social media, which can lead to an eating disorder or other mental health problems like low self-esteem, low self-confidence, sadness, or anxiety if care is not taken.</p>

                                    <div class="pr-subtitle">A Common Question: What Exactly is an Eating Disorder?</div>
                                    <p class="pt-serif">Food and the regulation of when, how much, and what is eaten are employed as coping mechanisms in people with an eating disorder, a serious mental health problem. They can hinder a person's physical, mental, and social functioning. Eating disorders are "behavioural problems characterized by significant and persistent disturbance in eating behaviours and related disturbing thoughts and emotions," as defined by the American Psychiatric Association (APA). Mental health problems such as depression, anxiety, self-harm, borderline personality disorder (BPD), obsessive-compulsive disorder (OCD), and substance use disorders frequently occur together.</p>
                                    <p class="pt-serif">There are three main types of eating disorders:</p>
                                    <p class="pt-serif"><b>Anorexia Nervosa: </b>Extreme thinness due to self-starvation is a
                                        hallmark of the eating illness known as <a href="https://www.abhasa.in/anorexia-vs-bulimia/">anorexia nervosa.</a> A common
                                        symptom of this illness is an obsession with one&#39;s weight, one&#39;s caloric
                                        intake, and one&#39;s physical activity levels due to a skewed self-perception
                                        as &quot;fat&quot; or &quot;overweight.&quot; They will resort to extreme measures, like
                                        starvation, self-induced vomiting, the use of laxatives, and punishing or
                                        excessive exercise routines, in an effort to maintain a healthy weight.</p>
                                    <p class="pt-serif"><b>Bulimia Nervosa:</b> The eating disorder known as
                                        <a href="https://www.abhasa.in/bulimia-nervosa-disorder-treatment/">bulimia nervosa is</a> characterized by recurrent episodes of binge eating followed by purging. After that, you might try to get rid of the extra calories by forcing yourself to throw up, using laxatives and/or enemas, or working out excessively.
                                    </p>
                                    <p class="pt-serif"><b>Binge eating disorder:</b> habitual overeating characterized by the consumption of large quantities of food, frequently consumed in a short period of time and to the point of fullness that the discomfort the individual experiences. indistinguishable from that of bulimia, minus the purging. Since binge eating is often done in private due to associated feelings of shame, guilt, and embarrassment, it is often a behaviour that goes unreported.</p>

                                    <div class="pr-subtitle">Symptoms of an Eating Disorder Include</div>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Being obsessed with weight and shape. </li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Not eating much at all.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Repeatedly eating huge amounts of food at once (often very quickly).</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Having very strict routines or habits when it comes to food.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Made yourself throw up or took laxatives after eating.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Excessive exercise (too much and too frequently).</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Staying away from social situations where food is involved.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Pulling away, getting worried, or feeling sad.</li>
                                    </ul>

                                    <div class="pr-subtitle">Physical Symptoms Can Include</div>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Feelings of dizziness, a racing heart, fainting, or low blood pressure or pulse rate.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Elevated blood pressure.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>A big drop in weight.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Problems with digestion, like heartburn, bloating, constipation, or diarrhea.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Poor or disrupted sleep.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Hair loss.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Late onset of puberty.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Not menstruating (women and girls).</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Weakened immune system.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Low energy and bad health all around.</li>
                                    </ul>

                                    <div class="pr-subtitle">Psychological Symptoms</div>

                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i><a href="https://www.abhasa.in/anxiety-disorder-treatment/"> Anxiety.</a></li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i><a href="https://www.abhasa.in/depression-rehabilitation-center-in-mumbai/">Depression.</a></li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Isolation and loneliness.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Guilt and shame.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Feelings of failure.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Obsessive thoughts and preoccupations.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Compulsive behaviours.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Self-harming.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Suicidal thoughts.</li>

                                    </ul>
                                    <div class="pr-subtitle">Eating Disorder Treatment</div>
                                    <p class="pt-serif">How you treat your eating disorder will be unique to your condition and its manifestations. Psychotherapy, nutrition education, medical monitoring, and occasionally medication are all part of the standard treatment protocol. Other health issues that arise as a result of an eating disorder need to be addressed as part of treatment as they can become severe, if not fatal if left unchecked. Hospitalization or another sort of inpatient program may be necessary if  an <a href="https://www.abhasa.in/how-to-help-someone-with-an-eating-disorder/">eating disorder</a> does not
                                        improve with regular therapy or if it creates health risks. Maintaining your
                                        physical and mental well-being while recovering from an eating disorder
                                        requires a systematic approach to symptom management.</p>
                                    <div class="pr-subtitle">Psychological Therapy</div>
                                    <p class="pt-serif">The most crucial part of treating an eating issue is psychological counselling. A regular appointment with a psychiatrist, therapist, or other mental health expert is required. The duration of therapy can vary from a few months to several years. You can use it to:</p>

                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Try to get your eating habits back on track and pick up some
                                            healthy weight.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Swap out your bad routines for wholesome ones</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>The ability to keep tabs on one&#39;s emotional and dietary status is a
                                            vital life skill.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Learn to solve problems and find healthy stress-reduction
                                            strategies.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Strengthen your bonds with others.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Lift your spirits</li>
                                    </ul>
