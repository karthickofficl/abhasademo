                                        <li class="pt-serif"><i class="fa fa-circle"></i> - Identifying triggers and addressing emotional aspects of addiction.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i> - Building resilience and developing healthier relationships with money.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>- Peer support and shared experiences in group therapy sessions.</li>
