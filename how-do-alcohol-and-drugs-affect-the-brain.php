                                    <h1>How do alcohol and drugs affect the brain? </h1>
                                </div>
                                <div class="fl-wrap text-block">
                                    <div class="clearfix"></div>
                                    <p class="pt-serif">Our brain is the most important yet complex organ of our human body. The three-pound organ has more than 100 billion nerves that make trillions and zillions of connections with the rest of the organs in the body. The human brain forms an essential part of the central nervous system, regulating and controlling our thoughts, memory, habits, touch, motor skills, breathing, vision, hearing, and everything else that keeps us alive and going. The brain is so complex that it starts to work right at the moment we are born and continues to function until we leave this world permanently. Studies suggest that more than 85% of people consume alcohol at least once in their lifetime, and in India, around 30% of men are regular consumers of alcohol. Another study suggests that more than <a href="https://www.giveindia.org/blog/5-facts-you-should-know-about-drug-abuse-in-india/">2.26 crore individuals</a> take opioids including heroin, opium, etc.

                                        <br>With alcohol and drug consumption becoming so prevalent that people have started talking about the emergence of a drug pandemic, a lot of people have this question: How does alcohol affect the brain? Or how does alcohol affect the brain and behaviour? How do drugs affect the brain? This blog is an attempt to educate people on the harmful effects of alcohol and drugs on the human brain.
                                    </p>
                                    <div class="pr-subtitle">How does the brain work normally without alcohol or drugs?</div>
                                    <p class="pt-serif">As said earlier, the human brain is made up of billions of neurons, the nerve cells that control the flow of information and the functioning of different organs in the body. These neurons control the thoughts, actions, emotions, feelings, movements, and other autonomous functions of the body. The brain has various circuits that coordinate the functioning of specific organs. These circuits are interconnected, and they work in sync and harmony to keep us alive and going. Neurons send signals between themselves to different nerves, brains, and other parts of the nervous system
                                        <br>When a neuron has to pass information to another neuron, it releases neurotransmitters in the space that exists between this neuron and the other neuron. This neurotransmitter gets attached to the receiving neuron, causing reactions, and this second neuron transmits information to another neuron in a similar manner. The billions of neurons are closely connected to each other and aid in the communication that happens between the brain and other body parts. Say, when you see a speck of dirt on the floor, you tend to lift your legs almost spontaneously because of the communication that happens between the eyes, brain, legs, and various other sensory organs. The next section will deal with how alcohol affects the brain and distorts its normal functioning
                                    </p>

                                    <div class="pr-subtitle">How does alcohol affect the brain?</div>

                                    <p class="pt-serif">Alcohol consumption, particularly excess consumption on a regular basis, intrudes into the functioning of the brain and causes damage. Mild drinking or consumption in smaller amounts may result in less harm. Yet moderate-to-severe alcohol addiction produces an adverse impact on the brain. The alcohol that a person consumes impacts the neurotransmitters in their brains. When they are affected, the communication that happens between the neurons may either slow down or not happen at all. That is why a person who is drunk takes time to understand what the other person is saying. When the communication that happens through neurotransmitters is disrupted, reactions to external messages may be slow, and vision may be blurred. The person also feels jittery and may not be able to talk in a flow.

                                        <br>The cerebellum in the brain has tissues that coordinate and control the movement of the body. Excessive alcohol consumption may distort the functioning of the cerebellum, resulting in an imbalance of the body and physical movements. The limbic system in the brain has the hippocampus, hypothalamus, and thalamus that control and regulate our feelings and emotions. The hippocampus is associated with memory, while the hypothalamus controls emotions.

                                        <br>Excessive consumption of alcohol affects the hippocampus, leading to memory loss, as well as the hypothalamus, impacting the ability to control one’s emotions and feelings. This is why some people keep smiling for no reason after drinking, while there are others who cry uncontrollably in an attempt to shed their anxiety and depression.

                                        <br>Long-term, continued usage of alcohol may aggravate these ill impacts and lead to premature death, coma, and other fatal disorders.
                                    </p>

                                    <div class="pr-subtitle">How drugs affect the brain?</div>
                                    <p class="pt-serif">As mentioned earlier, different neurotransmitters work in sync to regulate various functions of the body. Some common neurotransmitters include dopamine, endorphin, and oxytocin. Dopamine is the reward system that validates the rewards and motivates people to attain them, whereas endorphin is a natural painkiller released by the pituitary gland and hypothalamus in the body’s endeavour to relieve stress and pain. Oxytocin is also produced by the hypothalamus and is released during childbirth and sex. Different drugs intervene in the functioning of different neurotransmitters. Say, for example, that heroin or other opioid drug varieties influence the release of dopamine. This is temporary, and the person experiences pleasure and unexplainable happiness under the drug's influence. Over time, with regular intake of drugs, the brain stops what is called its natural functioning. With regular use of heroin, a person’s brain stops its natural release of dopamine. On the other hand, there is a craving to enjoy the pleasure that comes from drug consumption. The person starts craving to get into a state of happiness, which means they have to take the drug. This is one reason why people find it hard to withdraw from drugs and why they feel low and depressed when they are without drugs.

                                        <br>While heroin overstimulates the neurotransmitter leading to the release of dopamine, there are other drugs like marijuana that release hormones and chemicals akin to those that are released in our body, though both of them are very different. This is the answer to the question that you must have had in your mind, ‘how do drugs affect the brain”.
                                    </p>

                                    <div class="pr-subtitle">How can sobriety reverse the damage from alcohol?</div>
                                    <p class="pt-serif">Now you know how alcohol affects the brain, and let’s see how sobriety can help people recover from alcohol damage. The good news for alcoholics who are on a spree to reverse damages is that detoxing the body and staying away from alcohol for prolonged periods can help reverse the damage caused by alcohol consumption. Scientists observed that the shrinkage that happens in some parts of the brain to intervene in the cognitive behaviour of the person due to alcohol consumption is reversed when he or she stops the intake of alcohol for a longer period.

                                        <br>Sobriety means staying away from being influenced by an external substance. When a person refrains himself or herself from consuming alcohol or when they choose to stay sober, new cells develop, and this is called <a href="https://pubmed.ncbi.nlm.nih.gov/24842804/">neurogenesis.</a> The hippocampus, which was damaged earlier, begins to normalize. Though it could be harder and the person may experience cognitive impacts in the initial stages of recovery, making up their mind to stay on to it for prolonged periods or forever will improve brain development. What is more important in the early stages of sobriety is staying away from negative emotions and stressful activities, as this may aggravate the mind and motivate them to get back to drinking.

