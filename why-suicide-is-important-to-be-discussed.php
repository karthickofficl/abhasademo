                                    <h3 class="pr-subtitle pt-serif-h3">CALL THEY WANT IS TO BE HEARD!</h3>
                                    <p class="pt-serif">Some people feel that someone with suicidal ideations is bound to commit suicide. With rehabilitation and proper therapy. When you see certain warning signs in your loved one, please look for the best rehabs in India to provide him or her with the best treatment. However, the following measures are important to support people who are suicidal in the following ways:  </p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Being an emotional container for such a person at the time of need is very important. Sometimes people need to be heard in a non-judgemental manner.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>When you have a good bond with someone who is going through mental health issues, don’t make fake promises that you will always be there for that person. Rather, always keep a back-up of people who can rush to the patient so that we can save him/her.</li>
