                                    <h3>Why is suicide important to discuss?  </h3>
                                </div>
                                <div class="fl-wrap text-block">
                                    <div class="clearfix"></div>
                                    <p class="pt-serif">Statistics say that, on average, 4,00,000 people commit suicide every year around the world. It is the tenth leading cause of death globally. Every 40 seconds, another family loses a loved one to suicide. National statistics on suicides in India for 2011 showed that young adults aged between 15 and 29 years accounted for 35.4% of all suicides that year (NCRB, 2011), and the remaining deaths were not suicidal.  </p>
                                    <p>Talking about this topic has a lot of stigma and shame associated with it, which needs to be rectified.  </p>


                                    <p class="pt-serif">According to the WHO, suicide is an act of killing oneself intentionally, performed by the person with full knowledge or expectation of the fatal outcome.  </p>
                                    <ol class="ol">
                                        <li class="pt-serif">It involves an intentional/voluntary determination to end one’s life or not,</li>
                                        <li class="pt-serif">The seeds of suicide lay in the person himself/herself,</li>
                                        <li class="pt-serif">The presence of a known/hidden reason, causes one to end one’s life,</li>
                                        <li class="pt-serif">It is a state in which choices/options are never considered before the act,</li>
                                    </ol>
                                    <p class="pt-serif">The following is the trajectory of how suicide is either committed or attempted:</p>


                                    <h3 class="pr-subtitle pt-serif-h3">What are the Warning Signs?</h3>
                                    <p class="pt-serif">In the case of Sushant Singh Rajput, the 34-year-old successful Bollywood superstar, one careful look at his Instagram feed can also shed light on his state of mind. His last post, dedicated to the tragic death of his mother, who passed away when he was only a teenager, was a big warning sign that was ignored not just by his loved ones but by his fans as well. According to specialists in suicide therapy for recovery, people with suicidal ideations usually try to convey their feelings. But the problem is, many people take these signs for granted and look at them as either attention-seeking behaviour or routine behaviour. People also think that outwardly-looking, celebrity-type, or successful people don’t have vulnerabilities like suicidal ideations. But this is a myth and needs to be addressed. No matter how successful or talented an individual is, they are also vulnerable and prone to suicidal ideations.  </p>
                                    <p class="pt-serif">According to the Canadian Mental Health Association, some warning signs include the following:  </p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>repeated expressions of hopelessness,</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>helplessness, or desperation due to loss, shock, impending crisis (perceived or real)</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Weeping spells</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Anxiety & restlessness</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Mood swings (extreme happiness to sadness)</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Difficulty in decision-making</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Self-injurious behaviour</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>behaviour that is out of character, such as recklessness in someone who is normally careful, <a href="https://www.abhasa.in/depression-rehabilitation-center/"><b>signs of depression – sleeplessness,</b></a> social withdrawal, loss of appetite, loss of interest in usual activities, a sudden and unexpected change to a cheerful attitude, giving away prized possessions to friends and family, making a will, taking out insurance, or other preparations for death, such as telling final wishes to someone close, making remarks related to death and dying, or an expressed intent to commit suicide.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Drug and alcohol abuse, stress, and reckless behaviour are also common warning signs; another warning sign is if the person has already attempted suicide.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Bullying among teenagers, loss of a loved one, break up of a relationship</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Being an emotional container for such a person at the time of need is very important. Sometimes people need to be heard in a non-judgemental manner.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>This is necessary apart from counselling, therapy and antidepressants.</li>
