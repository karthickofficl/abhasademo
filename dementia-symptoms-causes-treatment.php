                                    <h1>What is Dementia? Symptoms, Causes & Treatment</h1>
                                </div>
                                <div class="fl-wrap text-block">
                                    <div class="clearfix"></div>
                                    <p class="pt-serif">Dementia is used to refer to chronic mental disorders that include loss of language, problem-solving, memory, reasoning, and other cognitive abilities. Before moving ahead with understanding the intricacies of dementia, one must understand that the term refers to the state of a person’s mental condition and not a particular disease or a specific disorder. It is a syndrome that persistently erodes one’s cognitive abilities beyond what is considered normal for the usual consequence of ageing biologically. As per the <a href="https://www.who.int/news-room/fact-sheets/detail/dementia" target="_blank">World Health Organization</a> report dated September 2, 2021, there are more than 55 million people combating dementia across the world, and there are more than 10 million new cases adding up every year.

                                        <br>Dementia remains one of the major reasons for disability and is the seventh largest cause of death across the world. The blog endeavours to answer what dementia is, record the dementia symptoms and early signs, dementia causes, and the treatment options available to handle dementia.
                                    </p>

                                    <div class="pr-subtitle">What is dementia?</div>
                                    <p class="pt-serif">Dementia is an extensive term used to refer to a mental condition. It could be loss of memory, thinking and reasoning abilities, attention disorders, and other forms of cognitive disabilities. The primary reason for dementia is the damage that occurs to the parts of the brain that are dedicated to learning, memory, decision-making, language comprehension, or problem-solving. </p>
                                    <p class="pt-serif">Studies suggest that about 5%–8% of adults living above the age of 65 have dementia in some form. This rate doubles after the data account for the age above 65. At least half of the population living in the age group of 80 and above has dementia. Dementia exists in several forms. Though it is common among people in old age, it is not an attribute of biological aging. Dementia is a deviation and distortion from how biological ageing usually happens. Having learned what dementia is, let’s move ahead to understand dementia causes and symptoms. </p>
                                    <div class="pr-subtitle">Dementia symptoms</div>
                                    <p class="pt-serif">Dementia occurs when a nerve cell in the brain loses its connection with other cells, and the healthy neuron stops functioning. It is common that people experience loss of connections as we age, but in dementia, the severity and impact are on the greater side. </p>
                                    <p class="pt-serif">The symptoms of dementia differ from one person to another. They also depend on a person's health condition, lifestyle, food and sleeping patterns, underlying conditions, and cognitive functioning. Depending on the severity of the disorder, </p>
                                    <div class="pr-subtitle">Early signs of dementia</div>
                                    <p class="pt-serif">The onset of dementia is very gradual, and it does not happen instantly. The common symptoms in the early stages, or, in other words, the early signs of dementia, include: </p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Forgetting things and places</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Missing out places, routes, and directions.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Losing out track of time</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Getting lost in places.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Forgetting where they kept things</li>
                                    </ul>
                                    <p class="pt-serif">People tend to confuse the signs of dementia with those of biological ageing. Family members and caretakers ignore these early signs of dementia, essentially because their onset is slow and extremely gradual. </p>
                                    <div class="pr-subtitle">Middle stage dementia symptoms</div>
                                    <p class="pt-serif">As the symptoms progress and dementia enters the midstage stage, the signs of dementia become visible and prominently noticeable. The common symptoms include the following:</p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Getting confused quite often</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Requiring help in executing personal activities</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Forgetting the recipe as they cook</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Experience difficulties in communicating their requirements</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Not knowing the days and months</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Finding it hard to take care of oneself</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Losing track of finances and bills.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Wandering without a reason.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Forgetting what the person in front says during a conversation.</li>
                                    </ul>
                                    <div class="pr-subtitle">Advanced dementia symptoms</div>
                                    <p class="pt-serif">In the advanced stages of dementia, the symptoms become predominant and severe. In this stage, the affected person becomes wholly dependent on others to execute their day-to-day activities, and there is persistent inactivity.
                                        <br>The signs of dementia and symptoms at this stage include the following:
                                    </p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Experiencing difficulty in remembering or failing to identify friends and close family members.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Enduring difficulties in walking and standing</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Repeating tasks, say bathing again and again because they tend to forget that they had already bathed.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Difficulties in handling finances- giving away money to people whom they don’t even know. </li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Decreasing motor abilities and functions.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Forgetting names of people and losing count of numbers</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Some people may experience depression and anxiety</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Hallucinations and illusionary fear are also common in the advanced stages of dementia.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Repeatedly asking the same question over and over again</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Losing physical and mental balance</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Inability to move and confinement to bed.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Appetite and loss of weight.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Some people lose ability to speak and communicate.</li>
                                    </ul>
                                    <div class="pr-subtitle">Dementia causes</div>
                                    <p class="pt-serif">As mentioned earlier, dementia causes damage to nerve cells or loss of connections to the brain. Based on the type of nerve cell that is impacted and the function it is executing, dementia carries different levels of complexity and can display different signs and symptoms of cognitive disabilities. </p>
                                    <div class="pr-subtitle">Types of dementia</div>
                                    <p class="pt-serif">Dementia is generally classified into three groups.</p>
                                    <div class="pr-subtitle">Primary Dementia</div>
                                    <p class="pt-serif">Primary dementia includes those conditions where dementia remains the primary form of illness.</p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Alzheimer’s disease</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Vascular dementia</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Frontotemporal dementia</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Lewy body dementia</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Mixed dementia</li>
                                    </ul>
                                    <div class="pr-subtitle">Secondary Dementia</div>
                                    <p class="pt-serif">Secondary dementia covers the cognitive disabilities that result due to an underlying disease or condition. They include</p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Huntington’s disease</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Creutzfeldt-Jakob disease</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Traumatic brain injury (TBI)</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Wernicke-Korsakoff syndrome</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Parkinson’s disease</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>HIV-associated dementia</li>
                                    </ul>
                                    <div class="pr-subtitle">Dementia-like symptoms from other illnesses</div>
                                    <p class="pt-serif">Some illnesses produce dementia-like symptoms and signs and these conditions can be halted with proper dementia treatment.</p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Vitamin deficiency</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Side effects of medications</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Normal pressure hydrocephalus (NPH)</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Brain tumors</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Endocrine disorders</li>
                                    </ul>
                                    <div class="pr-subtitle">Dementia Treatment</div>
                                    <p class="pt-serif">Diagnosis of dementia involves careful observation of the symptoms, underlying conditions, medical history, and behavioral changes. You may be asked the following questions to offer a proper dementia treatment.</p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Your age as people older than 65 years of age is increasingly vulnerable to dementia and Alzheimer's disease.</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Your medical history</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Your ongoing course of medications</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Symptoms and signs</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>The family history of dementia is also genetic. </li>
                                    </ul>
                                    <p class="pt-serif">Even though your physician may be able to diagnose that a person is suffering from dementia, they may find it difficult to classify the type. You may be required to consult a neurologist or a psychologist who would advise you to take up a neurocognitive test, a laboratory test, or an imaging test so as to devise an effective dementia treatment.
                                        <br>Most forms of dementia are treatable and a combination of drugs and counseling sessions shall help manage the symptoms. Drugs could slow down the progression of the disease.
                                    </p>
                                    <div class="pr-subtitle">Is it possible to prevent dementia?</div>
                                    <p class="pt-serif">ReThough dementia cannot be prevented wholly, taking precautionary steps may help reduce the risk of complications.</p>
                                    <ul class="ul">
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Maintaining a healthy lifestyle</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Following a healthy diet</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Having a fitness routine</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Taking up cardiovascular training</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Refraining from tobacco, drugs, and alcohol</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Engage your brain in mental activities</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Keep your mind engaged by conversing with people, friends, and family</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Do not feel alone or neglected</li>
                                        <li class="pt-serif"><i class="fa fa-circle"></i>Having a quality sleep cycle</li>
                                    </ul>
                                    <div class="pr-subtitle">Looking for dementia treatment?</div>
                                    <p class="pt-serif">If your loved one or friend is experiencing dementia and is looking for dementia treatment, you may consider Abhasa Rehabilitation Centre, the leading rehabilitation centre in India. Always remember that diagnosing the onset early might help you make meaningful plans for your loved one and the family and help manage the complications with ease. A combination of drugs and counselling therapies for both the patient and their family reduces the level of complications. </p>
