                                        <p class="pt-serif">Disputes, disagreements, and quarrels are a standard part of being in a relationship. Mainly because no two partners are exactly equal, our differences can cause divisions in the way we communicate, behave, and behave towards the other person. However, what happens if it isn’t only the occasional disagreement here and there? Can you always be sure if you're in a poisonous relationship? If being anxious, fearful, feeling drained, on edge, or even suspicious has to turn out to be a part of your own everyday life, you might be in a harmful relationship. </p>
                                        <p class="pt-serif">When the relationship lacks belief in, respect, empathy, and mutual support, it is time to evaluate whether you are usually in a healthy relationship—one that encourages both people and mutual development—or a toxic relationship. </p>
                                        <p class="pt-serif">The phrase “toxic relationship” was coined by communication and psychology expert, Doctor Lillian A. Glass, in her 1995 book Toxic Folks. She describes a toxic relationship as one in which people “don’t support each other, where there is conflict and one seeks to weaken another, where there is competition, where there is disrespect and a lack of cohesiveness.” </p>
                                        <p class="pt-serif">Dangerous relationships take an emotional, mental, and often physical toll on the people in them. Yet, they don’t always entail abuse. </p>
                                        <p class="pt-serif">In fact, toxic relationships can happen between two well-balanced individuals who are having difficulty connecting or communicating efficiently. When the bulk of interactions between the people in the relationship are unpleasant, when the good surpasses the bad, it's time to make a change. Sometimes toxic relationships must terminate in divorce, such as when there is abuse or harassment. </p>
                                        <p class="pt-serif">Other times, poisonous relationships may be transformed into healthy ones by modifying one's conduct and communication style. Not sure whether you're in a poisonous relationship? You’re certainly not alone. The symptoms can be so subtle that they will make you scratch your head and even wonder what’s going on. If you are wondering, “Am I in a toxic romantic relationship?” it’s vital that you make sure an individual knows the symptoms so you can take action if possible. Keep reading to be able to find out, no matter if you are always in a poisonous relationship.</p>
                                        <div class="pr-subtitle">Signs of toxic behaviour in a relationship</div>
                                        <p class="pt-serif">Each time a relationship teeters at the risk of making you happy or miserable, you might need some help identifying harmful relationship signs to understand what you are dealing with. These are the signs of a bad partnership:</p>
                                        <ul class="ul">
                                            <li class="pt-serif"><i class="fa fa-circle"></i>An individual focuses their energy on their lover and quite often feels used up physically, emotionally, and mentally.</li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>You sense you are consistently being judged, belittled, pressured, or governed. </li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>Individual drop friends, friends, and family, or activities an individual likes either for the reason that there is direct strain to do consequently or because an individual thinks it will probably produce things smoother.</li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>An individual should shut down, avoid saying what they think, and not feel comfortable being themselves when their lover is around.</li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>The relationship is one-sided.</li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>There is a power struggle in the relationship.</li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>You are being ignored.</li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>There’s no trust in the relationship.</li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>You can’t rely on your partner’s care.</li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>You fight over insignificant things.</li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>There is envy between the two of you.</li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>You feel unworthy, small, scared, or sad.</li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>Lying and dishonesty are part of your life.</li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>You have no privacy.</li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>You are scared of saying no.</li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>Your feelings and thoughts are not appreciated.</li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>An individual cannot discuss your demands or changes throughout your life that have been important.</li>
                                         </ul>
                                        <p class="pt-serif">Depending on how deep the hurt is, it is also possible that the harmful relationship you are in is not fixable. The only real chance you have of fixing what exactly is damaged in your partnership is if you are both prepared to handle the facts, admit your wrongdoings, and be available to change your actions.</p>
                                        <div class="pr-subtitle">Step 1: Talk about it </div>
                                        <p class="pt-serif">The first step to fixing a harmful relationship is to get on the same page with your companion. Generally, there needs to be work from both sides to repair precisely what is broken and progress in a productive way.  <br>
                                        Offer the other a place to spread out and bring up virtually any constructive or negative feelings that come up in the partnership. I would not interrupt. Focus alternatively on making the partner feel risk-free and heard even though they’re talking. Is it considered important that the partner also does this?</p>
                                        <div class="pr-subtitle">Step 2: Consider therapy </div>
                                        <p class="pt-serif">It can be helpful to be present at therapy together. Even so, do not forget that couples’ therapies are neither risk-free nor appropriate in the event that there is an imbalance of electric power in the marriage. The sides will need to want to correct common problems when you go to therapy and carry good intentions together with honest behaviour during each session.  <br>
                                        An experienced, objective third party will help you put things into perspective and increase your insight into your downfalls, your partner’s feelings, and recurring miscommunications. The therapist will most likely offer you exercises that will create a platform for honest conversations to bring you deeper together. Your current therapist may likewise help to distinguish violent behaviour; of course, if essential, they may motivate you to part ways.</p>
                                        <div class="pr-subtitle">Step 3: Build yourself up </div>
                                        <p class="pt-serif">Those who have been in harmful relationships for a long time usually discover that they have lost their perception of themselves. These people feel low, not worthy, and anxious; in addition, they may participate in deprecating self-talk or even express anger. Generate your self-esteem simply by engaging in activities that you enjoy, broadening your social group of friends, and investing in self-care. <br>

                                        When your lover tries to prohibit you from undertaking these things, it is considered time to stop the relationship. It may be especially important to establish a help network. Don’t decline help from other people. If you want to speak to someone important about this connection, do it. As soon as you have built yourself up, you will have an improved grasp of whether or not the relationship is salvageable or worth the investment. <br>

                                        Because you have recently been with someone for a long time, that does not mean you will be obligated to be able to stay. You are obligated to pay that person whatever, but you carry out owe it to be able to live some sort of life free via anxiety, depression, and fear. If it’s attainable to get to a healthy spot with your lover, work on fixing the dynamics in the relationship.  <br>

                                        It requires a lot of strength to let someone know your relationship isn’t on your side. Occasionally, to finish a toxic connection is to finish the connection. Whenever that occurs, concentrate your energy on how to develop yourself upward again with self-care and self-love by encircling yourself with individuals who love and care for you. While you may not think so right now, you can change your situation. Whether it is fixing the power or getting aside, you need to do what’s right for you, so go about it and don’t delay.</p>
