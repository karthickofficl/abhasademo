                                        <p class="pt-serif">ALike its name, this disorder is characterized by recurring thoughts, impulses, or pictures & compulsive obsession-related activities. Known as anxiety disorder.</p>


                                        <h3 class="pr-subtitle pt-serif-h3">There are various types of Obsessive-Compulsive disorders :</h3>
                                        <ol class="ol">
                                            <li class="pt-serif">Excessive handwashing: Being the classic type of OCD, this deserves a special mention. This is the commonest type in which the person has intruding thoughts, “My hand is dirty” which is then followed by the compulsive ritual of washing his/her hands till the anxiety is subsided. However this can take severe forms, in that, the patient washes hands for hours together sometimes resulting in rashes and peeling away of the skin, but even then the hand washing is continued till anxiety is subsided.</li>
                                            <li class="pt-serif">Excessive showering: A person with this symptom takes too long for showering, sometimes taking hours together.</li>
                                            <li class="pt-serif">Excessive cleaning: Cleaning the house, floors, tiles, and progressing to cleaning of personal articles, consuming a long time.</li>
                                            <li class="pt-serif">Excessive checking: Checking where the things are being kept, the door is locked or not, the gas has been switched off or not, several times due to anxiety leading to self-doubt, being the commonest examples of this particular type.</li>
                                            <li class="pt-serif">Excessive counting: Spending excessive time counting things is another type of this disorder.</li>
                                            <li class="pt-serif">Ordering and arranging: This involves spending excessive time in keeping things in order and also arranging things in symmetry in order to be perfect and aligned.</li>
                                            <li class="pt-serif">Primary Obsessional Slowness: This involves extreme slowness in motor activities.</li>
                                            <li class="pt-serif">Pure Obsessions: This involves unwanted, intrusive, and uncontrollable thoughts.</li>
                                            <li class="pt-serif">Hoarding: Hoarding involves collecting things unnecessarily and having extreme difficulty in parting away from them.</li>
                                        </ol>

                                      
                                      <h3 class="pr-subtitle pt-serif-h3">How to overcome this disorder?</h3>
                                      <p class="pt-serif">If you are <a href="../ocd-treatment-center"><b>suffering from OCD</b></a> and is keen on overcoming it, here are some of the treatment options for you </p>
                                      <ul class="ul">
                            <li class="pt-serif"><i class="fa fa-circle"></i>Medication: Consult a psychiatrist as soon as possible. Prompt treatment is the first remedy.</li>                           
                            <li class="pt-serif"><i class="fa fa-circle"></i>Consult a therapist: Cognitive-Behaviour Therapy is the most effective therapy for Obsessive-Compulsive Disorder.</li>
                            <p class="pt-serif">Apart from this, you can also incorporate yoga, exercise, meditation, mindfulness meditation, deep breathing, and Progressive Muscle Relaxation in your daily routine for overcoming this disorder.</p>
                        </ul>

                                     



