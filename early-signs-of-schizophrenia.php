                                        <h3>Early signs of schizophrenia</h3>
                                    </div>                                  
                                    <div class="fl-wrap text-block">
                                        <div class="clearfix"></div>
                                        <p class="pt-serif">Schizophrenia is a chronic brain disease. Symptoms of schizophrenia include delusions, hallucinations, disordered speech, difficulty thinking, and a lack of desire. Signs of schizophrenia encompass delusions, hallucinations, disordered speech, issue-wondering, and a loss of desire.</p>
                                        <p class="pt-serif">Most symptoms of schizophrenia may be significantly improved with remedies, and the risk of recurrence can be reduced. Despite the truth that there may be no cure for schizophrenia, research focuses mainly on new and more secure treatments. Experts are also examining genetics, performing behavioural studies, and utilising modern imaging to examine the structure and function of the brain to figure out what causes the condition.</p>
                                        <p class="pt-serif">These strategies carry the possibility of developing new and more effective remedies. The disease's intricacy may explain why there are so many misunderstandings about it. Schizophrenia does not imply multiple personalities or divided personalities.</p>
                                        <p class="pt-serif">The majority of people with schizophrenia are no more dangerous or violent than the general public. While a lack of community mental health supports may result in homelessness and numerous hospitalisations, it is a common misperception that people with schizophrenia end up homeless or in hospitals. The majority of people with schizophrenia live with their families, in group homes, or alone.</p>
                                        <div class="pr-subtitle">Schizophrenia Symptoms:</div>
                                        <p class="pt-serif">When the condition is active, the person may have episodes in which they are unable to tell the difference between actual and unreal events. The severity, length, and frequency of symptoms might vary depending on the condition; nevertheless, in people with schizophrenia, the incidence of severe psychotic symptoms frequently reduces as they get older.</p>
                                        <ul class="ul">
                                            <li class="pt-serif"><i class="fa fa-circle"></i><b>Positive symptoms:</b> hallucinations, such as hearing voices or seeing things that don't exist, paranoia, and exaggerated or distorted perceptions, beliefs, and actions, are all examples of abnormally present hallucinations.</li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i><b>Negative symptoms:</b> a loss or a decline in the capacity to begin plans, communicate, exhibit emotion, or find pleasure (those who are unusually absent).</li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i><b>Disorganized symptoms:</b> confused and disorganised thought and speech, difficulties with logical reasoning, and occasionally unusual behaviour or aberrant motions are all symptoms of schizophrenia.</li>
                                        </ul>
                                        <p class="pt-serif">Schizophrenia symptoms normally begin in early adulthood and must last at least six months before a diagnosis may be made. Men are more likely to have symptoms in their late teens or early twenties, while women are more likely to develop symptoms in their twenties and early thirties.</p>
                                        <p class="pt-serif">Troubled relationships, poor school performance, and a lack of motivation are some of the more subtle indicators that may be present early. Researchers think that a variety of genetic and environmental variables have a role in the aetiology and that life stresses may influence the onset and progression of symptoms. Because several variables may be involved, experts are unable to pinpoint the exact reason in each case.</p>
                                        <div class="pr-subtitle">Rehabilitation and Living with Schizophrenia </div>
                                        <p class="pt-serif">Many human beings with schizophrenia can benefit from a remedy and live productive and pleasing lives. Some individuals recover exceedingly well, while others remain symptomatic and require care and help, as with other chronic illnesses. Following the management of schizophrenia symptoms, many forms of therapy can be used to assist patients in managing their condition and enhancing their lives.</p>
                                        <p class="pt-serif">Therapy and psychological support can aid in the development of social skills, the management of stress, the detection of early warning signals of relapse, and the extension of periods of remission.</p>
                                        <p class="pt-serif">Because schizophrenia usually hits in early adulthood, people with illness frequently benefit from rehabilitation to help them build life-management skills, finish occupational or educational training, and maintain employment. Supported-employment programmes, for example, have been shown to assist people with <a href="https://www.abhasa.in/schizophrenia-treatment-in-mumbai/">schizophrenia</a> in achieving self-sufficiency. These initiatives help people with serious mental illnesses find employment in a competitive community.</p>
                                        <div class="pr-subtitle">Types of schizophrenia </div>
                                        <p class="pt-serif">Schizophrenia comes in a variety of forms.</p>
                                        <div class="pr-subtitle">Paranoid schizophrenia:</div>
                                        <p class="pt-serif">The most prevalent kind of schizophrenia is this. It may develop later in life than other types. Hallucinations and/or delusions are common symptoms, although your speech and emotions may be unaffected.</p>
                                        <div class="pr-subtitle">Hebephrenic schizophrenia:</div>
                                        <p class="pt-serif">This kind of schizophrenia, often known as 'disorganised schizophrenia,' usually strikes people between the ages of 15 and 25. Disorganised behaviours and ideas, as well as short-term delusions and hallucinations, are all symptoms.</p>
                                        <p class="pt-serif">You may speak in a disorganised manner, making it difficult for people to understand you. The facial expressions, voice tone, and mannerisms of people with disorganised schizophrenia generally exhibit little or no emotion.</p>
                                        <div class="pr-subtitle">Catatonic schizophrenia:</div>
                                        <p class="pt-serif">This is the most uncommon kind of schizophrenia, characterised by peculiar, restricted, and abrupt movements. You may alternate between being quite busy and being very still regularly. You may not speak much and may imitate the speech and movements of others.</p>
                                        <p class="pt-serif">Undifferentiated schizophrenia: Your diagnosis may show characteristics of paranoid, hebephrenic, or catatonic schizophrenia, but it doesn't seem to fit well into any of these categories.</p>
                                        <div class="pr-subtitle">Unspecified schizophrenia:</div>
                                        <p class="pt-serif">Symptoms meet the general conditions for a diagnosis but do not fit into any of the above categories.</p>
                                        <div class="pr-subtitle">Causes of schizophrenia:</div>
                                        <p class="pt-serif">Although the exact causation of schizophrenia is unknown, it is thought to be caused by a combination of the following factors: </p>
                                        <ul class="ul">
                                            <li class="pt-serif"><i class="fa fa-circle"></i>Brain chemistry </li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>Genetics</li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>Complications at birth </li>
                                        </ul>
                                        <p class="pt-serif">Following a traumatic incident, such as the death of a loved one or the loss of a career, some people acquire schizophrenia. Psychosis and schizophrenia symptoms can be triggered by experiences such as growing up in a town or city, stressful life events, and relocating to a new town or country. There's also a substantial correlation between heavy cannabis usage and the onset of schizophrenia.</p>
                                        <div class="pr-subtitle">Treatments for schizophrenia</div>
                                        <p class="pt-serif">Although there is no cure for schizophrenia, with the correct therapy, symptoms can be managed and the risk of subsequent episodes can be reduced. Every person's experience with schizophrenia is unique. It may improve before deteriorating, you may experience spells of illness, or the consequences may be more consistent.</p>
                                        <p class="pt-serif">Up to one out of every ten people may have a complete recovery, and one out of every five may see considerable improvement. Around half of those diagnosed with schizophrenia will live with it for the rest of their lives.</p>
                                        <p class="pt-serif">Medication and conversation therapy are the most frequent treatments for schizophrenia. <br>
                                        Antipsychotic drugs may be prescribed to you by your doctor to treat schizophrenia. These will help alleviate the symptoms, but they will not cure the condition. Working with your doctor to identify the appropriate medicine for you is a good idea.
                                        </p>
                                        <p class="pt-serif">If you wish, a carer or family member can assist you in making your decision. Each drug's advantages and adverse effects should be explained by doctors. If the medicine's negative effects are too tough to bear after a few weeks of trial, talk to your doctor about switching to another prescription. It's crucial not to quit abruptly because this might lead to withdrawal symptoms.</p>
                                        <p class="pt-serif">Talking therapies, also known as psychosocial therapy, assist you in examining your beliefs and behaviors. The goal of cognitive-behavioural therapy is to help you comprehend the connections between your ideas, feelings, and ehaviors. CBT examines your symptoms and how they affect your life, as well as your beliefs and views. CBT helps you become more aware of your episodes and teaches you strategies for dealing with stress and other symptoms.</p>
                                        <p class="pt-serif">Art therapy may assist you in learning new methods to interact with others, expressing how you are feeling, and accepting and understanding your feelings. Art therapy should be explored if your psychosis recurs. Arts therapy, which normally takes place in a group setting to help integrate communication and creativity, may be more beneficial if you are experiencing depression symptoms such as withdrawing from family and friends.</p>
                                        <p class="pt-serif">Family intervention is a process in which you and your family collaborate with mental health specialists to improve relationship management. This should be extended to people you live with or with whom you have regular contact. The level of assistance provided to you and your family will be determined by the nature of your challenges and your shared choices. This might be in the form of a group or individual session.</p>
                                        <p class="pt-serif">Self-care and management skills can assist you in understanding and overcoming schizophrenia symptoms. Your doctor or social worker may suggest that you seek help with self-management. You could also get peer assistance from others who have schizophrenia.</p>
                                        <p class="pt-serif">Self-care focuses on:</p>
                                        <ul class="ul">
                                            <li class="pt-serif"><i class="fa fa-circle"></i>Exercise</li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>Diet</li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>Relationships </li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>Daily routines</li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>Taking medication </li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>Recognizing your triggers and when you’re becoming unwell</li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>Keeping your recuperation going </li>
                                            <li class="pt-serif"><i class="fa fa-circle"></i>What to do in an emergency and where to seek assistance</li>
                                        </ul>
